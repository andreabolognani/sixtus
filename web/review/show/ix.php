<p>
	I tredici episodi di quest'opera si dividono in tre atti: la prima
	metà della serie vede tutto 'sto cast di rintronati scolastici
	trasformati nella resistenza umana contro l'oppressore angelico (la
	ragazzina di prima) alle prese con dei “piani” per vincere. Tra
	questi piani, ci sono concerti, il furto di buoni per la mensa,
	partite di baseball… cose del genere.
</p><p>
