	Comincia quindi una serie di puntate fuffa (compreso l'episodio in
	cui tutti vanno a pescare) in cui alcuni tizi si sforzano di
	esaudire i desideri di alcuni altri tizi, rimasti insoddisfatti
	della vita vera.
</p><p>
	Ma poi cominciano i problemi: in fila, spuntano il tizio malvagio
	che vuole donare la pace attraverso la morte, poi compaiono i cloni
	malvagi della ragazza angelo, infine arriva un NPC programmato per
	il male che trasforma le persone inutili in ombre divoratrici di
	anime.
</p><p>
	E quindi, botte. Sparatorie, in particolare, senza alcun motivo. Se
	la serie prometteva (e spesso mantiene) d'essere un dramma
	scolastico, fin troppo spesso apre una finestra in cui tutto diventa
	un survival (d'azione oppure horror). Non capisco.
</p>
