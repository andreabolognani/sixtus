<h2>
	Angel Beats!
</h2><h2 class="reverse">
	Buono
</h2><p>
	Insomma, è una serie che si lascia guardare, ma non l'ho messa in
	quella categoria perché fa un po' di più. Gioca.
</p><p>
	Avendo un buon numero di personaggi a disposizione, la serie si
	sposta da uno all'altro senza preoccuparsi molto di dirci cosa gli
	altri stiano facendo in quel momento. In più, piovo momenti
	magnifici, c'è gente che spara e che muore in ogni episodio, c'è una
	gilda segreta produttrice di armi nascosta &gt;9000 metri
	sottoterra, ci sono i mostri, il pesce gigante. In questa serie c'è
	tutto.
