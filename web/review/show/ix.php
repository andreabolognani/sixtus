	tanto le prendono, ogni tanto le danno. Poco importa.
</p><p>
	Alla fine di queste vicende si scopre che il ciclo non è affatto
	infinito: basta infatti risolvere i propri problemi interiori per
	guadagnarsi la libertà dal purgatorio. Fondamentalmente, chi si
	sente pronto può andarsene, e scomparire.
</p><p>
	Parte quindi un secondo atto in cui il protagonista, morto con
	palettate di dramma (come la maggior parte del cast, a dire il vero,
	dramma a carrette, a camionate, a pioggia…) capisce che
</p><ul><li>
