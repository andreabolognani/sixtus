<h2>La morte</h2>
</p><p>
Il 13 agosto 2005 seppi che mia madre stava morendo.
</p><p>

</p><p>
Tornai a casa, come facevo ormai ogni giorno da oltre sei mesi, alle 06:30, dopo
essere tornato per la quarta o quinta volta, sulla cima più alta in vista. Lì in
cima c'era una pletora di impianti di trasmissione, antenne delle radio, delle
TV, a decine. Lassù però c'era anche una delle viste migliori che potessi
raggiungere con soli sette minuti di volo. Da lì potevo ascoltare il silenzio,
dormire se ne avessi avuto voglia, o comunque passare il tempo studiando la
miriade di sentierini sull'altopiano di quella cima. E soprattutto potevo
restare solo, senza avere persone lente attorno.
</p><p>
Quando rientrai a casa, come d'abitudine, mi rimisi nel letto, aspettai finché
sentii qualche rumore, poi, come una persona normale, finsi di rigirarmi nel
letto, di voler dormire altri cinque minuti. Facevo così perché qualche volta
capitava che mia madre o mio padre se ne entrassero in camera mia, come se
avessi ancora sei anni, e che venissero a svegliarmi. Non volendo insospettirli,
quindi, tentavo come potevo di comportarmi come uno normale. Quella mattina mia
madre entrò ed io pensai ``Blu''.
</p><p>
Pensando che ancora dormissi, mia madre mi accarezzò la testa, mi chiamò e io
feci finta di avere ancora gli occhi e la voce impastati dal sonno, e chiesi chi
fosse. Lei sorrise.
</p><p>
Ma continuavo a pensare ``Blu''. Non capico perché. Al tavolo della colazione,
come accadeva un po' tutti i giorni, nessuno parlò.
</p><p>
Bevendo il caffé, pensai ancora ``Blu''. Imburrando una fetta biscottata, pensai
``Fegato''. Mangiandola, pensai ``Annegamento''.
</p><p>
Poi la colazione finì e mi chiesi che avessi quel giorno. Temetti che finalmente
arrivassero quegli effetti collaterali che irrimediabilmente seguono
l'acquisizione di incredibili e soprannaturali poteri. Ma deposi quell'idea,
perché non appena i miei furono usciti di casa tutto passò e non ebbi altri
pensieri fissi e improbabili.
</p><p>
E poiché ero in vacanza, lunga e meritata vacanza, non ci pensai più. Non avendo
appuntamenti fissati, fui l'ultimo ad uscire di casa, entrambi i miei genitori
se ne andarono prima di me, per recarsi al lavoro.
</p><p>
Me ne uscii anch'io, per andare a zonzo. Non avevo alcuna prospettiva, in quel
periodo: ero libero, fresco, avevo fin troppo tempo libero, potevo andare dove
volessi, senza alcun problema. Per una volta, pensai, me ne sarei rimasto in
città, sperando di trovare qualcuno. Quel giorno era giovedì, giorno di mercato:
me ne andai dunque al mercato, per vedere cosa c'era.
</p><p>
E c'era un sacco di roba. Borse, scarpe, pollo arrosto, sciarpe, costumi da
bagno, detersivi, giocattoli di pessima qualità, orologi, aspirapolveri; ogni
genere di chincaglieria, tutto per tasche comuni, quasi tutto trattabile.
``Guardi quant'è bello questo tessuto'', ``Senta com'è morbido questo stivale'',
``Oh che saporito questo formaggio'', tutta roba del genere.
</p><p>
E c'era ovviamente un gran baccano, gente che si chiedeva se valesse la pena di
comprare quelle ciabatte oppure no, che ``Poi a mio marito non piacerebbero'',
gente che tentava di ricordare la lista delle cose che avrebbero dovuto
prendere, c'era anche un inusuale numero di bambini, troppo grandi per l'asilo,
troppo piccoli per stare a casa da soli, che seguendo le madri al mercato si
ostinavano a desiderare la metà delle cose che avevano sott'occhio.
</p><p>
C'era veramente un gran baccano, tanto che ne ebbi piena la testa e decisi di
allontanarmi da lì. Finii in piazza, dove il mercato non arriva e la gente non
si accalca, non più di tanto, almeno. Due ragazze, ferme alla fermata
dell'autobus, di stavano lamentando apertamente di quanto l'autobus non
arrivasse mai. La cosa mi infastidì non poco, dato che non erano affatto sole
alla fermata; e neanche parlavano tra di loro, ma cacciavano lamenti l'una
sull'altra, senza ascoltarsi. Me ne andai, più per abitudine che per bisogno,
alla fontana che sta di fronte alla fermata, a lato della piazza, e bevvi un
sorso.
</p><p>
Quando mi voltai, non riuscendo ad ignorare quelle due chiaccherone, le vidi
entrambe intende a messaggiare con i propri cellulare, senza smettere di
lamentarsi ad alta voce di questo e di quello. Una stava scrivendo al suo
ragazzo, chiamandolo con almeno quattro appellativi mielosi in un messaggio di
trenta parole, informandolo che sarebbe arrivata tardi perché lei e la sua amica
stavano ancora aspettando l'autobus; l'altra stava invece scrivendo ad una terza
amica, con la quale si sarebbero dovute incontrare quella sera, a cena.
</p><p>
Non è affatto naturale che due persone si mettano a parlare così dei propri
affari in mezzo ad una piazza. La cosa mi urtò oltre ogni sorta di
sopportazione, perché vanno bene la spontaneità e l'amicizia, però c'è anche
gente che vorrebbe pensare agli affari propri. Scattai (a passo normale, non con
il mio nuovo innaturale scatto) verso queste due ragazze con l'intenzione di dir
loro qualcosa, quando una di loro mi vide e disse: ``To' guarda questo, passa
senza guardare se arrivano, che sei passa qualcuno lo stira''.
</p><p>
``Pure i tempi dei verbi, adesso?'' pensai io andandole incontro, ma poi feci
caso ad una cosa e mi fermai in mezzo alla piazza. Cosa che tra l'altro non era
affatto pericolosa: la piazza è carrozzabile soltanto ad autobus e taxi, e ne
giravano talmente pochi che avrei potuto sdraiarmi per terra e farmi una
dormita; ma non avevo più bisogno di dormire, tra le altre cose.
</p><p>
La cosa che notai fu che la ragazza parlava a bocca chiusa. La sua amica, lì
accanto, mi vide pure e disse: ``Oh, quel tizio mi sta guardando. Peccato che
non sia carino!''. E lo disse senza muovere le labbra.
</p><p>
Allora mi girai e tornai sui miei passi, per bere un altro goccio d'acqua alla
fontana; forse m'avrebbe fatto bene. E feci una cosa sbagliata: smisi per un
attimo di pensare alle due ragazze in particolare, cercando invece di sentire,
oltre il suono dell'acqua della fonta, il rumore della piazza. E fu come se
fossi nuovamente in mezzo al mercato, con tutte le dicerie e il chiacchericcio
della gente.
</p><p>
Potevo sentire che l'edicolante non aveva affatto voglia di scambiare due
chiacchere con quel pensionato, che arrivava tutti i giorni alla stessa ora, ma
che ci poteva fare? Era la cortesia del lavoro, necessaria a mantenere la
clientela. Potevo sentire anche quel pensionato, che aveva la stessa voglia di
parlare con quell'edicolante invadente, ma in fondo sapeva che quello era il suo
lavoro.
</p><p>
Mi voltai e controllai le due ragazze di prima: erano ancora lì, e una delle due
vide che le stavo osservando e cacciò un commento poco carino nei miei
confronti. La ragazza del bar invece pensava soltanto a staccare dal turno con
un quarto d'ora d'anticipo, per potersi andare a comprare quelle scarpe in
offerta che aveva visto in vetrina al negozio dietro l'angolo, prima che qualcun
altro gliele sottraesse.
</p><p>
L'uomo al bancone della gelateria invece pensava che la gente non dovrebbe
mangiare così tanto gelato, anche se faceva caldo, e che erano soltanto le 09:15
e gli mancavano ancora tre ore per finire il turno e andare a mangiare qualcosa.
</p><p>
E capii allora una cosa molto importante: la concentrazione è una cosa senza la
quale non si può ottenere nulla; in particolare il silenzio. Fissando qualcuno,
ero decisamente in grado di sentire i suoi pensieri; non fissando qualcuno, ero
invece in grado di sentire i pensieri di tutta la piazza. E non fu affatto
carino, da parte della piazza. Tentai di focalizzarmi sul ragazzo della
gelateria, che pareva avere pensieri né particolarmente negativi, né
particolarmente positivi, e pareva abbastanza pacifico. Poi però ebbe uno scatto
di follia, perché ricevette un messaggio sul cellulare; dopo l'ultimo cliente
della coda andò a controllare e seppe da un amico che in quel momento la sua
ragazza era stata vista in compagnia di un altro tizio. Ed io smisi di sentire
poco e cominciai a sentire molto.
</p><p>
Mi allontanai dalla piazza tenendomi le orecchie con le mani, ma non erano le
orecchie il problema, e fissando per terra. In qualche modo, arrivai abbastanza
lontano da non sentirmi la testa scoppiare di pensieri.
</p><p>
E mi chiesi, per prima cosa, se per caso non avrei guadagnato nuovi poteri ogni
sei o sette mesi... Poi tornai a concentrarmi sulla lettura del pensiero, che mi
dicono sia una cosa parecchio utile per farsi strada nella vita.
</p><p>
E lo fu.
</p><p>
Ora posso dire che la percezione del pensiero, come ogni altro senso, come il
dolore, può essere tenuto sotto controllo dalla mente, basta avere la disciplina
sufficiente. Allora questo non mi era chiaro.
</p><p>
Non appena smettevo di pensare attivamente a qualcosa, cominciavo a sentire
tutto quello che stava attorno. Come quando tenti di sentire qualcosa di
lontano, come quando ti sforzi di seguire due discorsi; soltanto che la un
discorso era il mio pensiero cosciente, l'altro discorso era il vocio di
centinaia di passanti, che copriva il mio come la musica copre la voce di quello
che tenta di ordinare da bere al banco durante un concerto.
</p><p>
Fortunatamente, già ai tempi, avevo sperimentato altre forme di dolore
estremamente acute: pellicine che si strappano, scheggie nella pianta del piede,
punture d'ape (non azzardatevi a commentare a meno che non siate stati punti da
un'ape: provare per credere, ho quasi pianto) e dolore pungente direttamente nel
cervello per aver acquisito un libro senza aprirlo. La mia mente deve aver
collegato da sola i due tipi di dolore, tentando di combatterli nello stesso
modo: soltanto, resistere al rumore continuo si rivelò più difficile che
resistere ad un dolore concentrato in pochi secondi.
</p><p>
Funziona un po' come l'udito: basta non fare caso alle cose; un po' come in
discoteca, dopo quattro ore di musica martellante nelle orecchie quella smette
di darti fastidio, ma non per intorpidimento, quanto per filtro. Quando devi
attraversare la strada, controlli da ogni lato se stiano arrivando auto, non
controlli il resto; il principio è lo stesso, filtrare le informazioni
sensoriali anziché analizzarle tutte. Lo si impara da piccoli, con gli occhi,
con le orecchie, con la pelle; lo imparai anch'io, un po' cresciuto ormai, per
la percezione del pensiero.
</p><p>
Non funzionò da subito, ma con calma, dopo un paio d'ore (era quasi mezzogiorno)
riuscii ad evitare di far caso a tutti i pensieri più stupidi (sono presenti in
una quantità assurdamente elevata, e sono spesso estremamente basilari), poi a
mettere tutto in sordina, e non fare più caso a nulla, se non cercando di
ascoltare. In fondo, non era niente d'impossibile, era soltanto una cosa mai
fatta prima. Quando poi fu mezzogiorno, ero divenuto abbastanza abile anche con
questa capacità, e me ne tornai a casa per pranzo.
</p><p>
Mi sarei aspettato di trovare i miei genitori a casa, ma quando arrivai la casa
era vuota. Trovai due cose: un biglietto lasciato per me sul tavolo della
cucina, in bella vista, e una grossa busta, che invece era stata lasciata in un
cassetto in corridoio, non molto ben riposta.
</p><p>
Il biglietto, scritto a mano da mio padre, mi annunciava che entrambi sarebbero
andati a pranzo, così senza particolare motivo, perché a mio padre piace portare
mia madre fuori a pranzo. Diceva anche che forse sarebbero tornati per cena,
forse no, forse avrebbero mangiato fuori, chissà. Cose tipiche di mio padre,
pensai.
</p><p>
Misi sul fuoco una pentola con l'acqua per la pasta, per una persona soltanto.
Poi pensai che in realtà avrei potuto non cucinare affatto, dato che potevo
campare anche senza. Spensi il fuoco.
</p><p>
Tornai in corridoio per sistemare meglio quella grossa busta che sporgeva dal
cassetto, lo aprii e presi la busta in mano, per verificare con l'altra se il
cassetto avesse effettivamente abbastanza spazio libero per contenere tutto
l'incartamento; mi cadde l'occhio sulla busta, che portava un timbro
dell'ospedale. La data era di quel giorno, il nome che compariva sulla busta
quello di mia madre. Aprii la busta con un certo timore.
</p><p>
Conteneva un bel mucchio di carte, una lunga lista di analisi, esiti di esami,
radiografie, cose mediche. Cose mediche non buone.
</p><p>
Tra tutto quello che mi era capitato di leggere, c'erano parecchi testi di
medicina; manuali per lo più, enciclopedie, volumi che avevo aggiunto all'elenco
perché non avevo motivi per scartarli. Molti di quei termini m'avrebbero fatto
meno paura se fossero rimasti sconosciuti.
</p><p>
Lessi quasi metà di quel referto prima di ricordarmi di ciò che potevo fare e
acquisirlo. Non era affatto buono, era pessimo, in effetti. Le davano qualche
settimana di vita. La motivazione non era nota, la colpa di tutto era però
imputabile al fegato, che stava cedendo. Cose che capitano, immaginai, a volte
le cose si rompono e smettono di funzionare. Tuttavia, si ipotizzava che un
trapianto avrebbe potuto salvarla, se si fosse trovato un donatore compatibile
entro otto giorni. Consultando le fonti che avevo memorizzato, trovai una mezza
dozzina di casi simili, quattro dei quali sopravvisuti e completamente ripresi.
</p><p>
Decisi che l'acqua per la pasta avrebbe potuto aspettare tutto il tempo
necessario. Se potevo volare, correre come un treno e leggere i pensieri della
gente, credetti proprio di poter trovare un fegato compatibile.
</p><p>
Intenzionato ad uscire, mi diressi verso la portafinestra che da sul mio
balcone, ma il destino aveva una valida ragione per volermi ancora a casa.
</p><p>
Squillò il telefono.
</p><p>
Abbastanza infastidito dal tempismo della telefonata, presi la cornetta e
risposi. Dall'altra parte, una voce maschile stanca e piuttosto abituata a
discorsi carichi di una certa drammaticità, si presentò come agente della
polizia stradale e chiese di parlare espressamente con me.
</p><p>
L'incidente era stato causato da un furgone carico di verdura, che scendeva
dalla valle lungo la strada. Avevo invaso l'altra corsia e spinto l'auto dei
miei genitori oltre il guardrail, giù per quasi trenta metri di scarpata. Erano
morti sul colpo.
</p><p>
L'agente, professionalmente, attese qualche istante prima di comunicarmi che
avrei dovuto presentarmi all'ospedale il prima possibile per riconoscere
entrambi i cadaveri e procedere a firmare un po' di carte.
</p><p>
Poi attese un altro po', si assicurò che avessi capito e riattaccò. Abbassai il
telefono, rimisi la cornetta al suo posto. Poi uscii di casa lasciando la
finestra della camera spalancata e volai all'ospedale. Non mi preoccupai di
essere visto, essendo pieno giorno.
</p><p>
Scesi a terra nel parcheggio più lontano dalla strada, seguii le indicazioni che
l'agente mi aveva lasciato per telefono, arrivai all'obitorio dell'ospedale.
</p><p>
Erano stesi su due letti in acciaio, coperti con un telo azzurrino, proprio come
si vede in televisione. Parlai sia con i medici che con gli agenti di polizia,
mostrai loro un documento, mi presentai all'agente che avevo sentito per
telefono, chiesi di vedere i miei genitori.
</p><p>
Un medico stranamente giovane nell'aspetto e anziano nel modo di fare scostò il
lenzuolo dal volto di mio padre prima, di mia madre poi. Quindi affermai a voce
e per iscritto chi fossero, chiesi se ci fosse altro, poi mi fu chiesto se
volessi essere lasciato solo per qualche minuto.
</p><p>
Tutti i presenti lasciarno la stanza tranne un medico, che evidentemente doveva
rimanere a presidiare quel luogo in ogni caso. Non badai a lui, presi i miei
genitori per mano e percepii quanto fossero freddi. Mi ricordarono quei libri
che, per provare, avevo ripreso in mano dopo la prima acquisizione; la
sensazione di vuoto era indice inconfutabile di come avessi già preso tutto ciò
che c'era. Per quelle due mani ebbi esattamente la stessa sensazione: non erano
più di questo mondo, non avrebbero più dato alcunché.
</p><p>
Per tutta la vita avevo desiderato di essere lasciato solo per starmene in pace:
mi sbagliavo. Ridistesi le mani fredde che tenevo nelle mie lunghi i fianchi
freddi, li baciai entrambi sulla fronte, rimisi a posto i due lenzuoli.
</p><p>
Me ne andai da quella stanza piena di morti. Uscii, vidi fuori in attesa il
medico giovane ma anziano e tre agenti di polizia, tra di loro quello che era
stato il mio `contatto', che mi disse di come avrei dovuto ricevere
comunicazione postale tramite raccomandata dal comune sulle procedure per i
funerali. Dissi che forse era presto, e chiesi di poter vedere l'autista del
furgone. Tutti loro mi dissero che non era possibile, per una lunga serie di
motivi. Ma non volevo che rispondessero alla domanda, volevo che pensassero a
quel tizio. E quel medico dall'età falsata lo fece: pensò al nome di quel tizio,
alla sua faccia, ma soprattutto alla stanza in cui era tenuto, con numero,
reparto e piano.
</p><p>
Stava fin troppo bene, il bastardo. Non aveva preso altro che una botta sul
naso, era dentro soltanto per accertamenti. Perché non si nutrissero sospetti,
era tenuto in una stanza normale, assieme ad altri due degenti, senza nessuna
sorveglianza; era legato al letto, però.
</p><p>
Non essendoci nessun particolare controllo lungo il percorso, non dovetti fare
nient'altro che dare l'impressione di andarmene, tornare un attimino indietro e
seguire la strada più breve per la sua stanza. Non ebbi nemmeno bisogno di
entrarci: passando per il corridoio, giunsi davanti alla sua stanza, sentii che
stava ancora pensando all'incidente e seppi che era lui.
</p><p>
Non stava piangendo per le sue azioni, non stava cercando le parole per
scusarsi, non stava nemmeno chiedendosi dove fossero i suoi parenti. Si stava
chiedendo quanto gli sarebbe costato far riparare il furgone, quanto aveva perso
in guadagni da verdura, quanto l'assicurazione avrebbe coperto.
</p><p>
Sperando vivamente che si voltasse a guardarmi, lo fissai dritto negli occhi e
pensai ``Se ho potuto spegnere i lampioni di tutto un parcheggio, pensa che
potrei fare a te, stronzo''. I suoi pensieri tacquero.
</p><p>
Immaginando che dormisse, nessuno si preoccupò delle sue condizioni per quasi
mezza giornata: quella sera, allora di cena, un'infermiera tentò di svegliarlo e
constatò che il suo cuore era fermo.
</p><p>
Quella sera rimasi a casa, e dormii tutta la notte nel mio letto.
</p><p>
E sognai un cane.
</p><p>
