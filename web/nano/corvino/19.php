conoscerla a fondo. E forse non moriremo''
</p><p>
Poi se ne andò, svanendo.
</p><p>
Solo in quel momento realizzai che, per essendo sempre con me, Battesimo e
Smeraldino potevano volatilizzarsi e lasciarmi solo.
</p><p>
Li avevo chiamati senza pensarci, quando ne avevo bisogno. Ma non era mai andato
a cercarli.
</p><p>
Ma Battesimo aveva ragione, Camelia era una preziosa fonte di informazioni,
sulle \streghe\ e sulla mia vita passata. Decisi che l'avrei fatta parlare. Di
tutto.
</p><p>

</p></div><div class="section"><p>

</p><p>
Andai a cercarla.
</p><p>
Ma non fu affatto facile. Non avevo idee, non sapevo da dove cominciare. Mi
incamminai.
</p><p>
Uscii di casa, scesi le scale. Feci cose normali.
</p><p>
Pensai. Pensai a dove potesse andare a cacciarsi qualcuno che sopravvive solo
nella mia mente.
</p><p>
La mia mente tornò al passato. Per un anno intero, quando se n'era andata in
Francia (ma c'era poi andata veramente?) ero andato a zonzo per la città
pensando a lei, sperando di trovarla. Che funzionasse in quel modo?
</p><p>
Provai.
</p><p>
Me ne andai a zonzo.
</p><p>
Passai per la sua vecchia casa. Passai per il parcheggio con il lampione
lampeggiante.
</p><p>
Passai al parco, mi fermai nel punto in cui la incontrai per la prima volta. Non
c'era. Me ne andai.
</p><p>
Passai per altri posti, un po' qua un po' là, ma non era da nessuna parte.
</p><p>
Passai infine per l'altro parco, raggiunsi le altalene, là dove quell'ultima
sera passata insieme c'eravamo fermati a chiaccherare.
</p><p>

</p></div><div class="section"><p>

</p><p>
Era là.
</p><p>
Stava seduta su una pachina, di fronte alle altalene. Le sedetti accanto.
</p><p>
``Mi hai trovata'' disse.
</p><p>
``Non proprio. T'ho cercata per tutti i posti che mi sono venuti in mente. Ho
solo cercato una panchina vicina'' risposi.
</p><p>
Non credevo che mentendo avrei migliorato la situazione; e poi lei viveva nella
mia mente, non credevo di poterle tenere dei segreti.
</p><p>

</p></div><div class="section"><p>

</p><p>
``Abbiamo tutto il tempo, il mio'' cominciai ``Dimmi, cos'è successo quella
sera?''
</p><p>
Non rispose.
</p><p>
``Ho riflettuto, sai? Rifarei quello che ho fatto, era la cosa giusta''
</p><p>
Feci una pausa.
</p><p>
``E' grazie a quella decisione che ho imparato a volare. Senza, chissà che
sarebbe successo!''
</p><p>
``Saresti morto'' disse lei.
</p><p>

</p></div><div class="section"><p>

</p><p>
``E sarebbe stato poi tanto diverso da ora? Se mi avessi ucciso quella sera?''
chiesi.
</p><p>
``Non capisci'' rispose.
</p><p>
Cominciai a spazientirmi ``No, infatti. Come potrei, scusa?''
</p><p>
``Tu mi hai resistito!'' disse lei ``Mi hai resistito due volte!''
</p><p>
Sospirò.
</p><p>
``Non era mai successo. Mai''
</p><p>
Si voltò dall'altra parte.
</p><p>
Per `resistere' intendeva forse il tirarsi indietro alla proposta del bacio? O
parlava del non tirarsi su affatto, quella volta nel bosco? Non capivo.
</p><p>
``Tu... tu eri strano. Avevi qualcosa che nessun altro aveva''
</p><p>
``La magia''
</p><p>
``Sì. No. Forse. Non lo so. Non davi alcun segno di risveglio, non ebbi sospetti
di quel genere. Eri troppo candido''
</p><p>
``Candido significa stupido?''
</p><p>
``Sì. Nessuno era mai stato così disinteressato. Tutti gli altri volevano
toccare, assaggiare. Tu invece no. Sarai mica... '' e mosse il mignolo.
</p><p>
``No'' dissi tirando indietro la testa perplesso.
</p><p>
Questa insinuazione mi ricordò assieme sia il fatto di apprezzare il violetto
durante i combattimenti, sia il fatto di essere multicolore, sia il fatto di
essere una \strega\ maschio. Mi venne il dubbio.
</p><p>
``No, non direi. E anche fosse? Problemi?''
</p><p>
``No, no, nessun problema'' e rise.
</p><p>
Il suo sorriso era ancora quello di una volta. Era umano.
</p><p>

</p></div><div class="section"><p>

</p><p>
``Allora cosa?'' chiesi, perché ero ancora sperduto.
</p><p>
``Tu mi affascinavi, va bene?'' soffiò ``Non capita spesso di stupirsi, dopo
seicento anni di attività! Non capita spesso che uno si dimostri così spontaneo
e poi volti via di fronte a tanta grazia''
</p><p>
``Eh'' dissi, senza motivo.
</p><p>
``Dannazione, io sono una \strega! Ho una certa reputazione, tra le altre! Non è
una cosa che possa prendere alla leggera, che credi?''
</p><p>
Le feci segno di continuare.
</p><p>
``Immagino che tu sappia già che sono raccoglitrice: rubo anni di vita ai
mortali e li commercio. E sono brava nel mio mestiere: riesco a sottrarre poco
alla volta, decadi, o anche anni singoli. E ti dirò che non è affatto facile, la
maggior parte di noi non è in grado di lasciare in vita il mortale di cui si
nutre''
</p><p>
``Ma tu hai detto'' la interruppi ``che sarei morto, quella sera''
</p><p>
``Si, infatti. Ti avrei voluto uccidere, ma volevo anche assaggiarti'' disse
lei.
</p><p>
La cosa stava diventando inquietante e vagamente eccitante; la lasciai
proseguire.
</p><p>
``Ero curiosa. Sapevo che ti allontanavi nei boschi, così ti ho teso un'esca. In
giro dicevano quanto fossi riservato, quindi pensai di utilizzare un'illusione
un po' più potente.
</p><p>
``Ma in qualche modo tu hai resistito, nonostante t'avessi offerto il piatto
forte. Nessuno s'era mai tirato indietro; tutti si risvegliavano una mezz'ora
dopo, senza memoria, senza forze e con dieci anni di vita in meno.
</p><p>
``Tu invece te ne sei andato senza dire una parola e hai avuto l'ardire di
piantarmi nuda nel bosco.
</p><p>
``Dopo quella volta, decisi che ti avrei osservato più attentamente. L'occasione
l'ebbi quella sera del gioco notturno. E quella volta c'eri persino cascato, ma
avevi bevuto quella robaccia impossibile e il tuo alito sapeva di cane morto e
di palude.
</p><p>
``Anche se sono una \strega, non significa che cose del genere non mi facciano
schifo!
</p><p>
``E alla fine, quando quella sera di novembre sono riuscita a catturarti e
trascinarti fino in casa mia, e t'ho invitato a prendere quello che non avevo
mai dovuto offrire a nessuno, perché tutti chiedevano per primi, tu osi
andartene nuovamente!
</p><p>
``Hai idea di quanto freddo facesse, quella sera? E mi ci è voluta tutta la sera
per convincerti ad entrare nel mio salotto! E quella sera t'avrei divorato
tutto intero: saresti morto lì dove stavi, in un paio di secondi''
</p><p>

</p></div><div class="section"><p>

</p><p>
Quest'idea mi suonò perversa e stranamente allettante.
</p><p>
``Dopo essere stata piantata a quel modo avrei avuto voglia di manifestarmi,
uscire a prenderti e spezzarti la schiena con queste mani.
</p><p>
``Ma te n'eri già andato, più in fretta di quanto mi aspettassi. Ero troppo
sorpresa per reagire
</p><p>
``Ho mantenuto il più stretto riserbo sul tuo conto, non parlai mai ad anima
viva della faccenda e ripresi a lavorare come al solito.
</p><p>
``Impiegai un po' di tempo, ma dopo sei mesi buoni riuscii a dimenticarti, a
lasciarmi la tua storia alle spalle e ad andare avanti normalmente.
</p><p>
``E che succede, quel giorno? Mentre sono al tavolo che mi lavoro due ragazzi di
ottima famiglia (nel senso dell'aspettativa di vita) spunti tu e quasi mi
sputtani davanti a centinaia di persone.
</p><p>
``E poi l'hai anche fatto, mi hai nominata nel mondo degli umani. Non avevo
scelta, dovevi morire e subito. Nessun tribunale streghesco avrebbe accettato
se t'avessi lasciato in vita, neanche per un paio di minuti per chiaccherare.
</p><p>
``Sai, non è un ambiente rilassato quello in cui viviamo. Ma per qualche motivo
non sono riuscita ad attaccarti a piena forza da subito. E tu sei stato capace
di assumere tutti i sette colori, anche più d'uno alla volta. Quello è stato
straordinario davvero.''
</p><p>
Sospirò.
</p><p>
``Nessuno, nessuno, né insegnante, né istruttore, né alcuna veterana millenaria
con cui abbia avuto modo di parlare ha mai preso in considerazione la
possibilità di essere picchiate a morte; nessuno mi aveva mai preparata alla
possibilità di venir battuta in combattimento, nessuno mi aveva preparata alla
sconfitta.
</p><p>
``E così mi sono ritrovata quì. Ho avuto paura, e mi sono nascosta. Poi per caso
ho incontrato i tuoi animali; ci siamo raccontati le nostre storie. Il lupo ha
messo una buona parola per te, e sono venuta a trovarti in sogno.
</p><p>
Poi cambiò tono, e si fece più aggressiva.
</p><p>
``Il vero problema è che tu sei un coglione, \corvino. Sei stupido come una
verza, non capisci un cazzo. Se m'avesse uccisa un drago avrei potuto sopportare
l'onta, ma invece sono stata uccisa dal più grosso cazzone del mondo.
</p><p>
``Il problema quì sei tu: tu non sai in che guaio hai messo te stesso e noi tuoi
\acquisiti! Affonderemo nella merda perché tu non sai nuotarci dentro''
</p><p>
E tacque.
