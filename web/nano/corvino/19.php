</p><p>
E la seconda, ben più terribile domanda, riguardò il futuro: tutto quello che
avrei ammazzato da allora in avanti avrebbe ripreso vita e mi avrebbe seguito?
</p><p>
Sinceramente, non credevo affatto che avere un grosso cagnolone e uno strano
pappagallo sarebbe stato un gran problema, per quando le due bestie fossero
grosse e parlanti, ma avere una \strega\ morta appresso? E magari più d'una?
</p><p>

</p></div><div class="section"><p>

</p><p>
``Se non hai intenzione di mangiarlo, lo do alle bestie'' disse lei, alzandosi
