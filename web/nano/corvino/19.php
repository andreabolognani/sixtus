</p><p>

</p></div><div class="section"><p>

</p><p>
Entrambi gli animali finiscono di mangiare, salutano e se ne vanno.
</p><p>
Lei, che stava sfaccendando dietro i fornelli, le padelle e i piatti, si volta e
