<h2>Io, \strega</h2>
<div class="intro"><p>
Una volta a casa, riflettei su quali fossero gli effetti dell'assimilazione.
</p></div><p>
Assimilando Battesimo ero effettivamente diventato lupo per un'intera
giornata.
</p><p>
Assimilando Smeraldino avevo assorbito i suoi poteri, le sue conoscenze e le
sue piume.
</p><p>
Assimilando \camelia, ero forse diventato una \strega?
</p><p>

</p></div><div class="section"><p>

</p><p>
Qual è il nome giusto per una \strega\ uomo? E' giusto? E' possibile?
</p><p>
Dovevo essere molto più stanco di quanto pensassi, mentalmente almeno, perché
quel quesito mi portò via tutto il pomeriggio e mi impedì di pensare ad altre
cose, ben più importanti.
</p><p>
Forse è stato meglio così, riflettendo a posteriori, perché probabilmente non
sarei potuto sopravvivere, se non mi fossi nascosto. Tant'è.
</p><p>

</p></div><div class="section"><p>

</p><p>
Rimasi chiuso nella mia stanza, a casa mia, al buio.
</p><p>
C'erano state molte cose difficili da accettare nella mia vita. Cose normali,
cose che capitano, ma comunque difficili da accettare.
</p><p>
Accettare il fatto di avere dei cugini. Accettare il fatto che devi farti male
per imparare ad andare in bicicletta. Accettare il fatto che la gente non sia
sempre a tua disposizione.
</p><p>
Accettare il fatto che le ragazze siano un mistero.
</p><p>
Accettare il fatto che un sacco di amici e di altra gente ne sa molto più di te,
accettare il fatto che un dannato professore di filosofia può rovinarti la vita
scolastica, il fatto che il bagno possa essere occupato da altri quando ne hai
più bisogno...
</p><p>
Accettare il fatto che la gente muoia.
</p><p>
Accettare il fatto che la gente possa o non possa accettare cose come queste.
</p><p>
Sono tutte cose con le quali tutti noi dobbiamo scontrarci, prima o poi.
</p><p>
Quella invece era una cosa con la quale pochi, anzi nessuno, s'era scontrato
prima. Era una cosa nuova; per me, per tutti.
</p><p>
Una cosa fondamentalmente sbagliata. O forse no? Beh, forse sì. Nel dubbio, sì.
</p><p>
Feci quello che fanno tutti al momento di una grossa scoperta inaspettata:
diffidai della sua naturalità e della sua legittimità.
</p><p>
Come fecero i conquistadores ai primi contatti con i nativi americani, come fece
l'homo sapiens quando incontrò il Neanderthal: provai l'istinto di ucciderlo.
</p><p>
Solo che in questo caso ero io l'estraneo pericoloso e sconosciuto da temere e
uccidere.
</p><p>
Ero scampato alla morte da circa mezz'ora, non mi sarei ucciso spontaneamente in
quel momento.
</p><p>

</p></div><div class="section"><p>

</p><p>
Passai quindi un bel paio d'ore fermo, catatonico, cercando di trovare il mio
posto nel mondo.
</p><p>
Avevo due genitori seppelliti da meno di un'ora; avevo uno zio eremita redivivo
che si stava occupando di ricucire questioni di famiglia con i nonni che non
vedevo se non a Natale e a Pasqua; avevo una casa vuota da sistemare; avevo un
lupo e un corvo vivi solo nella mia testa; avevo una \strega\ appena morta di
cui ero evidentemente l'unico erede.
</p><p>
Forse era un po' troppo per la mia mente. Non ressi il confronto, e mi
addormentai. Sorprendentemente, dormii abbastanza bene. Ma poi giunse il sogno,
e quel sogno, per quanto utile e istruttivo, fu anche terribile.
</p><p>

</p></div><div class="section"><p>...</p></div><div class="section"><p>

</p><p>
Nel sogno, mi svegliai a casa mia, nel mio letto.
</p><p>
Ero solo, Battesimo e Smeraldino erano irraggiungibili.
</p><p>
Mi venne anche sete, quindi mi alzai dal letto, uscii dalla mia stanza e me ne
andai in cucina.
</p><p>
Appena aperta la porta, sentii un delizioso profumo di colazione. O forse era
un pranzo, non saprei dire. So che camminai più in fretta verso la cucina.
</p><p>
La porta della cucina era aperta, appositamente per lasciare che il profumo mi
raggiungesse.
</p><p>
Ai fuochi, a mescolare, spadellare, grattuggiare, assaggiare, c'era lei.
</p><p>

</p></div><div class="section"><p>

</p><p>
Mi fermai sulla porta, impietrito.
</p><p>
Camelia si voltò, mi fissò negli occhi e disse ``Siedeti, \corvino. Dobbiamo
parlare''
</p><p>
Era la prima volta che le sentivo pronunciare il mio nome, dopo sei mesi o forse
di più. Per tutto il duello, mi aveva chiamato `\spontaneo'.
</p><p>
Servì da mangiare in due piatti, e mi fece sedere di fronte a lei.
</p><p>

</p></div><div class="section"><p>

</p><p>
``Questo non sta accadendo veramente'' esclamai.
</p><p>
Lei cominciò a mangiare.
</p><p>

</p></div><div class="section"><p>

</p><p>
Non sapendo che fare, rimasi lì, impalato. Ruotai gli occhi, tamburellai le
dita.
</p><p>
Lei continuò a mangiare, imperterrita. Al quarto boccone, indicandomi con la
forchetta e senza ingoiare, bofonchiò ``Si fredda''
</p><p>

</p></div><div class="section"><p>

</p><p>
D'un tratto, mi s'accese una lampadina in testa: se avendo battutto Battesimo
