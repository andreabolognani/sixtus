</p><p>

</p></div><div class="section"><p>...</p></div><div class="section"><p>

</p><p>
Mi sveglio.
</p><p>
Sono sudaticcio.
</p><p>
Mi alzo, scendo dal letto.
</p><p>

</p></div><div class="section"><p>

</p><p>
Fischio. Compare \battesimo.
</p><p>
Fischio ancora. Compare \smeraldino.
</p><p>

</p></div><div class="section"><p>

</p><p>
Il lupo si draida ai miei piedi. Lo accarezzo.
</p><p>
