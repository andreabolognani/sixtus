volontà, mantengono il proprio libero arbitrio.
</p><p>
Sono prigionieri, perché non possono abbandonare i confini, ma possono comunque
nascondersi e sparire. Può essere poi difficile andare a recuperarli, nei
recessi della propria mente.
</p><p>
Dovetti imparare a farlo.
</p><p>

</p></div><div class="section"><p>

</p><p>
Non sapevo che fosse successo tra noi. Non lo capivo.
</p><p>
Guardai il lupo, guardai il corvo. Quelli ricambiarono il mio sguardo con
