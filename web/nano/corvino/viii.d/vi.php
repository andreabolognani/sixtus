	<h2>
		Al parco
	</h2><p>
		Il giorno dopo, altre lezioni, altro ritorno a casa, altri
		compiti, altre cose, finalmente finì tutto quello che avevo da
		fare ed ebbi modo di andarmene.
	</p><p>
		Come il giorno precedente, andai a infilarmi il completo da
		corsa (non quello, quello era da lavare; ma ne avevo un secondo,
		per questo occasioni) e me ne tornai lungo il fiume, oltre i
		semafori, su per i marciapiedi, slalom tra i pedoni, su per la
		rampa, di nuovo al parco.
	</p><p>
		Essendo allora soltanto le cinque del pomeriggio, non c'era
		ancora gente che corresse. Meglio così.
	</p>
</div><div class="section">
	<p>
	</p><p>
Mi trascinai fino al parco, oltre il parcheggio. Non quello dietro, dove avevo
effettuato i miei esperimenti, ma quello davanti. Fortunatamente, non c'era
nessuno. Sfortunatamente, nessuno era là per darmi una mano. Ma non credo che mi
sarebbe stato più d'aiuto che d'impiccio dover spiegare a qualcuno come m'ero
rovinato così duro. Quindi solo soletto, porcando e bestemmiando, impiegai
soltanto una decina di minuti per coprire quei 150 metri che mi dividevano dalla
ma meta. E quando di arrivai, fui indeciso se sdraiarmi sull'erba oppure
stendermi su una panchina.
</p><p>
L'erba sarebbe stata più morbida, ma era ancora novembre e cominciavano ad
essere le undici di sera. E faceva freddo. Forse sarebbe stata la panchina. Ma
la panchina in metallo sarebbe stata ugualmente fredda, se non peggio.
</p><p>
Alla fine mi decisi e mi sdraiai per terra.
</p><p>
Non male.
</p><p>

Quando mi ripresi, non sapevo che ore fossero.
</p><p>
Feci per rialzarmi, ma la schiena si oppose. Si lasciai cadere, provai a
cambiare posizione, cercando di capire quali muscoli dolessero di più. Dolevano
tutti, in pratica. Mi rassegnai a rimanere lì qualche minuti ancora.
</p><p>
Cercando di dimenticare, o almeno accantonare, il dolore, mi sforzai di
ricordare i miei progressi di quella sera. E non erano pochi, in effetti.
Ritrovata un poco di lucidità, mi chiesi come si distribuisse il peso durante il
volo. Non essendo appoggiato a nulla, forse non m'avrebbe fatto male la
schiena... D'altronde, il volo spontaneo non era certo uno dei miei campi di
specializzazione, al tempo.
</p><p>
Senza particolare impegno, decisi di provare a sollevarmi, senza dovermi alzare.
Senza risultati, anche.
</p><p>
Come immaginavo, voler volare non basta. Gli uomini l'han voluto per svariate
migliaia di anni prima di riuscirci. Avrei potuto io, piccolo uomo, riuscirci in
una sera? O in tre sere, contando tutti i giorni? Alla fine, sì, ce la feci. Ma
mi ci volle un bel pezzo.
</p><p>
Cominciai cercando di ricordare tutto quello che stavo facendo le altre volte,
prima di staccarmi da terra. Forse James Matthew Barrie sapeva che dovevo fare,
quando scrisse che Peter Pan poteva volare grazie ai suoi ricordi felici, perché
ebbi effettivamente un ricordo felice abbastanza forte da tirarmi su.
Letteralmente. Non basta affatto voler volare, per volare.
</p><p>
Non per me, almeno. Ma mi bastò ricordare di averlo fatto. Il `voler volare' è
soltanto un desiderio, e a quanto pare molto raramente i desideri, in
particolar modo quelli relativi a sé, non sono positivi; e non possono essere
usati in questo ambito; quello che occorre è una spinta, una spinta emotiva.
Queste nascono spontaneamente (come quando volai la prima volta) ma possono
anche essere innescate dai ricordi; i ricordi diventano quindi molto importanti:
riportare la mente in una condizione positiva tramite un ricordo adeguato può
far la differenza tra un ragazzo con il mal di schiena disteso sull'erba in un
parco a mezzanotte e un ragazzo che vola verso casa sua.
</p><p>
Volare verso la propria casa è una cosa estremamente utile quando non si sta
bene. Vi dirò anche che volare sulla schiena, senza sapere dove si sta andando
non è puoi così rassicurante. E non sentirsi all'altezza di volare quando si
vola si traduce immediatamente nel non essere a quell'altezza, perché si scende.
L'attitudine positiva deve essere mantenuta senza pausa, perché con essa è
possibile anche scendere (senza danni) e arrivare dove si vuole; senza invece si
va sempre dalla stessa parte. E in effetti tutto quelli che feci fu pensare
``Due giorni fa ho volato, adesso invece voglio andare a casa'' e mantenere la
concentrazione sul quel pensiero.
</p><p>
E fu così che, anche in quelle condizioni non così buone, ritornai a casa. Come
alla mia prima esperienza, due giorni prima, atterrai sul balcone, che
fortunatamente da direttamente su camera mia. Solo che questa volta,
evidentemente, non ero stato l'unico ad entrare in camera mia; un qualche
genitore doveva essere passato di lì, durante la mia assenza, e doveva aver ben
pensato che la porta-finestra che da sul balcone fosse rimasta aperta per
dimenticanza, non dietro specifica intenzione. La specifica intenzione,
abbastanza larga, era che sarei potuto tornare volando a casa, e magari entrare
dalla finestra sarebbe stato comodo.
</p><p>
Fu così che fui costretto a sperimentare il volo a bassissima quota, il volo di
precisione. Lentamente, salii di un metro, sorpassai la balaustra, poi pensai
che sarebbe stato meglio non scendere in piena vista, quindi mi portai più
vicino che potei ad un grosso castagno che ornava la strada e scesi lungo il suo
fusto; una volta a terra, controllando più e più volte che nessuno se ne andasse
a spasso a quell'ora (era mezzanotte passata, ma non si sa mai) mi avvicinai
alla porta, estrassi le chiavi, ed entrai sul giroscale. Ora, la probabilità di
incontrare un coinquilino a quell'ora in quella stagione dovrebbe essere
abbastanza bassa, ma qualcuno accese la luce delle scale. Allora mi preoccupai
non poco e scesi in fretta. Non che fossi molto alto, non più di mezzo metro dal
pavimento, ma comunque persi la concentrazione e caddi. Come saltare dal letto e
cadere, come scavalcare un muretto, nulla di particolare, ma con la schiena in
quelle condizioni immaginai di dovermi sostenere con mani e piedi. Invece
atterrai in piedi, come i ginnasti alle gare, senza alcun dolore. Dopo un attimo
speso per realizzare che la schiena stava bene, sentii avvicinarsi i passi di
qualcuno, decisi che avrei finto e quindi comincia a salire le scale come se
niente fosse.
</p><p>
Chi scese era uno degli studenti universitari in affitto al quarto piano, subito
sotto il mio appartamento. Prima, fino all'anno prima, ci viveva un vecchiaccio
di quelli che non sopportano nessuno, né vicini né parenti, tirchio fino
all'osso, e mortalmente serio sulla legge che vuole il silenzio alla sera dopo
le nove. Quando finalmente decise di andarsene da questo mondo, uno dei suoi
figli che aveva poca briga e voglia di fare soldi decise di affittare
quell'appartamento a studenti o gente ugualmente strana, secondo un contratto
oscuro che forse neanche esisteva veramente. In barba al morto e alle sue
abitudini, quegli studenti erano dei gran casinisti, avevano ospiti di ogni
tipo, razza, colore, nazionalità, umore, ad ogni ora del giorno e della notte.
Uno di questi era il tizio che mi trovò sulle scale. Avendo fretta o sonno o
forse entrambe le condizioni, quel tizio se ne andò dritto dritto per la sua
strada e mi lasciò solo. Bene.
</p><p>
Non appena arrivai al pianerottolo successivo, mi fermai a controllare,
palpandomi la schiena, piegandomi in tutte le direzioni, scoprendo che in
effetti non mi doleva più, da nessuna parte. Stavo benone. Magnifico.
</p><p>
Così potei salire le scale con tutta calma, entrare in casa, togliermi le
scarpe, appendere la giacca e andarmene a dormire tutto tranquillo.
	</p>
