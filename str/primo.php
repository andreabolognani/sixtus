<p>
C'era una volta un ragazzo, giovane e ingenuo, che passava il suo tempo
aspettando il futuro. In possesso di un certo talento per la progettazione e
la realizzazione di macchine, sperava di poterne fare il proprio mestiere.
</p><p>
Un bel giorno, mentre passeggiava, giunse dal cielo una voce, che disse:
`Ragazzo, esiste un luogo dove la gente ha bisogno di qualcuno con un certo
talento per la progettazione e la realizzazione di macchine'. Rispose:
`ORLY? Ho io quel certo talento. Dove andrò?'. La voce riprese: `Ah, no, mi
sono sbagliato. Richiamerò tra un paio d'anni...'
</p><p>
Il ragazzo rimase di merda e tornò ai propri sogni, aspettando.
</p><p>
Dopo un paio d'anni, mentre passeggiava, giunse dal cielo una voce, che
disse: `Ragazzo, quel luogo esiste. Per raggiungerlo dovrai affrontare
terribili pericoli e dimostrare tutto il tuo valore, ma sappi anche che là
ti attende la più nobile delle principesse, la quale attende te soltanto'.
Rispose: `Indicami la via, ed io tenterò'. Riprese: `Non ancora, poiché
avrai bisogno della benedizione. Tieni, eccoti delle inutili scartoffie:
compila tutto scrupolosamente, portale un po' in giro e facci delle cose'.
</p><p>
Il ragazzo partì, sognando la principessa che attendeva lui soltanto, si
armò di coraggio e pazienza e partì con le scartoffie. Indugiò alcuni
giorni, meditando se fosse veramente il caso di abbandonare la sua casa, la
sua famiglia e i suoi luoghi per affrontare il viaggio; il pensiero della
principessa lo indusse a partire. Compilò scrupolosamente, le portò un po'
in giro e ci fece delle cose; cose che non piacquero subito a tutti. Ma
erano solo carte, e il ragazzo non se ne curò.
</p><p>
Esplicitati questi doveri, il ragazzo attese. Mentre passeggiava, giunse dal
cielo una nuvola, lucida e giallognola, e si posò sopra il giovane. Su di
lui piovve abbondate olio, ed il giovane si ritrovò fradicio, viscido e
attaccaticcio. `Eccoti la divina unzione' disse la voce: "Ora va'..."
</p><p>
Il ragazzo, gocciolando unto, partì. Giunse alla valle, vide in fondo ad
essa un castello, vide in cima ad esso una torre, vide dentro essa
l'agitarsi d'un fazzoletto, vide dietro esso la principessa che attendeva
lui soltanto.
</p><p>
Rinvigorito da quella visione, il ragazzo studiò il percorso e affrontò il
lungo viaggio, attraversò la valle, guadò il fiume, nuotò attraverso il
lago, salì sul monte, attraversò il valico, guadò la palude, si vece largo
nel bosco, scese nel canyon, scalò il picco, affrontò i troll, le manticore,
domò il grifone, volò oltre il massiccio montano, attraversò il deserto,
parlò al cospetto dell'oracolo, rispose ai quesiti della sfinge, sconfisse
la mummia, uccise il drago, uscì dal labirinto, sorpassò la colline, ed
infine giunse al castello.
</p><p>
Gli abitanti del castello lo accolsero: "Tu che se unto dalla nuvola, che
hai affrontato molti pericoli, ora ti attende l'ultima prova. Ecco, dovrai
realizzare per noi alcune macchine, sfruttando quel tuo certo talento; ma
non mangierai e non dormirai finché non avrai terminato. Hai tre giorni"
</p><p>
Il ragazzo, tribolato, ebbe un dubbio; ma il pensiero della principessa lo
sospinse ed egli affrontò anche questa prova. Senza mangiare, senza dormire,
lavorò incessantemente per gli abitanti del villaggio e in tre giorni compì
l'impresa e superò la prova. Poi, stanco e affamato, mangiò e dormì per tre
giorni.
</p><p>
All'alba del suo settimo giorno al castello, gli abitanti gli dissero: "Ohi,
hai mangiato e dormito per tre giorni, rimettiti al lavoro". "Come? - chiese
il giovane - non ho forse terminato il mio lavoro tre giorni fa?". "Non ti
avevamo forse detto che quella era solo la penultima prova? Ce ne saremo
dimenticati". Il giovane rifletté: "Fatemi incontrare la principessa che
attende me soltanto, ed io mi rimetterò al lavoro".
</p><p>
Al giovane fu indicata la via per la torre. Il ragazzo salì le lunghe e
ripide scale, aprì le porte e giunse alle stanze della principessa.
Trafelato per la lunga fatica, madido di sudore e con le gambe stanche, si
presentò alla principessa: "Oh principessa che attendi me soltanto, ho
attraversato mari e monti, laghi e foreste, ho fatto ciò che mi è stato
chiesto per i tuoi sudditi al castello ed ora giungo al tuo cospetto. La tua
attesa è terminata." La principessa era la donna più bella che il giovane
avesse visto in tutta la sua vita. Ammiccando con le sopracciglia, il
giovane le si avvicinò, le si sedette accanto e le prese la mano: "Oh
principessa che attendi me soltanto, che cosa vogliamo fare?". Dopo aver
menato uno schiaffo, la principessa, masticando un chewingum disse
rudemente: "Cioè, coso, non penserai mica che io, cioè, te la dia qui e
subito solo perché sei arrivato qua, cioè. Per chi m'hai preso? Adesso mi
porti con la tua macchina a comprare delle scarpe, stasera poi esco da sola
con le mie amiche e poi vediamo."
</p><p>
Il giovane, deluso e scoraggiato, invocò il cielo. Giunse allora una voce
che disse: "Ohi, giovane, che c'è che non va?".
</p><p>
Rispose: "Oh voce dal cielo, tutta 'sta trafila e adesso non mi cucco la
principessa?".
</p><p>
Riprese: "Eh, no. Che t'aspettavi? Adesso ti rimetti a lavorare per i
castellani, porti la principessa a comprarsi le scarpe e poi la lasci uscire
con le sue amiche."
</p><p>
Il giovane, trattenendo la rabbia, chiese: "Oh voce dal cielo, t'aspetti
veramente che io faccia tutto questo? Perché dovrei?"
</p><p>
La voce tuonò: "Ricordati che sei stato unto. Mi tutti lo sono. T'ho scelto
e unto apposta. Sai quanti tizi ho unto prima? Nessuno!"
</p><p>
"Puta caso che m'andasse comunque di fare tutto questo, alla fine posso
farci qualcosa con la principessa o no?"
</p><p>
"Beh, conoscendola, tra qualche anno e un centinaio di paia di scarpe, forse
otterrai un casto bacio sulla guancia"
</p><p>
Il giovane rifletté un momento. Scese dalla torre, e trovò gli abitanti che
chiedevano a gran voce: "Facci le macchine! Lavora! Sposta quello, carica
quell'altro, dai! Dai!".
</p><p>
Il ragazzo prese la via del ritorno, si voltò e dichiarò: "Ma anche no.
Ciao." e se tornò a casa sua.
</p>
