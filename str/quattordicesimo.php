</p></div><div class="section"><h2>
	Definitivamene definitivo, a colori &ndash; 29 novembre 2010
</h2><p>
	Ieri infatti mi sono messo d'impegno. Ho recuperato tre fogli rossi, tre
	fogli verdi. Quelli verdi eran finiti, ma per realizzare il fusto che
	regge i tre fiori me ne serviva un quarto. Non ci fu verso di recuperare
	altro che un cartoncino da 220gr marrone.
</p><p>
	Decisi che sarebbe andato bene, e cominciai da quello. Sì, perché se lo
	schema era già ben pronto nella mia testa, l'utilizzo del cartoncino non
	lo era. Avrebbe retto le pieghe senza strapparsi? Avrebbe retto il peso?
	Fortunatamente sì, resse.
</p><p>
	Ma il cartone è duro da piegare, e tra una cosa e l'altra nelle ultime
	due settimane ho piegato carta come un drago, risultato: le unghie del
	pollice e del medio della mano destra (quelle che uso per calcare le
	pieghe) si stanno consumando e cominciano a dolermi.
</p></div><div class="section"><p>
	Evviva l'ambidestria, ho cominciato a piegare con la sinistra. Ma mi ci
	è voluta quasi un'ora per finire soltanto il primo pezzo. Ovviamente, il
	cartone è troppo forte per restare chiuso da solo e le pieghe portanti
	tendono ad allargarsi, e parecchio.
</p><p>
	Tenendo un ritmo sempre più serrato, piegando con due dita alla volta
	(ho scoperto di essere immensamente più veloce nel calcare le pieghe
	avanti e indietro, con la mano sinistra) ho realizzato foglie, fiore,
	foglie, fiore, foglie e fiore.
</p><p>
	Lo stelo di cartone marrone è tanto forte da riuscire a tenere in
	equilibrio uno, due o tre fiori. Ho finito, esausto, a mezzanotte e un
	quarto.
</p></div><div class="section"><p>
	E pensai &laquo;&Egrave; normale che mi senta molto stanco dopo
	tutto questo lavoro, perché ho dovuto spendere degli EXP per
	realizzarlo!&raquo;
</p><p>
	Poi pensai a quanto fosse stupido quel ragionamento e mi sedetti al PC
	per visionare un filmaccio, `Mega Shark Vs Giant Octopus' che sconsiglio
	a chicchessia, perché si tratta di un film pessimo, che tenta di essere
	molto più di quanto non sia, e fallisce.
</p></div><div class="section"><p>
	Questa mattina, constatato che evidentemente non è possibile abbandonare
	tre fiori sul tavolo della cucina per una notte senza che i gatti o i
	fratelli (o forse i genitori) ci mettano mano abbattendo tutto, raccolgo
	i vari pezzi e, volendo portarli con me nella speranza di consegnare il
	tutto, mi chiedo come dovrei trasportarli senza che subiscano danni.
</p><h2>La scatola &ndash; 30 novembre 2010</h2><p>
	&laquo;Sarà il caso di metterli in una scatoletta&raquo; mi dissi. Una
	scatoletta di cartone, piccina da stare nello zaino, forte da non
	schiacciarsi, grande per tre fiori e il fusto.
</p><p>
	E mi ricordai di essere in possesso di una scatola perfettamente adatta,
	delle dimensioni corrette ma non solo. Era la scatola del mio God Gundam
	1/144, quel Gundam che può lanciare il Sekiha LoveLove Tenkyoken
	soltanto quando è pilotato da due persone che si amano, come dimostra
	l'ultimo episodio della serie.
</p><p>
	E un giro in Gundam è meglio che un giro in bicicletta, giusto?
</p></div><div class="section"><p>
	Ho affrontato le conseguenze della stupidità di quest'idea difendomi con
	la sincerità, perché erano idee completamente spontanee.
