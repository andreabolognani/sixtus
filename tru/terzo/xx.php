	che stando in piedi gli arriva appena all'anca.

	Simak non dice nulla.

	S'Mak si alza, riprende con se la lancia e si dirige verso il
	suo villaggio, o almeno immagino. Un paio di metri nel folto
	degli alberi ed ecco che scopare alla mia vista.

