	È qualcosa di regolare, veloce, che pesta sulla vegetazione. Qualcuno che corre. Ma i suoi che distinguo sono due, non sono soltanto passi. Tra un passo e l'altro, c'è qualcos'altro che si abbatte sul terreno.

	No, non sul terreno. Sulle ramaglie, forse? Si. Qualcuno sta avvicinandosi, di corsa, e utilizza qualcosa di pesante per farsi largo nella vegetazione.
	sec#
