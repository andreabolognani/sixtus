	Simak invece non si preoccupa. Tiene la sua espressione vuota e allunga la mano, porge l'enorme aculeo al gigante.

	Dietro di loro, intanto, sento avvicinarsi, con enormi tonfi, il suo da cui S'Mak sta probabilmente scappando.
