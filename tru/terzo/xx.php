	Simak allarga le braccia e mi fa segno con le mani di
	avvicinarmi. Lo deve fare un paio di volte, perché a me non va.
	Neache un po'.

	Ma lui insiste.

	Insiste un paio di volte. Poi comincia a spazientirsi. Aggrotta
	le sopracciglia. Rivedo in lui lo stesso sguardo che vidi in
	quel tizio sulla barca… il mozzo. Stevenson.

	Ha lo stesso sguardo dell'uomo che venne colpito da un fulmine
	prima di trasformarsi nel Lyznardh e spaccare la testa di un
	ammiraglio. Non voglio rivedere una cosa del genere.

	Decido di avvinarmi. Lui mi prende tra le braccia, mi solleva e
	salta, giù dal dorso della bestia fino al terreno. Ma non cade.
	Scende, ma senza cadere, senza accelerare.

	Mi posa delicatamente a terra.

