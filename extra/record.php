	bicicletta, mi sono detto
	begin#outside
		speak#gods#Perché prendere la bici per andare a correre se posso semplicemente correre di più?
	end#outside
	Così ho corso un po' di più.
	sec#
	E siccome la ciclabile lungo la zona delle Albere adesso pare essere aperta,
	sono potuto passare di là senza dover urlare «<span class="gods">In barba
	alla legge!</span>» mentre passavo.

	Con un totale di quattro pause acqua, rispettivamente due alla fontanella
	della funivia e due al parco di Cristo Re, mi sono fatto la mia bella corsa
	al sole e all'ombra, sull'asfalto e sull'erba, con i cani e senza i cani.

	In tristezza, siccom'era prestino, non c'era quasi nessuno…

	Ma, alla fine, ho coperto i <span class="code">17.8km</span> del <a
	href="http://g.co/maps/d9vz5">percorso</a> in <span class="code">1h
	53'</span>, con una velocità media di <span class="code">9.6km/h</span>.
	Mica male…

	Ma la cosa più importante è che non sono morto. Il giorno dopo (ed anche
	quella sera stessa, in realtà) potevo ancora camminare.
tab#iii
	Pare che io non abbia scritto questa storia da nessun'altra parte. Quindi lo
	faccio qua.
	title#Quel sentiero che non esiste
	Un bel di ero triste e mesto e me ne andai a spasso, presi la ciclabile del
	Fersina, verso Sud come spesso vado a correre, giù e giù fino al nuovo ponte
	per l'uscita Trento Sud dell'A22.

	Traversato il ponte, tornai verso Nord, fino al ponte di Ravina, sotto
	l'autostrada e lì, al bivio, decisi di tentare d'arrivare direttamente a
	Belvedere. Scoprendo che una strada che tagli i campi non esiste, arrivai
	fino a San Nicolò. Da lì non c'è strada che prosegua verso Nord, ma
	soltanto quell'imbocco per l'autostrada.

	Non volendo rinunciare, m'arrampicai su per la strada, verso Sardagna,
	trovai un pezzo di strada bianca che saliva per il bosco. Da lì, una pista
	sottile sottile puntava nuovamente verso Nord. Ed ecco l'avventura.

	Seguendo quella pista quasi invisibile attraverso il bosco, mi ritrovai là
	dove la cascata di Sardagna taglia il percorso. Attorno ad un albero, sotto
	un sasso, oltre la tana di chissà quale bestia, eccomi dall'altro lato. Su,
	su e su, se notate c'è un piccolo cocuzzolo di roccia che spunta dal bosco.
	E fui lì.

	Più in là, monotonia. Il bosco non cambia, ma si può osservare l'albergo
	giallo di Sardagna, direttamente da sotto. Una mezz'ora dopo, seguendo il
	contorno del cambio di vegetazione, sbucai sul percorso pedonale che porta
	da Piedicastello a Sardagna. Ed ora so dove portano quei tre metri di
	strada.
