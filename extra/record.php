tab#intro
	Un bel giorno (ieri) mi sono messo qui a pensare a tutte le mie imprese
	storiche… non sono esattamente incredibili, non sono cose che possono finire
	sul giornale, e soprattutto non sono cose che qualunque altro atleta possa
	fare senza particolare preparazione.
	
	Ma sono tutte cose che io posso fare, e contemporaneamente cose per le quali
	non riesco a trovare compagni disposti a seguirmi.
	sec#
	Allora, partendo da alcune imprese del passato che non posso datare ma che
	riesco a ricordare, elencherò qui un po' di cose, invece che metterle nelle
	storie.
