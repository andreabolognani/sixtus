	Transformers: Dark Of The Moon
</h2><h2 class="reverse">
	No.
</h2><p>
	Questo film è stata la più grossa delusione cinematografica. Questo film
	m'ha deluso al livello di Spider-Man 3. Questo film è stato innaffiato
	di soldi, gli è stato dato del potenziale. Ma tutto questo non è andato
	soltanto sprecato, questi soldi sono stati spesi per i motivi sbaglati.

	Non mi capacito di quanto fuori obiettivo l'intera produzione sia
	riuscita ad andre, mi stupisco sinceramente dei guadagni che questa
	schifezza sta facendo e non riesco a comprendere coloro che pensano che
	un tale sfacelo possa essere giustificato dalle esplosioni, dagli
	effetti e dalla gnocca.

	Requiescat in pace.
</p>
