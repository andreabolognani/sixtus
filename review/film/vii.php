	letto dai titoli, io lo sapevo. Leonard Nimoy ha offerto la sua voce, a
	venticinque anni di distanza, per Sentinel Prime.

	Quello che probabilmente non sapete è che nel VERO film dei
	Transformers, quello animato del 1986, Nimoy doppiò Galvatron.

	Ora, su come il vecchio Nimoy abbia interpretato il traditore mi
	pronuncerò dopo aver sofferto il film in lingua originale. Intanto,
	però, quel che ho sentito.
</p>
</div><div class="section">
<p>
	Io posso capire che abbia una certa età (c'ha ottant'anni, l'omino) e
	che la voglia di lavorare ce l'abbia ancora, e se vengono a chiederti di
	rifare un ruolo per Transformers magari lo farei anch'io.

	Posso capire che l'intera carriera di Nimoy, per quanto lunga e vasta,
	sia inevitabilmente cablata attorno al suo ruolo storico. Abbastanza da
	far dimenticare a tutti il resto del suo lavoro. Comunque.

	Il dominio di Spock porta con se alcune chicchine classiche, come il
	saluto con la manina, la presa alla Mr.Spock, e alcune frasi. Una in
	particolare. La frase pronunciata in punto di morte. Una frase
	importante.
</p>
</div><div class="section">
<p>
	Le ultime parole di Spock sono qualcosa di piuttosto importante. Sono
	storiche, evocano un significato e ricordi per molte persone. In
	particolar modo pronunciate con quella voce.

	E credo che nemmeno Nimoy le possa rivendicare come sue, ormai. Sono
	state consegnate alla storia tempo fa e non gli appertengono più.

	È per questo motivo che rimango intensamente deluso nel sentire Sentinel
	Prime, deciso a portare avanti il suo tradimento – seppur per una
	“buona” causa – anni dopo il termine della guerra, citare quelle stesse
	parole, in quella stessa voce, dal lato sbagliato.

	Mr. Spock le pronuciò nel sacrificare la propria vita, Sentinel Prime
	usa quelle stesse parole per giustificare un sacrificio planetario nel
	tentativo di resuscitare un pianeta morto.
</p>
</div><div class="section">
<p>
	Questo è sbagliato.
</p>
</div><div class="section"><a id="II/II"></a>
<p>
	Prima non mi ci sono soffermato. Ma questa trama, come anche ma peggio
	dei capitoli precedenti, è piena di buchi. È traforata, è sbrindellata,
	è lacera.

	Se nel primi due atti le cose stanno più o meno assieme, hanno una
	parvenza di senso, nella terza parte semplicemente smette di provarci.
	Lo stesso livello di trama che potrei aspettarmi da Doom, o – come disse
	lo stesso John Carmack – quella di un film porno: ci si aspetta che ci
	sia, ma non è così importante.

	Ed è esattamente così: la trama di questo film, soprattutto nell'ultima
	ora non racconta nulla, serve soltanto a dirci dove si svolgerà la
	prossima ingiustificata scena d'azione.
</p>
</div><div class="section">
<p>
	Trascurando le scene completamente inutili, che abbondano comunque
	(genitori di Sam, vi sto fissando), non mancano le parti ingiustificate.

	Vi ricordate di quando gli Autobot vengono fatti prigionieri? Quello è
	soltanto un esempio. Ad un certo punto, un palazzo crolla e dentro di
	sono quei trenta soldatini di prima. Stanno per morire. Poi, appena un
	attimo prima di una fine ingloriosa per tutti, to', arriva Optimus
	volante a fare il culo al mostro tentacolare.

	Perché non è arrivato prima? Cos'altro stava facendo?
</p>
</div><div class="section">
<p>
	Megatron.

	Megatron (ancora una volta doppiato da Hugo Weaving, non dall'ultimo
	degli stronzi) compare in tre scene. TRE.

	In una arriva, dice (non urla, dice) il suo «All hail Megatron» (altra
	frase storica di una certa importanza, ma voi probabilmente non ne avete
	mai sentito parlare – ancora una volta Hipster Gods) e poi si siede.

	In una seconda scena si siede. Vabbeh, si siede sul Lincolm Memorial
	dopo aver rotto la statua, ma, cazzo, si siede. Il cattivo si siede e
	aspetta che gli altri facciano tutto il lavoro.

	Nella terza, ancora seduto, si vede dare della fichetta, si sente ferito
	nel suo metallico orgoglio alieno, va a tradire il suo compare traditore
	e poi si lascia squartare in un colpo solo. Che cazzo.

	Non c'ha neanche provato.
</p>
</div><div class="section">
<p>
	Il ponte spaziale. Nel film precedente, Jetfire poteva usarlo a piacere,
	per trasportare se stesso e (molta) altra gente in qualunque punto dello
	spazio (di fatto, si chiede se quello sia il pianeta giusto!). Stavolta,
	lo stesso Sentinel Prime afferma di essere l'inventore e l'unico
	possibile utilizzatore di questa tecnologia.

	Il film prima, probabilmente, questi tizi non l'hanno visto. E adesso il
	ponte può trasportare pianeti interi. E non solo apre la porta, ma se
	per caso s'incagna (cosa che ovviamente succede) non solo basta
	prenderlo a botte (no, veramente, per riattivarlo bisogna prenderlo a
	botte) ma mentr'è incagnato, il pianeta Cybertron rimane un po' di qua,
	un po' di la. Mica esplode. Esplode solo alla fine.
</p>
</div><div class="section">
<p>
	I soldati. L'invasione di Chicago.

	Se mezz'ora prima il film ci dice che i Decepticon hanno in pungo la
	cittò intera, che lo spazio aereo è chiuso, i satelliti sono oscurati,
	cazzi&amp;mazzi, dopo che qualche malvagio culo alieno è stato calciato,
	cominciato a spuntare soldati negri incazzati, una squadra di dieci Navy
	Seal che sbuca da una fottuta pozza d'acqua. Verso la fine, arriva
	l'esercito.

	Da dove cazzo sbuca? Quando sono arrivati? Perché non sono arrivati
	prima, visto che sono tanto cazzuti?

	Beh, ovviamente, dovevano aspettare che questi quattro stronzi
	protagonisti facessero la loro fuffa, aspettare che venisse servito il
	loro numerino.

	E questo tralasciando il fatto che per ben due volte questi tizi si
	lanciano con il paracadute.
</p>
</div><div class="section"><a id="II/III"></a>
<p>
	Sapete a che serve la spaccacoglioni? Fa due sole cose.

	Passa metà del film a spaccare i coglioni a Sam e a Simmons
	(rispettivamente il protagonista e l'ex spaccacoglioni di due film
	prima, il ruolo di John Turturro). Poi si ricrede su Sam, arriva a
	considerarlo un uomo e un eroe. Eh già, alla fine il merito è tutto del
	bociamerda.

	Ed è l'ex di Simmons. To', pensa un po', altra sottotrama romantica che
	non ha il minimo peso e non frega a nessuno. Grazie, Bay, ne avevo
	proprio bisogno.
</p>
</div><div class="section"><a id="II/IV"></a>
<p>
	Sapevate che i Decepticon avevano sempre avuto alleati umani? Beh, io
	no… veramente, non lo sapeva nessuno.

	Che facevano? Bah, cose di poco conto, tipo tenere segreta la presenza
	dei Transformers. Poi Megatron, un film fa, ha deciso di mandare tutto a
	puttane. E quindi questi tizi devono morire, ma solo ad un certo punto
	del film, quando bisogna ricordare chi sono i cattivi.
</p>
</div><div class="section">
<p>
	E quindi Laserbeak, ch'era sempre stato una spia, improvvisamente
	diventa il più assassino tra tutti i Decepticon, e se ne va in giro
	impunito ad uccidere gente a mazzi. Senza alcun motivo apparente.

	Almeno lui fa quello che tutti gli altri sembrano dimenticarsi di poter
	fare: lui si trasforma. Eh già, lui lo fa.
</p>
</div><div class="section"><a id="II/V"></a>
<p>
	Vi ricordate di quando gli Autobot erano buoni e il loro leader Optimus
	Prime era l'essere più retto e giusto della galassia?

	Cazzo, il suo motto era «La libertà è il diritto di tutti gli esseri
	senzienti», vi ricordate? Lo diceva perfino, nel primo film.

	Ricordate il sacrificio, per salvare l'umanità intera e distruggere
	l'Allspark per evitare i casini? Vi ricordate il secondo sacrificio, la
	sua vita per salvare Sam nel secondo episodio?

	Beh, a quanto pare lui se n'è dimenticato. O forse s'è semplicemente
	incazzato, dopo essere stato preso a pesci in faccia dal governo USA per
	quasi cinque anni.
</p>
</div><div class="section">
<p>
	Seppur pronunciando le parole «giustizia» e «libertà» un paio di volte,
	Optimus sembra in preda ad una certa (giusta?) furia, gli girano i
	coglioni e comincia ad infilare la rettitudine dentro i suoi avversari,
	a colpi d'ascia.

	Perché se prima bisognava fermare Megatron ed evitare che prendesse
	l'Allspark (l'han fatto, è stato riempito di legnate, ma è fuggito), se
	prima bisognava impedire al Caduto di attivare l'Aggeggio del Destino
	(gli hanno staccato la faccia dal cranio, proprio come fece John
	Travolta a Nicolas Cage!), se finora Optimus ha staccato braccia e fatto
	il culo soltanto ad un cattivone e ucciso qualche personaggio
	secondario, adesso invece è senza controllo.

	Tolto il fatto ch'è fondamentalmente lasciato sullo sfondo per la
	maggior parte delle scene importanti, in questo terzo film Optimus è
	cattivo. È malvagio. Annuncia direttamente di volerli tutti morti,
	minaccia di morte il suo vecchio maestro Sentinel. E poi concretizza
	tutte le minacce.

	Anche se vediamo la cosa in non più di quattro inquadrature, Optimus
	arriva e con doppie lame, con ascie, a pistolettate, a calci in culo,
	uccide alieni malvagi a mazzi, finendo tre dei più grossi senza una
	piega.

	Nell'uccidere Shockwave non spiccica parola. Nello sverzare Megatron ne
	spiccica soltanto una. E per quanto riguarda Sentinel, che tenta di
	giustificarsi con dei motivi che, bene o male, mi paiono abbastanza
	condivisibili, che fa? Lo interrompe, interrompe il suo povero e anziano
	maestro, lo interrompe sulle sue ultime parole, lo fredda con un colpo
	alla nuca senza appello. E lo fa con il cannone a fusione di Megatron.
</p>
</div><div class="section">
<p>
	Queste non sono cose fatte per la giustizia.

	Queste non sono azioni degne del leader di quelli che «hanno ragione».

	È fin troppo americano.
</p>
</div><div class="section"><a id="II/VI"></a>
<p>
	Ora, tutti quelli che son passati davanti ad un negozio di giocattoli
	possono senza sforzo immaginare come non tutti i personaggi della serie
	di giocattoli compaia nel film: sono semplicemente troppi.

	E non è mai stato un problema. Non tutti i giocattoli compaiono nelle
	rispettive serie.
</p>
</div><div class="section">
<p>
	Posso capire che una certa (spaventosa) quantità di Decepticon vengano
	uccisi nei modi e nelle occasioni più disparate, ma gli Autobot?

	Non dovrebbero aver perso la guerra? Non dovrebbero essere rimasti in
	poche decine? Dovrebbero. Allora perché c'è un continuo e inutile
	ricambio di personaggi?

	Perché i gemelli non ci sono più? Perché erano stupidi? La prima volta
	questo gigantesco particolare non ha fermato nessuno.

	Perché Jolt, che ricopriva un minuscolo ma fondamentale ruolo nel
	secondo episodio è sparito nel nulla? Dove sono finite Arcee e le altre
	due moto? Da dov'è spuntato Dino (la Ferrari)? Da dove sbucano Brain, Q
	e i tre Wreckers?

	E soprattutto, perché nessuno nota come i Decepticon vengano riclicati
	in continuazione?
</p>
</div><div class="section"><a id="II/VII"></a>
<p>
	John Turturro.

	Quello stesso John Turturro che interpreta l'agente speciale Simmons
	dell'S7 nei due film precedenti, quello ch'è il personaggio meglio
	pensato e meglio interpretato dell'intera trilogia.

	Continuo a chiedermi cosa ci faccia veramente lì. È vero, è fenomenale,
	è comico, è assurdo. Tiene viva la mia attenzione per la prima metà di
	questo polpettone.

	Poi, purtroppo, viene lasciato indietro perché i “veri eroi” del film
	sono altri.
</p>
</div><div class="section">
<p>
	John Malkovich.

	Cazzo, John Malkovich. Non uno a caso. Anch'esso ottimo nelle sue poche
	scene. Capo di Sam prima, un paio di cazzatine in mezzo e infine due
	minuti di follia pura assieme a Bumblebee.

	Perché l'hanno lasciato fuori dal resto?
</p>
</div><div class="section">
<p>
	Alan Tudyk.

	Chi? Forse ve lo ricordate come quello che interpretava Wash, in
	Firefly.

	Altro fenomeno. Il suo personaggio è una delle poche cose azzeccate in
	questo filmaccio. E ce l'hanno lasciato per dieci minuti, poi via per
	sempre.
</p>
</div><div class="section">
<p>
	Ora io vi dico, se 'sti stronzi di regista e sceneggiatori avessero
	avuto un po' di vergogna, avrebbero potuto capire quanto i tre che ho
	appena citato valessero fondamentalmente tutto il film.

	Se questi tre personaggi avessero avuto lo spazio che meritavano avrei
	potuto assistere a qualcosa di magnifico. Ma no.
</p>
</div><div class="section"><a id="II/VIII"></a>
<p>
	E poi la fine. Perché questo film qui è decisamente l'ultimo. Lo stesso
	Bay l'aveva annunciato parecchio tempo fa.

	E il fatto che nient'altro possa essere aggiunto, in cima a questa
	gigantesca e multimiliardaria pila di sterco è un bene.

	Ma non è bene. Perché finisce di merda.
</p>
</div><div class="section">
<p>
	In due modi diversi.

	In pratica tutti tutti tutti i cattivi sono stati uccisi. Se non altro,
	tutti i personaggi con una certa importanza (Shockwave, Megatron,
	Soundwave e – mi dispiace dovercelo mettere, perché non meriterebbe
	nemmeno i miei sputi – Starscream) sono stati uccisi. Definitivamente
	uccisi. Sentinel Prime non aveva alleati in vita, ed è morto.

	Anche Cybertron, l'intero pianeta, è esploso. Sperabilmente senza via di
	recupero.

	Gli Autobot invece hanno un bilancio decisamente più leggero: alla fine,
	sono morti soltanto Q (freddato gratuitamente, se ne va anche più
	gratuitamente di quanto sia entrato) e Ironhide. Lui effettivamente
	l'hanno arrugginito a morte, alle spalle, senza onore. Brutta cosa.
	Optimus ha perso un braccio, ma chissenefrega.
