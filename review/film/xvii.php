		<h2>
			First Blood – 1982
		</h2><p>
			Questo film è strano.
		</p><p>
			Non capisco il motivo di quel che succede. Non c'è ancora il chiaro
			pretesto di mostrare della violenza sullo schermo, c'è qualcosa di
			triste e sottile.
		</p><p>
			Questa è la storia di un giovinastro, John Rambo, veterano del
			Vietnam che se ne va a spasso, godendosi la sua libertà. All'inizio,
			lo vediamo camminare per alcuni minuti (scene del genere non le fa
			più nessuno, perché adesso la gente si stuferebbe).
		</p><p>
			Rambo raggiunge una casetta, sta andando a cercare un suo vecchio
			commilitore. Trova invece una signora scorbutica, che gli rispondere
			prontamente e a tono, fino a rivelare, dopo lunga attesa, che quel
			commilitone è morto.
		</p><p>
			Rambo è qui per il cazzo, quindi. E se ne va.
		</p>
	</div><div class="section">
		<p>
			Sulla via di X (non so dove va, non lo sa neanche lui, veramente),
			Rambo entra in una cittadina montana americana, una di quelle
			comunità fa film, di quei paeselli dove tutti salutano lo sceriffo.
		</p><p>
			Proprio lo sceriffo, che passava di là per caso, incrocia Rambo e
			intuisce la sua scarsa attitudine alla socialità. Decide quindi di
			allontanarlo dalla città, e lo costringe a prendere un passaggio.
		</p><p>
			Senza particolare motivo (è affamato) Rambo cerca di tornare in
			città, ma lo sceriffo è fermamente intenzionato a mandarlo via. Dopo
			un minuto, Rambo finisce in cella.
		</p><p>
			Alla stazione di polizia (dei ranger? L'ufficio dello sceriffo? La
			guardia nazionale? Non lo so…) troviamo il $BaffutoStronzo, che
			subito si prende in odio Rambo, e tra gli altri il giovanissimo
			David Caruso (che vent'anni dopo sarebbe diventato Horatio Kaine di
			CSI).
		</p><p>
			Rambo non è intenzionato a collaborare, non avendo fatto nulla di
			male. Tutti gli agenti, invece, sono un po' stronzi. Sono
			strafottenti, e non hanno cazzi di essere gentili con il
			prigioniero.
		</p><p>
			Lo lavano con la pompa, poi s'accingono a raderlo. Assistiamo in
			questi momenti a vari brevi flashback che mostrano il passato di
			Rambo durante la guerra. D'improvviso, quindi, Rambo perde la sua
			stabilità e sbotta, atterra violentemente tutti gli agenti che
			incontra e fugge verso il bosco.
		</p>
	</div><div class="section">
		<p>
			Da lì, smette di parlare e non sente più ragioni. Lo sceriffo lo
			insegue personalmente per un bel tratto, poi chiama altri agenti, i
			cani e l'elicottero.
		</p><p>
			Rambo, intanto, si arrangia.
		</p><p>
			Scappa, si nasconde tra gli alberi, si trova da vestire, scende per
			una rupe. Intanto, arriva l'elicottero, a bordo del quale ritroviamo
			il $BaffutoStronzo. 'Sto tizio s'è visto quasi cavare un occhio
			(Rambo ha la manina pesante) ed è decisamente incazzato: abbastanza
			da sparare senza preavviso.
		</p><p>
			Rambo si getta dalla rupe, finisce per terra tutto ammaccato e
			l'elicottero scende in basso… mentre il $BaffutoStronzo tenta di
			seccare Rambo sparandogli da vicino, il poveraccio s'arrangia come
			può e tira un sasso: gli sbalzi di spavento del pilota finiscono per
			far cadere il $BaffutoStronzo, che si scappa la testa sul greto del
			torrente.
		</p><p>
			In quella, arrivano gli altri agenti, in cima alla rupe.
		</p>
	</div><div class="section">
		<p>
			Rambo, innocente, tenta di arrendersi. Qualche stronzo comincia a
			sparare e lui scappa. Da lì in avanti, è il delirio: lo sceriffo lo
			vuole catturare ad ogni costo, mentre Rambo è deciso a scappare ad
			oltranza.
		</p><p>
			Partono cinque agenti, con i cani: Rambo però è troppo abile,
			finiscono tutti a terra, uno legato ad un albero, due svenuti di
			nascosto, uno azzoppato, i cani invece vengono abbattuti.
		</p><p>
			Dopo aver annientato la squadra, Rambo sorprende lo sceriffo, gli
			spiega (con i suoi modi gentili) le sue ragioni (il primo sangue,
			che da il titolo al film, non è stato versato da Rambo) e gli intima
			di lasciarlo andare. Poi se ne va. Senza uccidere nessuno.
		</p><p>
			Ahimé, lo sceriffo non lo ascolta. Verso sera, ci sono 200 uomini di
			varie forze dell'ordine che setacciano la montagna.
		</p><p>
			Compare, a quel, punto, il colonnelo Trautman. Questo signore
			anzianotto afferma d'essere l'ufficiale comandante di Rambo durante
			la guerra. Arriva per conto dell'esercito, interrompere l'operazione
			e salvare tutta quella povera gente (che Rambo potrebbe seccare
			senza sforzo). Ma non c'è ragione che tenga, lo sceriffo è marmoreo.
		</p><p>
			La caccia continua.
		</p>
	</div><div class="section">
		<p>
			Ci sono decine e decine di persone alla ricerca di Rambo, nel bosco,
			e lui non è certo sovrumano. Finisce imprigionato in una vecchia
			miniera, ed impiega mezza giornata per uscirne. Da lì, però supera
			il cordone di sorveglianza, ruba un camion e si dirige in città.
		</p><p>
			Una volta lì, in barba alle decine di agenti presenti, riesce, con
			un paio di diversivi ben piazzati, ad isolare lo sceriffo, che
			rimane solo e isolato nella sua casetta.
		</p><p>
			Una volta all'interno, Rambo spara per uccidere (almeno credo).
			Trautman arriva di corsa per fermarlo.
		</p><p>
			Con lo sceriffo a terra inerme, il suo ufficio semidistrutto ma
			completamente circondato da un plotone intero di gazzelle e agenti,
			Rambo crolla tra le braccia del colonnello.
		</p><p>
			Il finale è tragico, e mostra come Rambo sia in effetti la vittima.
			Prima la guerra l'ha spezzato, e poi 'sto stronzo dello sceriffo gli
			ha fatto temere che tutto fosse ricominciato. I titoli partono su un
			fermo immagine, nel quale si vede il colonnello accompagnare il suo
			protetto a casa, con una coperta sulle spalle; Rambo, invece, ha lo
			sguardo perso, si volta indietro ad osservare la spaventosa quantità
			di danni ch'ha provocato, quasi senza rendersi conto del motivo.
		</p>
	</div><div class="section">
		<p>
			Questo film, che può benissimo vivere da solo, senza i vari seguiti,
			manca completamente di quella violenza esasperata che
			contraddistingue la serie.
		</p><h2>
			First Blood
