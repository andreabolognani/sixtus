		<h2 class="reverse">
			Rambo
		</h2><h2>
			First Blood Part II
		</h2><p>
			Questo film è radicalmente diverso dal primo, il messaggio è
			completamente diverso. Inoltre, esplode molta più roba.
		</p><p>
			Si ricomincia con una scena ora classica: guardie armate indossano
			larghi cappelli mentre sorvegliano numerosi prigionieri che spaccano
			pietre; chi si ricorda “La leggenda di Al, John &amp; Jack”?
		</p><p>
			Il colonnello Trautman attende Rambo, al quale mancano ancora cinque
			anni di lavori forzati. È giunto con una proposta, un'ultima
			missione in cambio della scarcerazione immediata. Rambo, che non ha
			niente da perdere ma nemmeno ha voglia di tornare sul teatro di
			guerra, è riluttante.
		</p><p>
			La notizia che la missione riguarda soldati americani tenuti
			prigionieri infine lo smuove. A quanto pare, Rambo conosce il
			territorio e la jungla meglio dei Vietnamiti.
		</p>
	</div><div class="section">
		<p>
			Senza passaggi intermedi, Rambo atterra nell'ultimo avamposto
			americano, raccatta un po' della sua roba (pugnale, scarponi,
			esplosivi… altra scena classica) e conosce Murdock, il responsabile
			della base.
		</p><p>
			Questo tizio, il pilota e al suo aiutante, sono tre stronzi.
		</p><p>
			La missione, contrariamente a quel che Rambo credeva, prevede
			l'ispezione di un campo militare che dovrebbe essere vuoto.  E
			l'alto comando vuole esserne sicuro. E per questo hanno scelto l'ex
			soldato cazzutissimo.
		</p><p>
			Rambo e il colonnello sono poco convinti, e lo sono anch'io.
			Murdock, quasi sfottendoli entrambi, ordina a Rambo di non
			ingaggiare il nemico: il suo compito è quello di andare, fotografare
			e tornare a casa.
		</p>
	</div><div class="section">
		<p>
			I problemi cominciano subito: il lancio con paracadute da alta quota
			parte male, per via di una cinghia incastrata. Rambo manca il punto
			d'atterraggio di &gt;900 decametri.
		</p><p>
			Questo certo non lo spaventa, ma già Murdock tenta di dare la
			missione per fallita e Rambo per morto. Intanto, Rambo recupara il
			suo contatto, Co. Chi si ricorda la scena in cui Topper incontra
			Ramada in “Hot Shots! Part Deux”?
		</p><p>
			Rambo e Co raggiungono il campo, lo trovano strapieno di guardie e
			di prigionieri. Rambo decide quindi di ignorare i suoi ordini e
			parte immediatamente al salvataggio di quello che tra i prigionieri
			è messo peggio. I tre si danno poi alla fuga.
		</p><p>
			Dall'altra parte, il colonnello è il solo uomo assennato di tutto
			l'esercito; Murdock, invece, è ben intenzionato ad ignorare tutto, e
			non a caso: la missione deve fallire, in modo che il governo ammetta
			che tutti i MIA siano KIA e autorizzi l'abbandono della missione.
			Per soldi, fondamentalmente: se l'opinione pubblica venisse a sapere
			che molti soldati sono vivi e prigionieri, il governo sarebbe
			costretto a salvarli, con enorme dispendio di tempo, uomini e soldi.
		</p>
	</div><div class="section">
		<p>
			E così, nonostante Trautman e il suo elicottero stiano ad un metro
			da Rambo e dal suo prigioniero quasi salvato, la missione viene
			abortita per ordine diretto di Murdock. Rambo e l'altro poveraccio
			finiscono catturati.
		</p><p>
			Ora, per qualche motivo, arrivano i Russi. Un tizio grossissimo e
			un ufficiale addetto agli interrogatori (che ricordo come il cattivo
			in “Beverly Hills Cop”) vogliono che gli americani sappiano che lui
			e gli altri sono prigionieri.
		</p><p>
			E via con la tortura (altra scena classica).
		</p><p>
			Intanto, però, la ragazza ritorna, si piazza segretamente nel punto
			opportuno e coglie l'attimo. Lei e Rambo fuggono, fuggono e fuggono
			e alla fine si nascondono.
		</p><p>
			Siccome lei sogna l'America e la libertà, mentre Rambo non sogna
			niente, finiscono per scambiarsi un bacio. Bah, potevano anche fare
			a meno, ma la cosa era necessaria per il dramma.
		</p><p>
			Segue una scena strana: lei si alza e s'incammina, Rambo la segue
			lasciando l'arco, poi se ne ricorda, torna indietro di un passo, lo
			raccoglie… in quella, il cattivo con i baffetti spara alla ragazza.
			Rambo risponde al fuoco con la potenza di una corazzata, ammazzando
			almeno quattro tizi (non il cattivo, quello muore dopo…).
		</p><p>
			Ora, con la ragazza morta, Rambo può passare all'azione. Si fissa la
			bandana e poi sono cazzi per tutti. Per il successivo quarto d'ora,
			è un morto dietro l'altro, tutti ammazzati alle spalle oppure uccisi
			da frecce.
		</p><p>
			Poi, comincia l'escalation finale: Rambo trova il cattivo con i
			baffi, l'ammazza con la freccia esplosiva. Impagabile.
		</p><p>
			Poi arriva il russo grossissimo con l'elicottero: Rambo schiva la
			bomba, schiva anche le schioppettate, sale sull'elicottero, butta
			giù il russo enorme, butta giù anche il pilota, ruba l'elicottero.
			Con quello, torna al campo e le mette a ferro e fuoco.
		</p><p>
			Liberati i prigionieri, caricatili sull'elicottero, ecco comparire
			l'ufficiale russo, a bordo di un elicottero enorme e stracarica
			d'armamenti.
		</p>
	</div><div class="section">
		<p>
			Visto che la fuga è impossibile, Rambo utilizza il vecchio trucco
			per ingannare l'orso e si finge abbattuto, poi spara un colpo col
			bazooka che fa un botto così grosso, ma così grosso…
		</p><p>
			A quel punto, l'elicottero (che non era affatto danneggiato come
			poteva sembrare) riprende la sua corsa verso la base americana.
			Mentre i soldati esultano e i prigionieri vengono caricati sulle
			ambulanze, Rambo fa a trovare Murdock, gli scarica l'M60 nelle
			orecchie, e gli intima di proseguire le ricerche negli altri
			villaggi e di inviare missioni di salvataggio per altri prigionieri
			che aspettano.
		</p><p>
			All'ultimo minuto, Rambo spiega al colonello che quel che vuole, per
			se e per tutti i commilitoni, è un po' di vero apprezzamento da
			parte della nazione, che tratta invece i soldati e i veterani come
			roba vecchia.
		</p><p>
			Il film si chiude con il colonnello che osserva Rambo andarsene
			verso l'ignoto.
		</p><h2>
