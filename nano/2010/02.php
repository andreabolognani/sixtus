<h2>Campeggio</h2>
<div class="intro"><p>
Per un paio di giorni non vidi né sentii nessuno, forse era un weekend,
forse gli altri erano al mare, forse chissà che altro... In ogni caso, nessun
contatto con nessuno. E un solo pensiero in testa.
</p></div>
<p>
Rivedere quella ragazza.
</p><p>
Se ricordo bene, la prima settimana fu una lunga inutile attesa. E come si fa di
solito a quell'età (ma sarebbe stato meglio se fossi stato più giovane, perché
ai tempi ero più giovane emotivamente di quel che ero fisicamente, e neanché lì
ero un gran ché) cercai in tutti i modi di scoprire chi lei fosse, dove
studiasse e dove abitasse mantenendo al contempo il più stretto disinteresse
apparente nei suoi confronti.
</p><p>
Fallendo, immagino.
</p></div><div class="section"><p>
Perché ai tempi ero un coglione, molto ma molto più inesperto, immensamente più
ingenuo ed ovviamente, per quanto non sembrasse, molto più gioviale di quanto
sono ora. Non un topo di biblioteca, ma comunque un discreto lettore (uno di
quelli che ancora riesce a leggere un libro intero senza dire cose come
``Peccato, mi sarei aspettato di meglio da questo autore, di cui già ho letto
questo e quest'altro titolo'' oppure ``Bene per l'impronta, lo stile, peccato
per la scontatezza della trama e la piattezza di alcuni personaggi''), non
modello da seguire ma discreto studente (uno di quelli che se per caso mi chiedi
qualcosa ti so rispondere o indirizzare dalla parte giusta), non un campione ma
comunque discreto atleta, non un festaiolo ma comunque uno che se gli chiedi di
uscire la sera butta via i suoi altri impegni. Insomma, un tizio. Uno di quelli
che indossa degli occhialoni spessi fin da quando andava all'asilo, uno di
quelli che con la bicicletta arriverebbe dovunque (e l'ho fatto), uno di quelli
non alti né bassi, né grassi né magri, uno di quelli che fanno battute
divertenti ma non stupide, di quelle che riescono a far ridere tutti, anche gli
adulti o i bimbi picci, ma soprattutto uno di quelli che non hanno mai avuto una
ragazza, non ne hanno una e non intendono farsela per due motivi: primo; avere
una ragazza costa tempo e soldi (o almeno così mi dicevano ai tempi), secondo;
le ragazze sono femmine e non hanno niente d'interessante. Già, a quei tempi mi
sfuggivano alcune cose. Parecchie cose. Tante di quelle cose che adesso non
avrei neanche la forza di ridere per quanto limitato era il mio orizzonte.
</p><p>
Ma tornando a quei giorni, non so bene come, non so bene perché, non so bene
tramite chi, man mano venni a conoscenza di tutte quelle sudate informazioni. La
ragazza si chiamava \camelia, frequentava la mia stessa scuola, e abitava anche
dalle mie parti, a pochi minuti da casa mia, proprio olte il parcheggio dietro
il magazzino del fai-da-te, oltre la terza rampa...
</p><p>
Ma anche una volta conosciute queste cose, non avevo chiaro... beh, nulla. Ai
tempi non avevo inteso nulla della situazione.
</p><p>
Nulla.
</p><p>
Quindi in pratica non feci alcunché per un paio d'anni.
</p></div><div class="section"><p>
Beh, non proprio nulla, ovviamente.
</p><p>
Ebbi modo di scoprire cose interessanti, per la maggior parte scoprii nuove cose
da odiare. Perché una delle cose che fai crescendo è appunto dividere le cose in
amate, non particolarmente amate, e odiate. Nel mio caso, le cose odiate furono
la maggior parte.
</p><p>
Da dove comincio?
</p><p>
Dalla scuola, direi. La filosofia. Perché ho avuto un professore che per tre
anni è stato pomposamente a raccontarci non cosa pensassero dei tizi morti da
decine, centinaia (ma anche migliaia) di anni, bensì perché pensassero quel che
pensavano. Il perché la gente avesse delle cose in testa, come questo ha o non
ha influito sulle vite umane nel periodo loro contemporaneo, ma anche
successivo, e magari anche precedente perché se ci fossero stati prima allora le
cose sarebbero state differenti. Già, perché lo stesso professore capitava
d'insegnarci anche la storia, ``la storia e la filosofia sono la stessa cosa,
come la filosofia della storia e la storia della filosofia, ma anche la fisica e
la matematica che nasce come strumento per la fisica'' e la geografia pure
perché se ci pensi andare in un posto con le gambe è come andarci con la mente e
quando pensi filosofeggi e quando filosofeggi esisti e quando esisti... boh,
altro fiume di bei paroloni che non intendo sforzarmi di ricordare. Fanculo quel
tizio e una buona metà dei tizi morti di cui mi ha parlato. Perché non è a caso
che con tutti quei filosofi in Grecia a fare un cazzo mentre la gente dovrebbe
lavorare alla fine son stati conquistati dai Romani. Poi, quando i Romani si son
evoluti, hanno preso quegli stessi dei ed hanno cominciato a farsi le stesse
domande sul perché la gente si fa domande, sono arrivati i barbari ad invaderli.
</p><p>
Poi la strada per arrivare alla scuola. Perché ovviamente non esiste una strada
comoda. Esiste soltanto una straducola, a senso unico, che serve
fondamentalmente per tutti i condomini della zona (perché la scuola dev'essere
in mezzo alle case, ovviamente), strada su cui passano quasi esclusivamente gli
abitanti della zona, che secondo il comune devono essere tutti dei maniaci della
velocità. Già, perché se 300 metri oltre la scuola parte un bello stradone che
esce dalla città e prosegue verso la periferia e lì la gente può tirare i 130
chilometri orari perché un vigile urbano chi l'ha mai visto, sulla cazzo di
stradina che io devo percorrere in bici tutte le dannate mattina, qualche
ingegnere geniale ha deciso che ci sarebbero stati i dossi per il
rallentamento, perché metti caso che ``Sto tornando stanco a casa dal lavoro, ho
tanta voglia di stendermi sul divano, per far prima porterò la mia Lamborghini
Gallardo alla modica velocità del suono, ma che dico? E i bambini? E le nonnine?
Grazie a Dio un ingegnere geniale del comune ha previsto e provveduto a porre su
questa mia stradina dei dissuasori, cosicché io eviti di mettere in pericolo me
e altri. Grazie, ingegnere geniale''. Peccato che questo affare tenga le
macchine sotto i 30 all'ora, loro che hanno delle cazzo di sospensioni
meccaniche o idrauliche, visto che sono macchine, invece io e la mia
biciclettina del cazzo non le abbiamo le sospensioni, e metti caso che io abbia
fretta la mattina perché devo arrivare a scuola prima che quegli stronzi mi
chiudano il cancello in faccia (perché me lo chiudono, il cancello - metti caso
che un terrorista bastardo voglia entrare a scuola per ammazzarci tutti, eh no,
non può: il cancello antiterroristi è sigillato!) magari la mia priorità è
pedalare come un dannato, vero? No, diventa evitare 'sti cazzo di dossi, perché
metti caso ci finisco sopra, che succede? Succede che se va bene mi si incassa
la schiena e poi son bestemmie, per me che ho lezione al quinto piano di un
edificio dove gli ascensori sono riservati agli insegnanti e ai disabili (ma
cazzo, solo perché ho le gambe allora devo necessariamente farmi tutte le
rampe?), metti caso che invece va male finiamo per terra io e la bici; io mi
gratto e magari mi rompo, perché almeno ho la scorza dura, ma la bici del cazzo
invece si rompe perché l'ho pagata poco. Grazie, ingegnere geniale del cazzo!
</p><p>
E poi ci sono i soldi. E io non sono uno che vuole soldi a mazzi, e non esco
neanche tanto la sera. E non posso mica mettermi a lavorare, neanche tanto
perché devo andare a scuola invece che lavorare, ma metti caso che mi becchino a
lavorare mettono in galera chi mi da i soldi... che paese. E quindi che devo
fare io quando disgraziatamente esce un qualche titolo decente sull'unica
console che sono riuscito a procurarmi, deo gratia, sudandola duramente per anni
e anni? Succede un cazzo, sbavo davanti alla vetrina come i bambini davanti alle
pasticcerie, aspetto mille anni quando ormai il titolo decente è diventato
storia e spero con tutte le mie forze di trovarne una copia usata in uno di quei
negozzietti che campano appunto comprando e rivendendo giochi usati. Ecco, una
cosa che non odio: i giochi usati poco e scontati tanto. Tipo la metà del prezzo
originale. Ed ecco invece una cosa che odio parecchio: tutti quei titoli stupidi
che escono ogni anno, che vendono un milione di copie in un mese, che il mese
dopo il 98\% di quelli che l'han comprati li rivende e quando li rivende vuole
tanti soldi quanto quelli nuovi; e c'è anche di peggio: i titoli invenduti sugli
scaffali, quelli sono invicibili, sono completamente immuni da svalutazione e
mantengono il prezzo di copertina originale in perpetuo.
</p><p>
Oh, ecco un'altra cosa. La Divina Commedia e I Promessi Sposi. Già, perché un
qualche ministro una volta ha deciso che tutti gli studenti della nazione
avrebbero dovuto spendere metò delle loro ore non scientifiche (ma che cazzo ho
scelto il liceo scientifico a fare, allora?) a leggere questi due titoli. Ma che
dico leggere? Sorbire forzatamente e poi analizzare, perchè quei due poracci di
Dante e del Manzoni (già, perché Manzoni non è Manzoni, bensì IL Manzoni, non
riuscirò più a chiamarlo normalmente...) non hanno scritto ciascuno un milione
di parole perché andava loro di scrivere e di far divertir la gente, no, loro
avevano bene in mente di diventare mostri sacri a centinaia di anni dalla
propria scomparsa, e sapevano bene che un sacco di gente si sarebbe dovuta
fermare a riflettere su ``Cosa voleva dire l'autore con queste parole?''. Ma che
cazzo avrà voluto dire con quelle parole se non quello che ha scritto? Quando io
voglio dire delle cose, le dico, non sto mica lì a farmi le seghe e a dire
altra roba. E questi due non erano neanche donne.
</p><p>
Già, perché la letteratura non è mica limitata all'italiano e ad autori maschi.
E non lo dico per sessismo, ma credevo almeno di poter essere più vicino alle
parole di un autore uomo, anche italiano (o quasi) invece che impelagarmi a
comprendere le parole di una qualche autrice inglese pazzoide che ``Ma perché
l'autrice ha descritto soltanto gli aspetti negativi di questa scena?'' e poi
vai a scoprire che l'illustre e ammirabile autrice è finita annegata in un lago
perché aveva esaurito la voglia di vivere. Dio! Anch'io l'avevo finita, leggendo
quella roba. E vaffanculo a tutti 'sti autori dannati, che se avete voglia di
morire morite, ma non chiedeti di venirvi dietro. E sia il ministro
dell'istruzione a dirmi che devo andar dietro alla gente morta. E ai matti.
Cazzo, c'erano anche i matti. Col professore di filosofia, quello di prima. Eh
già, perché ad un certo punto arriva questo tizio capoccione con tutte le sue
teorie grosse, contorte e fantasiose, che alla fine della sua carriera scrive un
bel librone pieno di cose astruse, e poi finisce in manicomio e muore. Ma forse
era già matto prima di scrivere il capolavoro. Ma forse no. Ma allora forse non
è un capolavoro e non dovremmo andare a leggere le cazzate che ci stanno scritte
dentro. Forse, eh? Ma ormai è tardi, perché già le abbiamo lette, quindi forse
siamo matti anche noi, ``E che cazzo! Perché diavolo devo seguir 'ste lezioni,
professore?'' ``Perché sennò ti caccio l'insufficienza in pagella e ti tocca
perdere l'estate a ripassare per l'esame di settembre'' rispose lo stronzo. E
vaffanculo lui e il suo cazzo d'esame a settembre. Tre volte l'ho rifatto.
Quando morirà, ballerò sulla sua tomba.
</p></div><div class="section"><p>
Ma la mia vita fu anche altro. Arrivò infatti l'estate a seguire la terza
superiore. Quell'estate, come molte delle precedenti, mi capitò di dover andare
in campeggio. Già, perché non è che posso stare a casa tutta l'estate perché
``Dai, \corvino, esci un po'! Non hai amici?'' e non posso neanche uscire tutta
l'estate perché ``Dai, \corvino, stai un po' a casa che non ti vediamo mai!'' e
l'unica maniera di interrompere questa routine era finire partecipante al
campeggio. Il campeggio della parrocchia, così ``Dai, \corvino, che sentire
qualche messa ti fa bene!'', e vaffanculo il parroco con le sue interminabili
discussioni su come ogni cosa moderna sia il male, e vaffanculo tutta la gente
che si vede per tutto un anno e poi torna in campeggio e ``Oh carissimo, come
stai? Come va? Come non va?'' e vaffanculo ``Metti tutti i tuoi vestiti sporchi
in un sacchetto che tra 15 giorni facciamo una lavatrice, forse, forse li
buttiamo via'' e ``Prendi questa maglietta che tanto non la metti più che poi la
buttiamo''. Vaffanculo, vaffanculo e vaffanculo.
</p><p>
Ma come ho detto nella vita ci fu anche altro. Infatti, il destino mi mise
davanti ad una buona notizia; un bel dì (non era un bel dì) venni a scoprire per
pure purissimo caso che Camelia avrebbe partecipato al campeggio. E questo fu
almeno tre settimane prima della partenza. Tre settimane d'inferno in terra.
</p><p>
Perché non c'è niente di peggio nella vita di un uomo (stupido) che attendere
qualcosa di completamente sconosciuto. Ai tempi, infatti, andavo completamente
allo sbaragaglio.
</p><p>
Andò così, più o meno...
</p><p>
Passai circa una settimana chiedendomi come sarebbe stato. ``Come sarà?'' pare
una domanda semplice, ma riflettete effettivamente un attimo e ditemi come sarà.
Come sarà che? La domanda era chiaramente incompleta, ma non me ne resi conto
prima di una settimana, poi comincia ad afferrare il problema. Non avevo per
nulla chiaro cosa avrei voluto che succedesse. Non un singolo indizio.
</p><p>
Poi qualcosa cominciò a muoversi, e la domanda spontanea cambio in ``Cosa le
dico?''; altra domanda impossibile. Non sapevo infatti cosa dirle, ma nemmeno
cosa volessi dirle. Era ancora un vicolo cieco. E così feci una delle cose
stupide che si fanno in quei momenti. Andai a raccattare due amici, due amici
fidati, di quelli che forse ``l'avevano vista''. Questi due, per puro caso lo
stesso paio d'amici della fontanella la sera del concerto, erano \jolly\ e
\skeggia.
</p><p>
``Allora, stronzi, che mi dite'' cominciai.
</p><p>
``Nulla di particolare'' fece \jolly, che aveva avuto una ragazza e mezza, una
alle medie e mezza al liceo.
</p><p>
``Che mi dici tu, pennellone?'' disse invece \skeggia, che non so bene chi/cosa
avesse avuto, ma aveva una grande `esperienza' nel campo.
</p><p>
Qualunque fosse il campo.
</p><p>
``Succede che tra un tot partiamo per il campeggio, e so che ci partecipa anche
Camelia e io ancora devo spiccicarci parola'' faccio io.
</p><p>
``Qual è il problema? Vuoi fartela?'' \skeggia\ parte a cannone. Ai tempi, avevo
solo una conoscenza teorica della meccanica di quello che intendeva:
``Tranquillo, t'insegno tutto io: allora, la prima cosa da fare è dire: `Ehi!' e
poi stare zitti o magari andarsene. Funziona sempre''
</p><p>
``Ah si?'' butto lì io, poco convinto.
</p><p>
``Certo certo, perché le fai capire che lei non è importante. Devi farle capire
subito che lei non conta un cazzo, così stabilisci chi è che comanda. Altrimenti
lei ti mette sotto e non ne esci più'' prosegui il mio amico.
</p><p>
Faticai in quel momento a capire che in quel caso `lei ti mette sotto' era da
intendersi come cosa non buona. Perché non avendo alcuna esperienza empirica
sull'argomento, m'ero limitato ad imparare la teoria e il gergo (grazie,
Internet!) ma ero chiaramente alle prime armi e non avevo ben chiari i
confini di... non avevo chiaro un cazzo.
</p><p>
Quella discussione andò avanti per quasi un'ora ed ebbe come unico effetto di
incasinarmi la mente e farmi giurare che mai e poi mai sarei diventato come un
di quei due.
</p><p>
Se non altro, la quantità di riferimenti sessuali infilati nella conversazione
mi portò inequivocabilmente a realizzare un modello in cui io e Camelia
eravamo rispettivamente maschio e femmina in una relazione tra maschio e
femmina. E fu in quel momento che mi resi effettivamente conto di quello che
stava succedendo, il momento in cui trovai la parola per descrivere il tutto.
Avevo un cotta per una ragazza. Passai quindi tutta una sera, sveglio fino alle
due di notte, godendo per questa inaspettata quanto semplice rivelazione. Mi par
di ricordare d'aver dormito immensamente bene quella notte.
</p><p>
Mi svegliai quindi la mattina dopo bello pimpante, ma con la veglia vennero
anche alcune considerazioni che non furono un gran ché positive... primo, capii
che avevo impiegato un paio d'anni a realizzare che avere come chiodo fisso
(semi-fisso, pseudo-fisso, non-poi-così-fisso, in fin dei conti) per due anni
(DUE anni) la faccia di una ragazza era semplicemente dovuto al fatto che ero
attratto da lei. Due anni. Due anni buttati nel cesso. E dopo ``aver vagato per
cento vite d'uomo per questa terra, ora non ho tempo'' (come disse una volta uno
stregone) perché dopo due anni persi a fare un bel cazzo di niente ora mi
ritrovavo con nemmeno una settimana di tempo prima di passarne due in campeggio
con lei intorno.
</p><p>
Mi si dipinse davanti agli occhi un quadro terribili, il quale mi convinse fin
nel profondo che non avrei avuto alcun futuro se alla prima occasione, una volta
isolati dalla civiltà (il campeggio prevedeva effettivamente un certo
isolamento, niente telefoni, niente TV, niente giornali) in cui le mi avesse
rivolto un saluto... aspetta, lei mi avrebbe rivolto un saluto? Sarebbe stato
giusto lasciare che fosse lei a far la prima mossa? Non avrei dovuto essere io
galantuomo ad avvicinarmi per primo? Non avrei dovuto avere con me un
preservativo nel caso ci fosse stata occasione di consumare?
</p><p>
Queste e varie altre domande riempivano la mia testa e mi lasciavano senza tempo
libero per altre attività cerebrali. E purtroppo, non erano tutte stupide. Non
così stupide, almeno. Infatti, tra le altre cose mi stavo chiedendo anche cosa
avrebbero fatto i miei due amici stronzi, che ovviamente sarebbero venuti in
campeggio. Come funzionavano queste questioni tra amici? Avrebbero mantenuto il
segreto? Avrebbero spifferato cose imbarazzanti ad altra gente? Avrebbero invece
potuto intervenire a mio favore con la ragazza?
</p><p>
Per mia gran fortuna, scoprii in seguito che il destino avrebbe provveduto a
sistemare queste questioni per proprio conto, senza pesare ulteriormente sulle
mie povere spalle. Ero solo con le mie seghe mentali, in pratica.
</p><p>
Ed ovviamente, non conclusi nulla nulla fino al giorno della partenza.
</p><p>
Il giorno della partenza fu tremendamente faticoso. Svegliati la mattina e
comincia a rispondere alle solite stupide domande del parentame del tipo ``Che
bello, oggi parti per il campeggio. Sei contento?'' e svariate altre domande del
cazzo, visto che quello era qualcosa come il sesto o settimo campeggio al quale
partecipavo. Poi sopporta quell'odissea che è dover lasciar far le tue valigie a
qualcuno, perché non mica con ci si protrebbe aspettare: se uno fa la tua
valigia, allora la fa lui e tu puoi andare affanculo coi tuoi amici; e invece,
quando qualcuno del parentame fa la tua valigia lui sta lì davanti alla valigia,
ed ogni decimo di secondo parte una richiesta di chiarimenti tipo ``Ma questa
maglia s'intonerà col cielo che si vede in montagna? Ma questi calzini ti stanno
bene?'' et similia, quando io posso pensare solamente ``I pantaloni devono avere
tre buci, le maglia invece devono averna quattro e tutto va bene'' ed ore e ore
(tutta la mattina) passata a riempire una, due, tre borse di `cose d'emergenza'
che ``non si sa mai che ti serva, ce l'hai''.
</p><p>
Ma non solo: esiste una qualche politica parrocchiale per cui non tutti
dovrebbero venire in macchina, non tutti con la stessa macchina, ragazzi e
genitori non nella stessa macchina, gente con altra gente, tu vai qui che io
vado lì, cose di chiesa per fare comunità, annunciare buone novelle,
partecipazione, pace nel mondo, etc etc etc... Fatto sta che ognuno di noi
finisce in una macchina a caso con genitori e ragazzi mai visti. E che succede
in macchina? Le solite due cose: nessuno ha una cartina, né idea di dove sia il
posto, nonostante il campeggio sia nella stessa baita da almeno sette anni, ed
ovviamente, mentre il padre autista sta cercando di seguire la macchina davanti,
la madre che non ha un cazzo da fare da deve assolutamente seguire il viaggio di
suo figlio/figlia verso la maturità comincia a rifare le stupide domande di cui
sopra, e tu non puoi mica mandarli a cagare come si meriterebbero, perché sei
con sconosciuti su una macchina sconosciuta che si va verso due settimane del
cazzo di campeggio della parrocchia. Eh, no, non è bello mandarli a cagare.
</p><p>
Poi finalmente, dopo un'ora prevista di viaggio e un'ora non prevista,
finalmente smonti dalla macchina sconosciuta, ti ritrovi in un qualche
parcheggio sterrato e cominci a scaricare il tuo zaino. Dov'è il mio zaino? Di
chi è questo? Tu col cappello, hai visto il pisauro? Tu biondo, aiutami a
scaricare l'idrovora; tu moro, vai a cercare il Mastro di Chiavi... tutto così
per mezz'ora almeno, aspettando che tutte le macchine della carovana arrivino,
verificando che tutti abbiano tutto, e adesso finalmente possiamo incamminarci,
no. Preghiera: ``Ti ringraziamo, o Signore, per essere giunti fin quì...'' etc
etc etc per almeno venti minuti.
</p><p>
Poi si parte.
</p><p>
Zaini in spalla, molti zaini, molte spalle, eccoci all'inizio del sentiero che
in una mezz'ora soltanto ci porta alla baita. Mezz'ora. Dopo venti minuti di
preghiera e di altre cose inutili? Sta scendendo il sole, non potevamo pregare
dopo? Al caldo, magari? Dopo mangiato, magari? Eh, no. Non si poteva. Lo dice la
Bibbia, da qualche parte.
</p><p>
E allora su. Su, su, su. Su per questo sentiero, con lo zaino sulla schiena, una
borsa di qualcuno in spalla perché lui poverino da solo non ce la, e no, non può
morire di stenti e finire in paradiso il disgraziato, ma può invece donare a me
la possibilità d'aiutarlo per avvicinare me alla santità e altro proselitismo
gratuito mentre salgo il sentiero con il prete. Fanculo.
</p><p>
Decido che tenere il passo di questa gente e stare ad ascoltare le loro
conversazioni (già, perché se non hai il fiato per camminare con la borsa invece
per raccontarti come va il lavoro ce l'hai, eh, stronzo?) non vale la pena,
prendo il mio ritmo e in meno di un minuto il distacco tra me e questa gente mi
garantisce perlomeno di non sentire le loro cazzate. E arrivo anche prima, che
non è male; potrò infatti riposarmi, una volta in cima.
</p><p>
Arrivo in cima alla rampa più erta del mondo, un tratto di cento metri con un
dislivello di almeno centodieci metri, tutta coperta di erba bagnata (``Ha
piovuto ieri così da far bel tempo per tutto il campeggio'' disse il prete) che
ti consente di scivolare meglio di qualunque altro pendio, e che trovo in cima?
Trovo un simpatico cuoco da campeggio che ha appena cominciato a preparare il te
caldo, oh, bontà divina.
</p><p>
``Posso averne un bicchiere?'' chiedo con tutta la cortesia che ho in corpo.
</p><p>
``Eh, no, devo prima sentire il prete'' dice lui. Il prete, ovviamente, sale per
ultimo e con una certa calma da prete. Che gli si fermi il cuore, a quello. E
anche al cuoco, lui che non può far da mangiare senza permesso.
</p><p>
E allora, prima di arrendermi, chiedo se posso sbarazzarmi della mia roba,
magari sapendo quale sarà il mio posto per dormire. E scopro che l'unico tizio
già presente non sa un cazzo dell'organizzazione (la sa il prete, ovviamente) e
scopro pure che le tende sono ancora da montare. E le possiamo montare? No, i
picchetti ce li avrà il prete, già!
</p><p>
E porchi e madonne, che tanto il prete è in fondo alla valle e non può sentirmi
(poteva sentirmi, invece).
</p><p>
E quindi mi arrendo, allargo la mia roba come capita, su per il prato,
organizzando i pesi in modo che niente rotoli giù per il pendio, poi giunge una
voce che annuncia: ``\corvino, vai mo' a dare una mano al tizio con il
generatore''. Già, perché non c'è la corrente in campeggio ma qualcosa di
elettrico ce l'abbiamo (tipo il faro, per vederci la notte visto che metà delle
attività previste sono notturno Dio solo sa perché) e quindi occorre trasportare
in baita il generatore elettrico Diesel. E scopro che trasportarlo significa
esattamente tornare alla base del pendio impossibile, prendere il generatore per
un lato, accompagnato da un altro martire evocato per l'occasione, e trascinare
i 30 chili che il generatore pesa su per la cazzo di rampa.
</p><p>
E giù porchi e madonne, sia io che l'altro martire, che il prete è ancora
lontano e non può sentirci (poteva sentirci).
</p><p>
Nel tempo che io e quel povero tizio, \lucky, impieghiamo per compiere la nostra
via crucis, tutti gli altri pellegrini all'alpe sono giunti alla baita. Si,
anche il prete. E allora si, possiamo bere il te caldo; e no, non ne rimane per
noi, dopo che tutti gli altri stronzi prima di noi ne hanno preso un bicchiere.
Vi si possano distendere le budella, dannati stronzi senza il generatore sulla
schiena!
</p><p>
A noi rimane come unica consolazione una succosa, conviviale e fraterna,
comunitaria quanto insapore fetta d'anguria. Di chi cazzo è stata l'idea
geniale? L'anguria, il frutto/ortaggio con il minor rapporto volume/nutrimento,
che non è altro che acqua e zucchero (poco zucchero) riempito di semi e mezza
vuoa dentro. Chi ha deciso di portare le anguire? Hey, aspetta un attimo, chi le
ha portate? Com'era la borsa con le angurie? Era la mia, quelle cazzo di borsa
che m'avevano affibiato al parcheggio. Fanculo voi, le vostre angurie e quella
borsa di merda, che neanche aveva le cinghie regolabile ch'erano troppo lunghe
per le mie braccia: vaffanculo e che vi germoglino i semi nella pancia.
</p><p>
Ma come ho detto prima, non c'era solo questo nella mia vita. C'erano anche cose
buone, ben nascoste tra le rotture di palle, ma c'erano. Non prima di aver
montato le tende. Non prima di aver ampliamente discusso con cani e porci su
come/dove/perché mettere le borse in tenda. Non prima che qualcuno mettesse
delle scarpe sul mio cuscino, e varie altre amenità commesse da un anonimo
sconosciuto che ancora maledico la gente a caso sperando di beccare lui.
</p><p>
La cosa buona arriva: un tale, uno dei responsabili, richiamò l'attenzione di
tutti mentre appendeva un cartello bello grande alla porta principale, l'elenco
dei gruppi. Per comodità organizzative, per spartirsi i vari mestieri necessari
a mantenere l'ambiente abitabile e per avere già le squadre pronte per giocare,
tutti i partecipanti al campeggio vennero infatti divisi in tre gruppi; se
ricordo bene, 22 partecipanti per quattro gruppi. Ora io non ricordo nulla di
quel momento, perché non appena lessi il nome di Camelia vicino al mio
nell'elenco dei gruppi, smisi di vedere e di sentire, e finii a trottelellare in
un pianeggiante prato colmo di fiori dai colori sgargianti.
</p>

