<h2>Il lampione</h2>
</p><p>
La mattina seguente ci fu quella dannata prova di latino.
</p><p>

</p><p>
Traduzione di un qualche passo di un qualche autore, che narrava di come un
qualche tizio che dopo essere sfuggito ad una marmaglia che intendeva ucciderlo
per un crimine non commesso, riesce a raggiungere una piazza e a tenere un
discorso che dovrebbe provare la sua innocenza.
</p><p>
Boh, non mi parve affatto plausibile come storia, quindi ebbi la geniale
intuizione di alterare lievemente la traduzione, piegando i verbi e riadattando
la situazione. Quello che molte delle mie compagne col 10 in pagella potevano
permettersi largamente, sia sui temi che alle interrogazioni, ma che
evidentemente risultò vietato a quelli come me.
</p><p>
Bel tema di merda. Anche se ancora una volta mi salvai dall'insufficienza in non
ricordo ben quale modo.
</p><p>
Tra le altre cose, l'orario di quel giorno (non ricordo quale fosse esattamente)
prevedeva una cosa come otto ore di lezione con un'abbondante pausa pranzo, il
ché m'obbligò a restare a scuola fino alle cinque di pomeriggio, passando tra
l'altro più di un'ora chiuso in aula magna senza il permesso di uscire se non
per andare al bagno; e no, non potevo starmene tre quarti d'ora al bagno senza
scatenare le ire (non il panico, le ire) di qualche invadente insegnante che
viene a cercarti al cesso se ci metti troppo... Quindi me ne rimasi per un tempo
interminabile rinchiuso nell'edificio scolastico, vagamente impegnato a seguire
le lezioni, senza poter pensare veramente all'accadato della sera precedente.
</p><p>
Non la parte interattiva, che al momento m'interessava assai poco, ma alla parte
finale che vide la mia impossibile e inspiegabile litigata con la gravità, con
la mia vittoria, in effetti.
</p><p>
Passai almeno due ore la mattina a pensare esclusivamente al latino, ma le due
ore seguenti invece le spesi a pensare a quello che avevo mangiato, a quello che
avevo bevuto, se avessi per caso leccato rospi, inalato sostanza strambe,
toccato barili pieni di robaccia verde, questa e altre cose terribili e
fantasiose, e comunque più plausibili di quello ch'era successo veramente.
</p><p>
``Ma era poi successo veramente?'' fu invece la domanda che mi posi a pranzo;
infine, nella pausa e poi avanti avanti fino alla sospirata fine delle lezioni
mi chiesi: ``Ed era veramente tutto bianchiccio quand'ero in volo?''
</p><p>
Mi parve di ricordare infatti di vedere bianchiccio, oltre a provare
quell'anomala sensazione di leggerezza. E ci pensai a lungo. Anche perché quelle
tre ore di lezione del pomeriggio si rivelarono essere un interminabile monologo
del professore di `educazione artistica', lo stesso stronzo che la giornata
presnete non s'era risparmiato di descrivere un solo chicco di grano, una sola
fibra di canapa, un solo granello di sabbia, un singolo tratto di pennello di
tutto quel che noi si vide in gita. E per un qualche motivo non era stanco morto
come lo eravamo noi.
</p><p>
Beh, per la verità quello non era vero: io non ero affatto stanco morto.
All'inizio pensai che fosse l'adrenalina per il compito di latino: ti svegli
nervoso, cammini nervoso, bevi nervoso, pedali nervoso, sali nervosamente le
scale, ti siedi nervoso, poi leggi nervosamente il testo originale, ne afferri
vagamente il senso, traduci un po' come capita, poi ci pensi e ci ripensi, poi
arriva nervosamente l'ora della consegna, e tu sudi come un condannato a morte,
consegni nervosamente il tuo foglio e poi ti rilassi. Quando poi ti rilassi e
abbassi la guardia, il sonno e la stanchezza ti piombano addosso come giaguari
su un grosso alce stanco.
</p><p>
E quello effettivamente era accaduto ad una buona metà dei miei compagni. Altri
non erano più stanchi del solito, anzi erano più freschi della maggior parte di
noi perché avevano dormito durante il compito; io, invece, stavo abbastanza
bene. Ero in forze, nonostante la nottataccia e il tema. Me ne resi conto
soltanto alle cinque, quando tutti se ne tornano stancamente a casa, camminando
con passo pesante.
</p><p>
Io invece volai verso casa. Non letteralmente, stavolta. Tornai in bici, per
terra, ma molto in fretta. Non molto in fretta (ero già prima il più veloce ad
andarsene) ma comunque non lento.
</p><p>
Arrivato a casa mi sedetti a riflettere seriamente sulla sera del giorno prima,
cercando di capire come e se avrei potuto verificare l'accaduto. Presi anche in
considerazione la possibilità di parlarne con \camelia, ma l'istinto mi disse di
non avvicinarmi a lei (e non me lo disse mai), quindi abbandonai l'idea e decisi
di proseguire in solitaria.
</p><p>
Ma non avevo altro punto di partenza che casa di \camelia. Dovevo andare lì, con
il pericolo di trovarla per strada? In effetti, mi resi conto mesi dopo che in
quei momenti ero stato una mammoletta cagasotto per aver provato una così
profonda paura di un incontro che non capitò più.
</p><p>
Nella disperata e irrazionale ricerca di un luogo alternativo, vagai un po' di
qua, un po' di là e alla fine mi ritrovai al parcheggio. Quello dietro
l'edificio sagomato in malo modo, sotto il lampione traditore. E notai che il
lampione stava bene. Non male, dato che soltanto un giorno prima doveva essersi
fulminato; e non male neanche gli operai del pronto intervento riparazione
lampioni; quanti ce ne vorranno di quelli come loro per cambiare una lampadina?
Almeno un paio, perché immagino ci voglia qualcuno a tenere la scala, o a
manovrare il braccio della gru prima che l'altro possa raggiungere gli otto o
nove metri d'altezza del lampione.
</p><p>
E mentre pensavo alla lampadina funzionante mi sentii sollevato. Non
particolarmente felice, ma rincuorato. Gaio. Sollevato. Sollevato è il termine
giusto, perché mentre pensai a quanto fosse alto il lampione e stimai che
fossero appunto otto o nove metri, ci arrivai.
</p><p>
Con più coscenza e meno sonno del giorno prima, stavo galleggiando a svariati
metri d'altezza e vidi da vicino com'è la lampadina di un lampione. Nulla di
particolarmente interessante: è soltanto una lampadina molto grossa, coperta da
un verto piuttosto spesso. Ma niente di ché. Decisi che non valeva la pena di
osservare oltre, anche perché era tecnicamente ancora giorno, nonostante i
lampioni fossero accessi. Saranno state le sette, le sette e mezza di sera, e
non ero affatto certo che nessuno sarebbe passato per quel parcheggio. Per
quanto poco frequentato che fosse, era comunque pieno di automobili parcheggiate
in almeno la metà dei posti a disposizione, e in quell'ora molti sarebbero
potuti tornare a casa dal lavoro (forse era un po' tardi, ma perché rischiare?).
</p><p>
Decisi di scendere. E mi resi conto di non sapere affatto come fare.
</p><p>
Non fu bello.
</p><p>
Per un attimo mi sentii idiota, semplicemente.
</p><p>
Poi immaginai per un attimo la scena: passa un tizio qualunque, trova uno appesa
al lampione, lo vede (mi vede) e urla, chiama la polizia e cose del genere.
Vabbeh, pensai, mal che vada dirò di essermi arrampicato e di non essere più
capace di scendre, come fanno i gatti sugli alberi.
</p><p>
Non molto plausibile, ma meglio che farsi beccare.
</p><p>
Poi immaginai che fosse lei a vedermi. E allora caddi. Caddi a peso morto. Non
fu bello. Nient'affatto.
</p><p>
Dev'essere sembrato divertente, visto da fuori. Ma posso garantire che da dentro
l'unico aspetto positivo fu la brevità della caduta. Perché non caddi molto a
lungo, non abbastanza per girarmi ma neanche abbastanza a lungo perchè l'impatto
fosse fatale.
</p><p>
Caddi secco secco sulla schiena, ma m'accorsi di star urlando soltanto per lo
spavento. Ritenni, sul momento, di essere stato enormemente fortunato per non
aver subito la benché minima frattura, soltanto i graffi sulle mani per essermi
appoggiato in tutta fretta sull'asfalto, il fiato corto per aver gridato, ma per
il resto stavo bene.
</p><p>
Decisi di andarmene, di archiviare quell'episodio e di dormirci sopra. Ma
tristemente non era nemmeno ora di cena, e prima del sonno sarebbero passate
ore. Mi odiai per non aver aspettato la notte, prima di tentare strambi
esperimenti di piegamento delle leggi fisiche.
</p><p>
Tornai a casa, ebbi la mia cena. In silenzio, raccontando di com'ero caduto
passando in un qualche posto senza onore, cercando di distoglere l'attenzione,
cambiando discorso, controinterrogando i miei genitori sulla loro giornata.
</p><p>
Decisi di non uscire quella sera, per evitare altri spiacevoli incidenti e altre
possibili cadute, anche peggiori. Per non sfidare la fortuna.
</p><p>
Però un dubbio mi assillava. Ero salito fino alla lampadina senza fare nulla di
particolare. Non stavo effettivamente facendo nulla; niente mosse, nessuna
parola. Mi stavo chiedendo come fosse la lampadina, ed ero contento che
funzionasse.
</p><p>
Aspettai qualche giorno, per evitare di tornare troppo presto al luogo
incriminato (chissà che qualcuno m'avesse visto) e per farmi passare il dolore
alle mani (qualche graffio me l'ero fatto).
</p><p>
Così, due notti dopo il mio secondo volo tornai al lampione. Questa volta ero
molto più preoccupato che interessato ai possibili risultati, semplicemente
avevo paura di cadere come l'altra volta. Un po' come quando devi saltare sopra
un buco, o da una roccia all'altra. Sai bene che puoi saltare fino a lì, non è
neanche tanto lontano, con un passo ce la fai, ma sai anche che se per caso
dovessi scivolare o cadere ti faresti parecchio male. E non solo, c'è anche il
disonore per aver perso i denti facendo una cosa stupida. Non una grande
prospettiva. In fondo, la paura dell'altezza è una cosa assolutamente naturale.
</p><p>
Come quando ti vuoi tuffare da una scogliera: te la fai addosso, rimugini un
pochetto, valuti l'altezza, la profondità dell'acqua. Dopo un po' ti decidi e
salti oppure ti arrendi. Se riesci a buttarti la prima volta puoi tornare allo
stesso salto tutte le volte che vuoi.
</p><p>
Ma con il mio lampione non fu la stessa cosa. Forse il lampione era stato
sostituito con uno più alto, assieme a tutti quelli del parcheggio. Non avevo
alcuna voglia di tornare lassù. E poi la luce del lampione era andata, di nuovo.
Cominciai a pensare a quanto potente potesse essere il destino, che poteva
fulminare quella lampadina una volta al giorno. Forse era davvero il caso che
qualcuno abbattesse quel parcheggio e rifacesse tutto l'impianto, con un po' di
sale in zucca, stavolta, magari.
</p><p>
Dopo una buona mezzo, finalmente, me ne andai. Non valeva la pena di rischiare i
grattoni sulle mani, la schiena e il culo per vedere da vicino un lampione
spento. Così me ne andai, sconfitto.
</p><p>
Dopo una ventina di passi, mentre compativo la mia codardia, vidi sfarfallare la
luce alle mie spalle. Quando mi voltai a controllare, notai chiaramente che il
lampione era tornato a funzionare. Il destino doveva avercela giù dura con quel
lampione: decisi di tornare a controllare. Un po' più allegro, stavolta, deciso
a scoprire il perché di quel portento.
</p><p>
Ma mentre mi avvicinavo, il lampione si spense nuovamente. Fanculo.
</p><p>
Tornai sui miei passi senza indugio e feci per andarmene.
</p><p>
E il lampione si riaccese.
</p><p>
Allora ci tornai una terza volta.
</p><p>
E ancora una volta si spense.
</p><p>
``Lampione del cazzo'' pensai ``Mi prendi per il culo? Fanculo te e chi t'ha
messo qui''.
</p><p>
L'intero parcheggio divenne buio. Tutti i lampioni. Tutti i cazzo di lampioni.
</p><p>
Mi convinsi che qualcosa non andava e mi allontanai fino al distributore di
benzina, dal quale si poteva intravedere l'area del parcheggio, senza vedere
direttamente le luci. Quando fui là fuori, la luce era tornata ancora una volta.
</p><p>
Mi chiesi seriamente se potessi essere io la causa.
</p><p>
``Bravi lampioni, continuate a funzionare'' dissi.
</p><p>
E funzionarono.
</p><p>
Poi tornai di là, in mezzo al parcheggio. E funzionavano.
</p><p>
``Lampioni del cazzo, spegnetevi''. E funzionavano.
</p><p>
Evidentemente si era trattato di una clamorosa botta di sfiga ed io non
c'entravo nulla.
</p><p>
Un po' deluso pensai ``Teoria del cazzo, che mi salta in mente?'' e il lampione
più vicino si spense.
</p><p>
Rimasi in quel parcheggio per un paio d'ore.
</p><p>
Provai e riprovai, per essere assolutamente certo di poterlo fare a volontà.
</p><p>
E dopo parecchi tentativi, non senza un discreto sforzo di concentrazione, fui
in grado di spegnere un lampione qualsiasi, fissandolo e incazzandomi. Finché
restavo rilassato a sufficienza, invece, tutte le luci restavano accese.
</p><p>
Alla fine feci provai anche qualche coreografia, spegnendo i lampioni in ordine,
o due a due. Due insieme mi risultò più difficile, ma capii che sfruttando la
rabbia derivante dal fallimento potevo effettivamente riuscirci. Mi bastava non
essere sicuro di poterlo fare.
</p><p>
E fu così che imparai a spegnere i lampioni.
</p><p>
Ed ero abbastanza sollevato da sollevarmi, e andai a controllare di nuovo come
fosse la lampadina del lampione da vicino. Poi mi venne l'idea che mi rovinò la
schiena: ``E se spegnessi la luce mentre fluttuo?''
</p><p>
Male, male, male, molto male.
</p><p>
Spegnere una luce significa anche tornare alla gravità, come scoprii
dolorosamente. E' abbastanza accettabile che una mente non ben addestrata non
possa provare due emozioni diverse in uno stesso momento. Non fu affatto facile
imparare una cosa del genere, infatti, e mi ci volle un sacco di tempo. Ma in
quel momento mi fu lampante quanto fosse importante fare una sola cosa alla
volta.
</p><p>
Ero per terra, caduto pesantemente sul culo da una buona altezza. Già allora,
come avrei scoperto poi, ero abbastanza duro e non mi ruppi nulla, ma mi presi
comunque una bella incassata alla schiena, e non mi rialzai prima di una decina
di minuti. E quando mi rialzai fu anche peggio, perché la pressione sulle ossa
del bacino cambiò posizione o direzione; non so esattamente come funzioni, so
che mi si risistemarono le ossa e il procedimento, seppur naturale (almeno
spero) non fu affato indolore.
</p><p>
Quanto è brutto camminare con il male alla schiena spero non dobbiate mai
saperlo. E' brutto. Parecchio. Stai piegato perché non riesci a stare dritto, e
sai che stare piegato non aiuta la schiena; vorresti andare più in fretta,
perché vorresti arrivare prima, ma i movimenti sono più dolorosi quanto più sono
rapidi, quindi vai piano e sai che ci metterai di più, aggravando la tua
situazione e dilatando l'attesa del divano. Oh, lui sì è tuo amico e potrebbe
aiutarti e confortarti, ma è lontano, a casa che ti aspetta.
</p><p>
E io abitavo anche al quinto piano. Niente ascensore, lunga lunga ascesa, venti
rampe diverse con lo scorrimano da un solo lato.
</p><p>
Decisi che non avrei tentato di ritornare a casa direttamente, ma che avrei
cercato un posto dove riposarmi, possibilmente stando comodo.
</p><p>
Mi trascinai fino al parco, oltre il parcheggio. Non quello dietro, dove avevo
effettuato i miei esperimenti, ma quello davanti. Fortunatamente, non c'era
nessuno. Sfortunatamente, nessuno era là per darmi una mano. Ma non credo che mi
sarebbe stato più d'aiuto che d'impiccio dover spiegare a qualcuno come m'ero
rovinato così duro. Quindi solo soletto, porcando e bestemmiando, impiegai
soltanto una decina di minuti per coprire quei 150 metri che mi dividevano dalla
ma meta. E quando di arrivai, fui indeciso se sdraiarmi sull'erba oppure
stendermi su una panchina.
</p><p>
L'erba sarebbe stata più morbida, ma era ancora novembre e cominciavano ad
essere le undici di sera. E faceva freddo. Forse sarebbe stata la panchina. Ma
la panchina in metallo sarebbe stata ugualmente fredda, se non peggio.
</p><p>
Alla fine mi decisi e mi sdraiai per terra.
</p><p>
Non male.
</p><p>
Quando mi ripresi, non sapevo che ore fossero.
</p><p>
Feci per rialzarmi, ma la schiena si oppose. Si lasciai cadere, provai a
cambiare posizione, cercando di capire quali muscoli dolessero di più. Dolevano
tutti, in pratica. Mi rassegnai a rimanere lì qualche minuti ancora.
</p><p>
Cercando di dimenticare, o almeno accantonare, il dolore, mi sforzai di
ricordare i miei progressi di quella sera. E non erano pochi, in effetti.
Ritrovata un poco di lucidità, mi chiesi come si distribuisse il peso durante il
volo. Non essendo appoggiato a nulla, forse non m'avrebbe fatto male la
schiena... D'altronde, il volo spontaneo non era certo uno dei miei campi di
specializzazione, al tempo.
</p><p>
Senza particolare impegno, decisi di provare a sollevarmi, senza dovermi alzare.
Senza risultati, anche.
</p><p>
Come immaginavo, voler volare non basta. Gli uomini l'han voluto per svariate
migliaia di anni prima di riuscirci. Avrei potuto io, piccolo uomo, riuscirci in
una sera? O in tre sere, contando tutti i giorni? Alla fine, sì, ce la feci. Ma
mi ci volle un bel pezzo.
</p><p>
Cominciai cercando di ricordare tutto quello che stavo facendo le altre volte,
prima di staccarmi da terra. Forse James Matthew Barrie sapeva che dovevo fare,
quando scrisse che Peter Pan poteva volare grazie ai suoi ricordi felici, perché
ebbi effettivamente un ricordo felice abbastanza forte da tirarmi su.
Letteralmente. Non basta affatto voler volare, per volare.
</p><p>
Non per me, almeno. Ma mi bastò ricordare di averlo fatto. Il `voler volare' è
soltanto un desiderio, e a quanto pare molto raramente i desideri, in
particolar modo quelli relativi a sé, non sono positivi; e non possono essere
usati in questo ambito; quello che occorre è una spinta, una spinta emotiva.
Queste nascono spontaneamente (come quando volai la prima volta) ma possono
anche essere innescate dai ricordi; i ricordi diventano quindi molto importanti:
riportare la mente in una condizione positiva tramite un ricordo adeguato può
far la differenza tra un ragazzo con il mal di schiena disteso sull'erba in un
parco a mezzanotte e un ragazzo che vola verso casa sua.
</p><p>
Volare verso la propria casa è una cosa estremamente utile quando non si sta
bene. Vi dirò anche che volare sulla schiena, senza sapere dove si sta andando
non è puoi così rassicurante. E non sentirsi all'altezza di volare quando si
vola si traduce immediatamente nel non essere a quell'altezza, perché si scende.
L'attitudine positiva deve essere mantenuta senza pausa, perché con essa è
possibile anche scendere (senza danni) e arrivare dove si vuole; senza invece si
va sempre dalla stessa parte. E in effetti tutto quelli che feci fu pensare
``Due giorni fa ho volato, adesso invece voglio andare a casa'' e mantenere la
concentrazione sul quel pensiero.
</p><p>
E fu così che, anche in quelle condizioni non così buone, ritornai a casa. Come
alla mia prima esperienza, due giorni prima, atterrai sul balcone, che
fortunatamente da direttamente su camera mia. Solo che questa volta,
evidentemente, non ero stato l'unico ad entrare in camera mia; un qualche
genitore doveva essere passato di lì, durante la mia assenza, e doveva aver ben
pensato che la porta-finestra che da sul balcone fosse rimasta aperta per
dimenticanza, non dietro specifica intenzione. La specifica intenzione,
abbastanza larga, era che sarei potuto tornare volando a casa, e magari entrare
dalla finestra sarebbe stato comodo.
</p><p>
Fu così che fui costretto a sperimentare il volo a bassissima quota, il volo di
precisione. Lentamente, salii di un metro, sorpassai la balaustra, poi pensai
che sarebbe stato meglio non scendere in piena vista, quindi mi portai più
vicino che potei ad un grosso castagno che ornava la strada e scesi lungo il suo
fusto; una volta a terra, controllando più e più volte che nessuno se ne andasse
a spasso a quell'ora (era mezzanotte passata, ma non si sa mai) mi avvicinai
alla porta, estrassi le chiavi, ed entrai sul giroscale. Ora, la probabilità di
incontrare un coinquilino a quell'ora in quella stagione dovrebbe essere
abbastanza bassa, ma qualcuno accese la luce delle scale. Allora mi preoccupai
non poco e scesi in fretta. Non che fossi molto alto, non più di mezzo metro dal
pavimento, ma comunque persi la concentrazione e caddi. Come saltare dal letto e
cadere, come scavalcare un muretto, nulla di particolare, ma con la schiena in
quelle condizioni immaginai di dovermi sostenere con mani e piedi. Invece
atterrai in piedi, come i ginnasti alle gare, senza alcun dolore. Dopo un attimo
speso per realizzare che la schiena stava bene, sentii avvicinarsi i passi di
qualcuno, decisi che avrei finto e quindi comincia a salire le scale come se
niente fosse.
</p><p>
Chi scese era uno degli studenti universitari in affitto al quarto piano, subito
sotto il mio appartamento. Prima, fino all'anno prima, ci viveva un vecchiaccio
di quelli che non sopportano nessuno, né vicini né parenti, tirchio fino
all'osso, e mortalmente serio sulla legge che vuole il silenzio alla sera dopo
le nove. Quando finalmente decise di andarsene da questo mondo, uno dei suoi
figli che aveva poca briga e voglia di fare soldi decise di affittare
quell'appartamento a studenti o gente ugualmente strana, secondo un contratto
oscuro che forse neanche esisteva veramente. In barba al morto e alle sue
