?>

<h2>Le botte</h2>
<div class="intro"><p>
Tutte le leggende hanno un fondo di verità: lo sapevo allora come lo so
adesso. Quello che scoprii allora e che ora insegno a chi ne ha bisogno è che le
\streghe\ picchiano. Picchiano durissimo, picchiano come i fabbri. No, peggio
dei fabbri.
</p></div><p>
Non dubitai più delle parole dei miei due seguaci animali su come nessuno fosse
sopravvissuto.
</p><p>
Per prima cosa, passammo entrambi `di là'. Non è opportuno combattere queste
battaglie `di qua': la maggior parte delle presenze sono troppo fragili e non
possono reggere gli effetti dello scontro.
</p><p>
La \strega\ abbandona il suo aspetto mortale e assume quello che immaginai
essere la sua vera forma. Pur restando fondamentalmente la stessa persona,
mostrò (o forse smise di nascondere) le sue... come chiamarle?... Appendici
streghesche? E `streghesco' sarà una parola vera?
</p><p>
Ma chiaramente non ebbi il tempo di cercare queste riposte, allora. La \strega\
nel suo vero aspetto assomigliava a... uhm... Non lo so. Sembrava la stessa. Con
una quantità e un volume di capelli esagerati, svolazzanti, aguzzi, affilati;
con i risvolti degli abiti, lunghi in modo innaturale, che schioccavano
tutt'intorno. Stava ad un paio di metri da terra, fluttuando, con gli occhi in
fiamme.
</p><p>
Ebbi paura.
</p><p>
Io, intanto, decisi di mostrarmi all'altezza della situazione, sfoderando
anch'io il mio vero aspetto. Non che avessi un `vero' aspetto da mostrare, ma
istintivamente mi misi addosso i vestiti che meglio si adattassero a quella
situazione. Se avessi dovuto morire, perché non avrei dovuto farlo elegante?
</p><p>
Indossai quindi un completo nero. Senza cravatta: pantaloni e camicia,
completamente neri, senza ricami, senza fronzoli. Aggiunsi degli stivali e dei
guanti in pelle. 
</p><p>
E aggiunsi anche il cappello. E il cappotto lungo fin quasi a terra.
Personalmente lo ritenni abbastanza figo. E non lo cambiai più.
</p><p>
Ma l'abbigliamento non mi avrebbe protetto in battaglia.
</p><p>

</p></div><div class="section"><p>

</p><p>
Ebbi l'impressione che qualcosa di terribile mi sarebbe arrivato addosso, passai
istintivamente all'indaco, divenni duro come un sasso (forse di più, non ero
certo di quanto forte o resistente fossi, non avevo provato, se non contro il
muso di \battesimo) e sperai che bastasse.
</p><p>
Bastò, in effetti, perché non morii subito.
</p><p>
La \strega\ pronunciò qualche parola, non saprei cosa, non saprei in quale
lingua. Non seppi dire nemmeno se fosse un insulto nei miei confronti o se fosse
qualcosa di magico; quello che so è che mi piovvero addosso fulmini e saette, da
ogni direzione, da sopra e da sotto. La terra attorno a me esplose e tutto andò
in pezzi; non io, fortunatamente.
</p><p>
Nonostante cercassi di scansare almeno parte di quello che arrivava, ero
decisamente troppo scuro e troppo lento per riuscirci. Battesimo mi ricordò di
come il violetto mi lasciasse lento; avevo il colore sbagliato per vincere a
quel gioco; ma non avrei potuto cambiare, allora, perché sarei stato beccato
prima di poter schivare. Quindi, decisi di prenderle e resistere.
</p><p>
Ebbi fortuna: in qualche modo, i fulmini non funzionano molto bene sul violetto.
O forse ebbi soltanto una gran botta di culo; o forse ero veramente troppo
grosso per quel genere di attacco. Non saprei dirlo, ora, perché non mi sono più
lasciato beccare.
</p><p>
Ne uscii fumante, con gli abiti un poco sgualciti.
</p><p>
Tossii con tutta la forza che mi era rimasta, che non mi parve molta, sul
momento. Ruotai il capo nella sua direzione. Non appena il fumo si diradò, un
attimo dopo, i nostri sguardi s'incrociarono.
</p><p>
Vedermi ancora in vita dev'essere stato profondamente irritante per la \strega,
che probabilmente avevo (come le sue numerose colleghe) ucciso ogni avversario
senza grandi sforzi, sempre con un singolo colpo. Venni poi a sapere ch'era
andata esattamente così.
</p><p>
Ruggì. Non so se lo fece di bocca o in qualche altro modo, ma ruggì.
Qualcos'altro stava arrivando, e il secondo giro s'annunciava essere ben
peggiore del primo assaggio. Sgranai gli occhi e mi sentii morto dentro;
sbiancai e corsi come il vento per allontanarmi.
</p><p>
La seconda volta ci fu una scrosciata di raggi e onde blue e viola. Un pessimo
accostamento, da vedere e anche da sentire, immaginai: il suo attacco mi seguì
come un occhio di bue segue il suo attore sul palco. Corsi sufficientemente
veloce per scansare la prima bordata, o così almeno pensai: non andava affatto a
bordate, infatti, era continuo. Il cono del suo sguardo stava mandando a fuoco
tutto quello che di intatto c'era rimasto.
</p><p>
Non che ce ne fosse molto. Corsi per allontanarmi, corsi in cerchio attorno a
lei, sperando che non riuscisse a ruotare il collo abbastanza in fretta, corsi
tentando di avvicinarmi progressivamente, per allargare l'angolo di rotazione e
per tentare in qualche modo di contrattaccare.
</p><p>
Corsi, credo per una decina di secondi, facendo un paio di giri. Quando le fui
abbastanza vicino da avere la parvenza di un'opportunità, quella interruppe
quella tecnica devastante e mi sferzò di artigli, con i capelli e i lembi degli
abiti. Scoprii quanto fossero affilati e rapidi. Mi presero nonostante il mio
colore fosse il più corretto in quel frangente.
</p><p>
Urlando per il dolore, mi allontanai di corsa. Incampai in qualcosa, o forse mi
cedettero le gambe, e caddi a terra.
</p><p>
Credetti d'essere finito. Attesi un colpo mortale alla schiena.
</p><p>
Non arrivò.
</p><p>
Ero abbastanza disperato e non avrei certo considerato troppo disonorevole
accetttare un aiutino in quell'occasone. Stavo cercando di capire quali fossero
le cose da fare per garantirmi di salvare la pellaccia, e inconsciamente devo
essere cambiato in rosso e in verde.
</p><p>
Il verde mi permise di riprendermi dalle botte, dalla bruciature, dai graffi. Il
rosso mi permise invece di percepire gli unici pensieri altrui presenti nelle
vicinanze, quelli della \strega. E' il bello del multicolore.
</p><p>
Stava trattenendo l'attacco per un suo dubbio: stava cercando di decidere se
fermarsi e mangiarmi, per ottenere la mia vita, o se invece uccidermi e ottenere
soddisfazione. Per un instante, si chiese addirittura se non fosse il caso di
tenermi in vita, mettermi in una gabbietta ed espormi a casa sua.
</p><p>
O di usarmi come giocattolo.
</p><p>
L'ultima prospettiva non mi parve poi così male, in quelle condizioni. Forse la
vicinanza alla morte ti rende pazzo per qualche secondo.
</p><p>

</p></div><div class="section"><p>

</p><p>
In ogni caso, fui contento di avere quei preziosi istanti.
</p><p>
Il mio verde è potente.
</p><p>
Fui in grado di rimettermi in piedi, ma restai per terra. Esternamente, a quanto
pare, ero ancora bianco. Dicisi di spegnerlo, e persi il colore. E questo è il
bello dell'essere lo \spontaneo\ trasparente.
</p><p>
Questo indusse la \strega\ a credere che fossi finalmente morto. Le botte doveva
sembrarle prese in quantità sufficiente. Si avvicinò lentamente, senza toccare
terra.
</p><p>

</p></div><div class="section"><p>

</p><p>
Trattenni il fiato, da buon morto.
</p><p>
Sperai con tutto me stesso che fosse sopra di me, in quel momento, perché ero
faccia a terra e per quanto ne sapevo non potevo vedere alla mie spalle.
</p><p>
Mi voltai di scatto, gambe bianche e pugni violetto.
</p><p>
Sferrai il sinistro delle castate di Rozan.
</p><p>
La raggiunsi al mento.
</p><p>
Volò all'indietro, arcuandosi, cadde una decina di metri più in là.
</p><p>
Saltellando sulle punte dei piedi, sentento nelle orecchie `Gonna fly now' di
Bill Conti, esultai a pugni alzati. Come Rocky in cima alle scale della
biblioteca di Philadelphia.
</p><p>
Fu un po' prematuro, perché la \strega\ si rialzò. Ma fu anche una grossa
soddisfazione.
</p><p>
Perdeva sangue dal naso e dalle gengive, le colava su mezza faccia. Il suo umore
non era peggiorato molto, però. Forse era già vicino al limite.
</p><p>
Gridò ``Dannato \spontaneo\, come osi?'' e si scagliò in avanti, sempre senza
toccare terra.
</p><p>
Questa volta attaccò con più furia e meno tecnica, e mi lasciò uno spiraglio:
venne in avanti con le mani, con i capelli e tutte quelle sue appendici
streghesche fruscianti, ma non si coprì minimamente.
</p><p>
Decisi di reagire alla sua carica, correndole incontro. Tutto violetto, fu
abbastanza facile scansare gran parte di quei fronzoli.
</p><p>
La presi con una spalla, in pieno petto. Mi presi anche un singolo secondo per
apprezzare quanto, nonostante l'aspetto terribili, la quantità di estremità
affilate che aveva sfoderato, lo sguardo di fuoco e la bocca sanguinante, il
petto era ancora morbido.
</p><p>
Non ho detto che la \strega\ non era poi così alta. Un paio di centimetri in
meno del sottoscritto, che non è un gigante. Mi bastò un piccolo salto per
prenderla in pieno, spingendola a terra e cadendoci sopra.
</p><p>
Rimessomi in piedi, mi allontanai rapidamente di un paio di passi. Attesi di
vedere che sarebbe successo.
</p><p>
Non successe niente.
</p><p>
Sospettai che stesse utilizzando il mio trucco di un minuto prima. Troppo
comodo. Però il dubbio mi rimase.
</p><p>
Che fare, a quel punto? Dovevo uccidere la \strega? Seppur sapendo cos'era (e
soltanto a grandi linee), avrei dovuto uccidere una persona che conoscevo da
anni? Avrei dovuto uccidere una ragazza? Avrei potuto uccidere \camelia?
</p><p>

</p></div><div class="section"><p>

</p><p>
I dubbi fanno perdere un sacco di tempo.
</p><p>
E il suo ERA un trucco.
</p><p>
Un'illusione, per la precisione. Aveva lasciato un'immagine, un residuo di sé
nel punto in cui l'avevo piantata, ed era scappata poco più in là per
nascondersi dietro uno degli alberi che i suoi fulmini non avevano incenerito e
abbattutto.
</p><p>
Silenziosamente, strisciò alle mie spalle e tentò per la terza volta d'infilarmi
le unghie nella schiena.
</p><p>
Una mia combinazione di rotazione e di durezza (adoro il violetto, anche se non
è una cosa che suona bene, detta da un uomo) le impedì di arecarmi danni
significativi.
</p><p>
Ero troppo vulnerabile, troppo spaventato per non reagire violentemente e
rapidamente. Afferrai quel posto che aveva tentato di ferirmi e tirai. Tirai
forte, evidentemente, più di quanto avessi intenzione di fare e più di quanto
credevo possibile.
</p><p>
Con un rumore abbastanza secco, come di un albero abbattuto, la spalla si ruppe
e il braccio della \strega\ seguì la mia mano, inerte.
</p><p>
A quel punto, sommersa dal dolore, la \strega\ gridò.
</p><p>
Gridò come non avrei mai creduto possibile da un corpo così piccolo.
</p><p>
Dev'essere una cosa estremamente dolorosa, e quasi mi pentii d'averlo fatto.
</p><p>
Ripresasi, tentò di infilzarmi con l'altra mano. Afferrai anche quella.
</p><p>
La fissai negli occhi, mi ricordai chi fosse e dissi ``\camelia, per Dio,
fermati. Basta, ho vinto. Piegati''
</p><p>
Credevo ancora che ci fosse una ragazza, una ragazza dolce, sensibile e bella,
dentro la strega. Ma non c'era. C'era stata, forse.
</p><p>
Ma quella che avevo davanti era una ragazza con dentro una strega.
</p><p>
Non rispose, ma tentò invece di mordermi.
</p><p>
Tirai con l'altra mano.
</p><p>

</p></div><div class="section"><p>

</p><p>
Ci fu un secondo urlo, peggiore del precedente.
</p><p>
La lasciai andare, e stramazzò a terra.
</p><p>
Non stava perdendo sangue, ma una brodaglia nera, melmosa e poco invitante.
</p><p>
``Avrai anche vinto, \spontaneo'' disse con un filo di voce, la voce normale, la
voce di \camelia ``ma non credere di aver ottenuto qualcosa''
</p><p>
Sputò sangue, o qualunque cosa fosse quello che le scorreva in vena. Poi si
schiarì la gola, tossendo.
</p><p>
``Siamo in molte. Troppe perché tu possa sperare'' disse
</p><p>
``\camelia'' le chiesi ``Che farò''
</p><p>
Ero sinceramente in dubbio, in effetti. Battesimo e Smeraldino mi avevo
avvertito di quanto terribili le \streghe\ fossero, ma doverle ammazzare tutte,
lei specialmente, mi pareva eccessivo. Ero giovane. Ero al mio terzo combattimento.
</p><p>

</p></div><div class="section"><p>

</p><p>
Parlò ancora, con le ultime forze, disse ``Tu e noi siamo nemici, non c'è altro.
Tu e tutti quelli come te non potete esistere in questo mondo o nell'altro. Non
ve lo permetteremmo. Che io muoia adesso non conta, perché domani o il giorno
dopo qualcuna di noi ti troverà e la tua vita apparterrà a noi''
</p><p>
Per quanto mi paresse indifesa, mi lanciò un'occhiata terribile e i suoi capelli
saettarono verso la mia faccia, ma i suoi poteri erano allo stremo e fu troppo
lenta. Afferrai una ciocca per mano e la trattenni, ma quella ancora poteva
fissarmi e sorrise scoprendo i denti macchiati del suo sangue.
</p><p>
Mi ricordai di quello che aveva fatto, dopo i fulmini. Mi piovve addosso quella
fiamma blu e viola che aveva arato il bosco dentro il quale stavamo, mi piovve
in faccia da meno di mezzo metro.
</p><p>
L'unica idea che mi venne fu di infilarci la testa, tentando di chiuderle gli
occhi con un colpo.
</p><p>
Mentre la fiamma mi mordeva la carne, mi divorava i capelli e distruggeva il mio
bel cappello, spinsi rabbiosamente per avvicinarmi, con tutto quello che mi
restava. Violetto, violetto, violetto.
</p><p>
Le piantai la fronte in mezzo agli occhi, qualcosa si ruppe.
</p><p>
Non fu niente di mio, e la fiamma si estinse.
</p><p>
Urlò per la terza volta, e fu la peggiore.
</p><p>
Dovetti arretrare tappandomi le orecchie.
</p><p>

</p></div><div class="section"><p>

</p><p>
Quando fui in grado di riaprire gli occhi, lei stava in una pozza di melma, un
misto di fango e sangue.
</p><p>
Era stramazzata a terra con la testa rotta.
</p><p>
Mi riposai un attimino, tirando il fiato, ma senza abbassare del tutto le mie
difese.
</p><p>
``E' morta?'' chiesi ai miei due seguaci.
</p><p>
Mi dissero che mai avevano veduto un cosa simile. Mai nessuno aveva messo una
\strega\ in difficoltà, neanche lontanamente. Nessuno credeva neppure che le
\streghe\ fossero mortali.
</p><p>
Uno dei suoi flagelli si mosse.
</p><p>
Emise un lamento.
</p><p>
Mi prese un colpo al cuore pensare che fosse viva, nonostante quelle condizioni.
Provai addirittura pietà per il dolore che stava probabilmente provando in quel
momento.
</p><p>
E nonostante la testa rotta e due braccia strappate, quella si riprese,
rialzandosi come un serpente. Era una maschera di sangue, appena capace di
reggersi, ma non aveva perso un goccio dell'odio che provava nei miei confronti.
</p><p>

</p></div><div class="section"><p>

</p><p>
``Allora, che vuoi che faccia?'' le chiesi, dopo aver sputato a terra un grumo
di sangue che avevo in bocca.
</p><p>
Quella sibilò ``Questa non è una rimpatriata, \spontaneo! E' un duello. Soltanto
uno può andarsene. I due terminano con la morte di uno dei contendenti. O di
entrambi''
</p><p>
Furono le sue ultime parole.
</p><p>

</p></div><div class="section"><p>

</p><p>
Si trascinò verso di me un'ultima volta. Camminando, perché non aveva più la
forza di fluttuare.
</p><p>
Con la bocca spalancata, mostrando un inusuale numero di denti, spezzati e
insanguinati.
</p><p>
Ripensai agli anni precedenti.
</p><p>

</p></div><div class="section"><p>...</p></div><div class="section"><p>

</p><p>
La rividi, la prima volta, al parco, davanti al palco del concerto.
</p><p>
Come avrei potuto immaginare che quattro anni più tardi ci saremmo azzuffati
fino alla morte?
</p><p>
La rividi al campeggio, quand'ero appoggiato a terra, sull'erba, dopo aver
trascinato il generatore al suo posto; lei arrivò con il suo borsone, e mi
sorrise.
</p><p>
La rividi la prima sera, quella sera della panchina. Il primo contatto. Non
avevo smesso di sorridermi.
</p><p>
La rividi quel giorno nel bosco, nella sua magnificenza.
</p><p>
La rividi la sera del licantropo, quando le nostre labbra era state così vicine.
</p><p>
La rividi ogni altro giorno di quel campeggio, la rividi in stazione a prendere
il treno che la portò lontano, la rividi a scuola, la rividi a piedi, la rividi
in bicicletta, la rividi quella sera, a casa sua.
</p><p>
Sentii l'eco della sua voce, quando mi pose quella domanda.
</p><p>
``Ti va di baciarmi?''
</p><p>

</p></div><div class="section"><p>...</p></div><div class="section"><p>

</p><p>
Presi la sua testolina tra le mie mani.
</p><p>
Non c'era nulla nei suoi occhi. Erano vuoti.
</p><p>
Aspettai per un attimo infinito.
</p><p>
Che dicesse qualcosa, che cambiasse idea, che mi desse un segno.
</p><p>
Non parlò, ma tentò ancora di azzannarmi la gola.
</p><p>
Con i denti, con la lingua, tentò di raggiungermi.
</p><p>
La fissai un'ultima volta, lei fissava me.
</p><p>
Con le sue ultime forze, mi stava odiando.
</p><p>
Tentò di parlare, ma non ne fu in grado. Udii il suo pensiero, pensava ``Muori,
\spontaneo''
</p><p>
Tirai forte.
</p><p>

</p></div><div class="section"><p>

</p><p>
Il collo si spezzò, con lo stesso suono secco che avevano fatto le spalle.
Soltanto, risuonò nelle mie orecchie molto più a lungo.
</p><p>
Poi, silenzio. Non urlò più.
</p><p>
``Ne resterà soltanto uno.''
</p><p>

</p></div><div class="section"><p>

</p><p>
Una luce scese dall'alto e non vidi più nulla.
</p><p>
