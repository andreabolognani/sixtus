	Il NaNoWriMo è una cosa strana, difficile, da provare.
	sec#
	title#Di come ne venni a conoscenza
	Era quel giorno il 3 novembre 2010.

	Me ne andavo con alcuni compagni (solo allegri) a giocare di
	ruolo, un pomeriggio.

	Sul finir del gioco, uno di quei compagni mi nominò di dover
	tornare a casa presto per scrivere.

	Mi feci raccontare dell'esistenza di una sorta di
	internazionale sfida con se stessi, gratuitamente
	partecipabile iscrivendosi alla comunità. Il suo scopo è
	realizzare una prima draft completamente originale,
	composta di almeno 50000 parole.
	sec#
	Decisi di partecipare nel momento in cui ne venni a
	conoscenza. Tornando verso casa, quella sera stessa,
	buttai giù la traccia per la mia storia e cominciai a
	scrivere.
	title#NaNoWriMo 2010
	Vinsi, con quasi 10 giorni d'anticipo e pur avendo
	cominciato in ritardo. Ne uscì qualcosa di completamente imprevisto, la storia
	pubblicata come NaNoWriMo 2010, disponibile per la
	visione in pdf, intera o in capitoli separati.
	sec#
	Ma la prima draft è ovviamente piena di buchi, di cose
	terribili e di strafalcioni. Anche la storia funziona
	poco. E nel caso non lo sapeste ancora, il finale è
	mancante (avevo toccato le 51 mila parole, ero stanco!)
	title#Corvino Multicolore
	Quindi, con un titolo semidefinitivo e qualche
	correzione in mente, eccomi al lavoro su una versione
	migliore, meglio scritta e magari con più cognizione di
	causa (ora che conosco i risvolti della storia). Non
	offro alcuna garanzia sul risultato, né sui tempi di
	realizzazione.
