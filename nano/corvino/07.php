<h2>La lettura</h2>
<div class="intro"><p>
La mattina successiva mi svegliai fresco come una rosa.
</p></div><p>
Alle cinque del mattino.
</p><p>
Per prima cosa mi chiesi come mai nessuno in casa fosse già sveglio: per antica
tradizione, mio padre si svegliava per primo tutti i giorni lavorativi. Quando
al mio risveglio lui non era già in piedi poteva significare soltanto che fosse
malato, oppure che fosse un sabato o una domenica.
</p><p>
Ero abbastanza sicuro che fosse giovedì.
</p><p>
Presi l'orologio e controllai l'ora, scoprendo di essere in largo anticipo sulla
sveglia, che sarebbe dovuta suonare oltre due ore dopo. A quei tempi infatti
avevo cominciato a mancare in termini di ore di sonno: m'ero quindi abituato a
dormire il più a lungo possibile, partendo tardi al limite per giungere in
orario alla prima lezione. Il ché significava che a quei tempi la mia svelia
suonava assieme alle campane alle 07:45, il ché dava a me esattamente sette
minuti per uscire dal letto, vestirmi, lavarmi, prepararmi al volo, mangiare o
bere ciò ch'era avanzato sul tavolo della colazione, urlare i miei saluti al
parentame (che di solito si trovava nelle proprie stanze, oltre numerosi muri) e
poi fuggire fuori di casa. Recuperata la bicicletta dal parcheggio dietro casa,
mi rimanevano un totale di otto minuti per piombare entro il dannato cancello
della scuola, quello che poi alle 08:00 si chiude.
</p><p>
Quindi via come il vento con la mia bicicletta, con qualunque tempo, con
qualunque temperatura. Una volta dentro il cancello, un ritardo comprese entro
cinque minuti era assolutamente tollerabile, potevo quindi parcheggiare con
comodo, chiudere il lucchetto, controllare la catena, i freni, dare un'occhiata
al panorama, per poi dirigermi all'entrata principale. Per un qualche motivo,
davanti all'edificio scolastico c'è un enorme prato, che poi venne
ridimensionato per far posto ad un campo da pallavolo all'aperto. Ma secondo
qualcuno che evidentemente doveva essere importante, non poteva assolutamente
essere usato come parcheggio; men che meno si poteva usare quell'altro cancello,
che sarebbe stato così comodo ma avrebbe permesso alla gente di pestare l'erba,
crimine condannato da tutti i popoli civilizzati.
</p><p>
Così, dovendo parcheggiare dall'altro lato del mondo rispetto alle scale degli
studenti (già, perché i docenti avevano una loro entrata riservata,
espressamente vietata a quelli come noi) avevo il mio bel da fare e rischiavo di
consumare una fetta troppo importante di quei cinque minuti di ammissibile
ritardo. In pratica, dalla sveglia all'entrata in classe per l'inizio della
lezione passavo un quarto d'ora frenetico, tanto frenetico che nemmeno di
permettava di svegliarmi: non appena seduto al banco, infatti, il sonno
s'impossessava di tutte le mie membra senza che io potessi farci nulla.
Fortunatamente il mio banco era stato accuratamente posto in seconda fila,
dietro la mia compagnia Giulia: gran bel pezzo di ragazza, che oltre ad essere
piuttosto ben carrozzata e di buona famiglia (ma anche spocchiosa e
insopportabile, sconsiglio a chiunque di parlarci) era alta un metro e ottanta
ed aveva le spalle di un nuotatore; il ché rendeva molto facile a me
nascondermi dietro la sua bella e imponente sagoma, potendo dormirmi tutta la
prima ora e magari anche la seconda.
</p><p>
Ma tornando al punto, quella mattina mi sveglia fresco. A memoria, feci
sincermante fatica a ricordare un'occasione precedente nella quale mi fossi
svegliato riposato, spontaneamente, in un giorno di lezione, prima della
sveglia. E non cinque o sette minuti prima, ma quasi tre ore prima. E dopo
soltanto cinque ore scarse di sonno.
</p><p>
Mi resi conto di due cose, in quei momenti: primo, avevo dormito bene, ma
veramente bene, per la prima volta in almeno un paio d'anni; secondo, avevo
davanti a me almeno due ore di silenzio. Che avrei fatto quelle due ore? Non ero
affatto preparato a quella situazione; per un po' mi rigirai nel letto, tentando
di riaddormentarmi, cercando una posizione sonnifera, ma non ci fu verso. Avevo
effettivamente dormito tutto il necessario e non avevo né il bisogno né la
voglia interiore di dormire. Di fronte all'evidenza, decisi di alzarmi.
</p><p>
Che avrei potuto fare, mantenendo un silenzio tale da non svegliare i miei
genitori che ancora dormivano, per due ore intere? Dalle 07:00 in poi, o forse
anche prima, avrei potuto semplicemente spiegare che avevo dormito bene, che
m'ero svegliato di mia sponte e avrei anche potuto guardarmi un po' di
televisione, ma prima di quell'ora avrei dovuto inventare una bella scusa per
essere in piedi. E per raccontare una balla abbastanza grossa mi sarebbe stato
utile conoscere la verità, e non avevo un singolo indizio.
</p><p>
Ma spiegazioni non richieste a parte, due ore di noia m'attendevano. Non potendo
accendere il televisione, non potendo ascoltare la radio, non potendo andarmene
a spasso... o forse sì? Perché non utilizzare quella finestra che mi era stata
chiusa in faccia la sera precedente e uscirmene fuori, andare un po' a spasso e
respirare un po' d'aria pura. Perché era novembre e fuori la temperatura era
abbastanza proibitiva; non che soffrissi particolarmente il freddo, ma non mi
sarei sentito molto furbo ad uscirmene senza giacca, senza scarpe. Non quel
giorno, almeno. Se avessi avuto a portata tutto l'abbigliamento per uscire di
casa, senza fare rumore, c'avrei ripensato. Decisi di prepararmi per
quell'eventualità. Nel frattempo, però, avevo ancora un'ora e tre quarti di
nulla da riempire.
</p><p>
Tutto ciò che avevo a disposizione erano libri. Non avrei potuto accendere il
computer: nonostante fosse chiuso nella mia stanza, la sua bella ventolona era
abbastanza polverosa e vibrante da causare interferenze sui sismografi di mezza
nazione. Sarebbe stato rischioso metterlo in funzione. Libri, dunque. 
</p><p>
E fu così che mi misi a studiare.
</p><p>
Presi in mano il primo libro che mi capitò. Era di latino. Niente latino, quel
giorno, grazie a Dio: prossimo libro. Era di storia. Già meglio, ci sarebbero
state due inutili ore di storia con l'inutile professore di filosofia.
Dov'eravamo? Quel gran casino ch'è la WWII: nome breve, elenco infinito di
eventi. Eventi principali, eventi secondari, ma soprattutto analisi delle
meccaniche sociali, politiche, militari (ma anche tutte le possibili
combinazioni con il trattino, come socio-politiche, bellico-militari,
socio-psico-bellico-poli-proto-anti-liberal-insurrezional-tecno-culinarie).
</p><p>
Gli storici pongono l'inizio della guerra alle 04:45 del 1 settembre 1939,
quando la Luftwaffe sferra un primo attacco su numerosi obiettivi sensibili
polacchi; entro le otto di mattina sia la marina che la fanteria tedesche
portano i propri attacchi. Nessun formale atto di dichiarazione di guerra è
ancora pervenuto alla Polonia. Nell'arco della stessa giornata, il regno unito
ordina la mobilitazione delle proprie truppe, si prepara all'evacuazione in caso
di bombardamenti, mentre un nutrito gruppo di nazioni circostanti si dichiara
neutrale al conflitto.
</p><p>
Il giorno seguente i governi britannico e francese inviano un ultimatum alla
Germania, intimando di cessare ogni attività ostile; il duce si dichiara
neutrale, la Gran Bretagna emette un ordine generale per la leva obbligatoria di
tutti i maschi tra i 18 e i 41 anni; Danzica viene annessa alla Germania.
</p><p>
Questo e molta altra roba, perché il libro non si limita affatto alla storia, ma
si perde in una varietà di commenti, analisi laterali, analisi globali, punti di
vista, diagrammi di flusso; il tutto per tentare di spiegare perché le cose
fossero accadute, invece che limitarsi al lavoro dello storico, che come disse
il professor Jones non consiste nella ricerca nella verità ma nella ricerca dei
fatti.
</p><p>
Quel libro conteneva molte più opinioni che fatti; mentre passavo di pagina in
pagina, di capitolo in capitolo, mi fu lampante quanto questo libro fosse
somigliante, nell'intento, al filosofico professore; entrambi avevano la
fastidiosa tendenza a sottolineare quanto ne sapessero più di noi, a spiegare
l'origine delle parole evidenziando quanto il vero significato fosse diverso da
ciò che noi comuni mortali intendevamo dire con quella parola; ed entrambi
tendevano a dimenticare quanto riflettere sul perché o per come questo o quello
fosse distante dal vero obiettivo della lezione di storia. Poi, non appena
qualcuno con del sale in zucca accennava appena a puntigliare su questa verità,
ecco partire l'invettiva del professore, perché fondamentalmente ``chi non
studia o non ama la filosofia non ama il pensiero, la riflessione, non si pone o
non si sofferma sulle domande giuste'' e fondamentalmente, parafrasando il
resto, non combina un cazzo e non arriva da nessuna parte. Tutti i professori
fanno questo discorso: senza il loro corso di studi non si arriva. Mai.
</p><p>
Quando giunsi alla fine del libro, lo gettai di lato e persi alcuni minuti a
bestemmiare con il professore, il libro, il suo autore e tutta quella manica di
persone che passa la sua vita a farsi le seghe su argomenti come questo e prende
pure dei soldi (e non pochi, immagino) soltanto per produrre ore e ore di
sfinimenti e rotture di palle a poveri studenti come il sottoscritto.
</p><p>
Poi mi resi conto di essere giunto in fondo al libro, pur essendo partito dal
capitolo IV. Ripresi il libro in mano, controllai le pagine: partendo dalla
numero 73 ero giunto fino all'ultima con del testo rilevante (togliendo
l'indice, le note di produzione, le fonti delle immagini), ch'era la 213. Erano
140 pagine. Guardai l'orologio, ed erano le sei e dieci. Avevo veramente letto
due terzi di libro in poco meno di un'ora? Impossibile.
</p><p>
Sfogliai i contenuti dei capitoli che avevo attraversato, rendendomi conto che
ricordavo quasi alla perfezione ogni immagine, ogni passaggio, ogni data.
Volendo essere sicuro di quello che stava accadendo, feci alcune prove: cercando
un evento, una foto, una data a caso, o anche un nome, riuscivo ad associarvi
immediatamente, senza particolare sforzo, la pagina corretta.
</p><p>
Notevole. Decisi di provare con il libro per le prime due ore: fisica.
</p><p>
Questa volta le pagine che avrei dovuto leggere per completare il libro erano
soltanto un'ottantina (volumi corti). L'esperienza fu parecchio dissimile dalla
precedente, ma non meno fastidiosa.
</p><p>
Ahimé, anche il libro di fisica aveva (anche se in numero estremamente minore)
degli specchietti il cui scopo era contestualizzare storicamente il modo in cui
un tale modello era stato teorizzato, chi aveva scoperto il fenomeno, in quale
anno, chi altri avevo effettuato la stessa scoperta ma al tempo non era noto,
cose del genere. Spesso poco interessanti, ma almeno brevi. Il resto erano
formule, contestualizzazione del fenomeno, associazioni a fatti osservabili
nella vita quotidiana.
</p><p>
In una mezz'ora giunsi in fondo al libro. Aprendone una pagina a caso, leggendo
una parola a caso, potevo effettivamente descrivere l'esempio, la formula
analitica, e quando c'era anche il nome del fisico che per primo s'era
preoccupato di analizzare il fenomeno.
</p><p>
In quella, suonò la sveglia.
</p><p>
E fu così che per la prima volta nella mia vita (o almeno la prima volta in
molti, molti anni) feci colazione in orario, senza correre, seduto al tavolo
della cucina con i miei genitori.
</p><p>
Nulla di particolarmente interessante su questo, però. Mio padre non parla
durante la colazione, mia madre nemmeno. Fu un pasto silenzioso, quasi
spettrale. Ma in fondo ho sempre apprezzato il silenzio. Una buona pacifica
colazione dopo, me ne andai.
</p><p>
Mi misi lo zaino in spalla, inforcai la mia biciclettina, e con cinque minuti di
vantaggio mi diressi a scuola. Ciclando, ciclando, pensavo a quante pagine avevo
lette quella mattina. E se in quel momento già conoscevo a memoria due volumi,
se avessi studiato un giorno intero probabilmente avrei potuto terminare il
programma delle superiori. Decisi di provarci quel pomeriggio.
</p><p>
Intanto arrivai a scuola, parcheggiai la bicicletta nel solito posto, fissai il
lucchetto, fissai le montagne per un minuto intero (non avevo alcuna fretta) e
poi me ne andai su in classe.
</p><p>
Per la prima volta quell'anno, e forse per la prima volta in tutte le mie
mattina da studente delle superiori, non ebbi alcun bisogno di nascondermi
dietro Giulia per dormicchiare. Non ebbi neanche problemi a scavalcarla, a
sporgermi di qua e di là per tentare di leggere alla lavagna. In barba ai miei
compagni, alla professoressa di fisica le prime due ore e al professore di
filosofia le due ore successive, seguii la lezione leggendo i nomi di ogni paese
sulla carta geografica dell'Italia, ch'era appesa alla parete opposta. Ad ogni
domanda posta da uno dei due professori, in ogni occasione, avrei potuto
nominare autore, legge, data e pagina corrispondente sul testo. Ad ogni domanda.
</p><p>
Evitai comunque di rispondere spontaneamente, e soltanto una volta l'inutile
chiaccherone tentò di cogliermi in fallo (come spesso faceva nelle sue lezioni
di filosofia, in quelle di storia meno spesso) chiedendomi chi fosse stato
comandante generale delle armate italiane durante la seconda guerra mondiale.
Gli risposi che fu Ugo Cavallero, nato a Casal Monferrato il 20 settembre 1880,
morto suicida con un colpo di pistola alla testa 13 settembre del '43; anche se
non è ben chiaro se il suicidio fosse stato spontaneo oppure imposto dal nemico,
né se il nemico fosse tedesco o qualcuno fedele a Badoglio. Questo impedì al
professore di ribattere e di pormi altre domande per tutta la lezione.
</p><p>
E siccome era venerdì, dopo la quarta ora, me ne andai a casa. E come avevo
deciso di fare quella stessa mattina, presi ogni libro di testo a mia
disposizione, cominciando da quelli del quinto anno. Si, anche quello di storia
dell'arte, il più inutile libro che potessero farmi acquistare. Non che provi
nulla di particolarmente ostile nei confronti dell'arte o degli artisti (beh,
veramente si, ma non è questo il punto), ma per qual motivo si suppone che io
debba conoscere gli stili architettonici dei romani, gli stili pittorici del
rinascimento, io ch'ero iscritto ad un liceo scientifico. Uno in cui si studiano
le scienze; ``Eh, dai, in fondo tutti i più grandi scienziati dal quindicesimo
secolo in poi si possono considerare artisti'' per svariati motivi, dicevano i
miei insegnanti.
</p><p>
Misi i libri in una pila, una pila enorme, schifosamente pesante, e comincia dal
libro blu di letteratura: era il Paradiso, terzo volume della Divina Commedia in
tre volumi piccoli piccoli; infatti, avevo messo i libri in ordine di grandezza,
anche per lasciare quello di storia dell'arte in fondo (lui e le sue enormi
fotografie occupavano pagine in un formato quasi quadrato, parecchio scomodo per
lo zaino e per le braccia). E Paradiso fu.
</p><p>
M'accorsi che in fondo i canti di Dante non sono poi tanto lunghi. La dimensione
del volume è dovuta occasionalmente alle stampe a tutta pagina e per la maggior
parte ai terribili commenti e approfondimenti sui contenti. Già, perché dato che
dato incontra sui contemporanei anche in paradiso e ci fa dei grandi discorsi, è
importante conoscere i suoi tempi e la gente di quei tempi. E quindi, via!
Commenti, approfondimenti, riferimenti oscuri a gente oscura. Ma almeno le
pagine erano brevi, potevo quasi leggerle di un botto, tutte intere. E ci
riuscivo: potevo effettivamente fissare tutta una facciata, due pagine aperte,
per un secondo con gli occhi sgranati, per afferrare tutto il contenuto. Provai
effettivamente ad osservare il libro aperto, poi chiudere gli occhi e verificare
se le singole righe corrispondessero.
</p><p>
Potevo fare anche quello. Passa circa tre altre ore, sfogliando pagina per
pagina un plico di libri che comprendeva l'intero quinto anno delle superiori. E
seppi tutto. Tutto tutto, ogni virgola.
</p><p>
Poi però arrivai all'ultimo, il libro di storia dell'arte. Quello era più
pesante: nutrivo un così basso interesse nei suoi confronti, anzi, forse era
addirittura un interesse negativo; poggiai la mano sul libro e desiderai
fortemente di non doverlo aprire, di poter passare oltre senza la sofferenza di
aprirlo e sfogliarlo, anche se avessi dovuto soltanto dare un'occhiata singola
alle pagine. Feci questo con il libro in mano; e mi venne il mal di testa.
Abbastanza intenso in effetti, tanto che lasciai cadere il libro a terra per
afferrarmi le tempie. Mi sfregai gli occhi per qualche secondo, perché aprendoli
vidi immagini, scritte, stampe muoversi nella stanza.
</p><p>
Mi venne il dubbio che avesse funzionato. Scelsi una pagina di quel libro mai
aperto (beh, aperto sì, studiato no, non dopo le mie recenti scoperte, almeno) e
vidi chiaramamente comparirmi in testa la raffigurazione di quella pagina, con
tutto il testo e la fotografia. Parlava del Perugino, il `divin pittore', vero
nome Pietro di Cristoforo Vannucci, nato a Città della Pieve attorno al 1450 (la
vera data è sconosciuta, secondo il testo) e morto a Fontignano nel 1523.
L'immagine era una fotografia dello `Sposalizio della Vergine', datato
1501-1504. Non tanto divino, per i miei gusti. Ma quel che conta è che ci presi,
al primo colpo, su un libro non aperto.
</p><p>
Decisi di riprovare, questa volta con qualcos'altro, un vecchio libro che non
avrei potuto ricordare neanche se avessi voluto. Cercai nello scaffale dei libri
dei precedenti anni scolastici, vidi spiccare `I Promessi Sposi' e scelsi
quello. Lo preso in mano e cercai uno dei pochi passaggi che ricordavo,
l'Innominato incontra il cardinale Federigo. Vengo preso nuovamente da una fitta
alla testa, come quando senti stridere i freni, come quando qualcuno strofina il
gesso sulla lavagna, come quando mentre cuci ti sfugge l'ago sotto l'unghia, un
dolore lancinante che però lascia pochi danni. Ripresomi da quella botta, avevo
chiaro in mente capitolo e pagina. Aprii il volume, cercai la pagina corretta, e
lì effettivamente lessi, a colpo sicuro, dell'incontro tra i due.
</p><p>
E per quanto intenso fosse il dolore di quella lettura istantanea, decisi di
fare un'ultimo tentativo. Presi dallo scaffale la copia di `Robinson Crusoe',
che avevo dovuto leggere nell'estate tra la prima e la seconda superiore, e di
cui non ricordavo nulla di nulla, se non il nome dei due personaggi, Robinson e
Venerdì. Scelsi quello non solo perché era stato una lettura forzata, bensì
perché si trattava dell'unico volume in lingua inglese che avevo a portata: era
stato un compito d'inglese, infatti, e la cosa mi era risultata estremamente
sgradita, al tempo. Lo presi e lasciai che mi tornasse il dolore tra le
orecchie. Non fu né meglio né peggio che con i volumi provati precedentemente.
Ma funzionò ugualmente, potevo vedere esattamente, nella mia testa, il momento
dell'incontro tra il protagonista inglese e il suo amico selvaggio. Non che
capissi bene tutto (ai tempi le lingue non erano il mio forte) ma potevo
associare parole a pagine, bene o male.
</p><p>
E come Neo, rialzandosi dalla poltrona a bordo della Nebuchadnezzar, dissi:
``Conosco il kung fu''.
</p><p>
