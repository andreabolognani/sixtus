<h2>Il lupo e il corvo</h2>

<div class="intro"><p>
Rimasi a casa, a pensare.
</p></div>

<p>
Quando mancarono quindici minuti a mezzogiorno, uscii dalla finestra, volai fino
alla Mariglia, la cima con le antenne delle radio dove passavo tanto del mio
tempo a guardare il panorama.
</p><p>
Giunsi con un minuto di anticipo. Lui invece si presentò in orario.
</p><p>
Era mezzogiorno di una splendente giornata. A tutti gli effetti, avevo di fronte
un lupo, grosso come un cavallo, con due enormi occhi verdi e una terribile fila
di denti.
</p><p>
&laquo;E' forse perché ho ucciso?&raquo; chiesi.
</p><p>
&laquo;Giammai, noi non puniamo per questo&raquo; disse.
</p><p>
&laquo;Per cosa punite, allora?&raquo; chiesi ancora.
</p><p>
&laquo;Non è per punire che sono qui. E' per battezzare. Ucciderai ancora, e molto&raquo;
</p><p>
&laquo;Davvero?&raquo;
</p><p>
&laquo;Forse. Forse invece morirai&raquo; sentenziò il lupo. E scattò in avanti, a fauci
spalancate, per sbranarmi.
</p><p>
Non ero dell'umore adatto per lottare contro un cane rabbioso grosso quanto un
orso. Non ero dell'umore adatto per sentirmi dire che avrei ammazzato ancora. Ma
nemmeno mi sentivo colpevole: non avevo affatto desiderato la morte di quel
tizio. O forse sì?
</p><p>
In quel momento, mi convinsi che, in fondo, avevo fatto troppo poco; quel tizio
sarebbe peggiorato e morto anche da solo, se soltanto guardandolo era morto.
Oppure, per qualche ragione, ero tanto forte da non dovermi affatto impegnare
per togliere una vita. Che fosse predestinato ad andarsene o che fosse soltanto
una mosca volta ad infastidirmi, non mi curai più del suo fato.
</p><p>
Tornai a concentrarmi sulle fauci del lupo, che si facevano vicine vicine alla
mia faccia. E fu così che scoprii che in fondo anche i lupi grossi come cavalli
puzzano di cane, l'alito in particolare. Ancora una volta mi sentii leggero,
scarico di ogni peso di coscenza, e salì la nebbia tutto attorno a noi.
</p><p>
Feci un passo a lato, scansando appena la sagoma della bestia, che mi mancò e
atterrò molti metri oltre. Voltandosì, mostrò un ghigno che credetti non
nascondesse un certo gusto per la caccia, ma credo anche che non gli piacque
vedere la sicurezza nei miei occhi, perché subito caricò nuovamente.
</p><p>
Questa volta mi buttai a terra, lasciando che mi passasse sopra. Era veloce,
certo, ma io lo ero di più. Avrei potuto anche andarmene, forse, ma non lo feci.
Quegli occhi verdi mi infastidivano, così come quella profezia sulle future
numerose uccisioni.
</p><p>
&laquo;Dimmi, mostro, perché dovrei uccidere ancora?&raquo; urlai.
</p><p>
&laquo;Capirai questo e altro, forse, dopo che uno di noi sarà morto&raquo; replicò
quello, caricando per la terza volta.
</p><p>
E per la terza volta, lo scansai facilmente &laquo;Dovrai fare ben di meglio, se
vorrai anche solo toccarmi, cucciolone&raquo;
</p><p>
&laquo;Sì, infatti. E' vero, dunque, quello che dicevano: sei potente. Ma sei anche
sbadato e strafottente. Dimmi, credi che ci sia nebbia?&raquo;
</p><p>
Non potei esimermi dall'inarcare le sopracciglia, persi la concentrazione e vidi
la nebbia svanire. Non era nebbia. Era quell'effetto bianchiccio che avevo
provato sulla pista ciclabile, quando per la prima volta avevo corso come un
treno.
</p><p>
&laquo;Lo sbarbatello torna moro non appena gli si fa notare l'evidenza. Vediamo
dunque quanto tieni botta, contro uno parimenti bianco&raquo; e dicendo questo,
cominciò a schiarire, passando dal nero all'argento, fino a sbiancare del tutto.
</p><p>
&laquo;Non sei l'unico&raquo; disse. E scattò.
</p><p>
Questa volta mi prese completamnte di sorpresa: me lo ritrovai in faccia,
buttato a terra prima ancora di rendermi conto di cadere. Tenendomi schiacciato
a terra con due zampe sopra le braccia, si fermò, mi sbavò addosso e ridacchiò
&laquo;Saprai di pollo, anche tu come gli altri?&raquo;
</p><p>
Alle sue parole non diedi alcuna importanza (almeno in quel momento), ma la
sbavata invece mi diede fastidio. Molto fastidio.
</p><p>
Se potevo spegnere un lampione col pensiero, se potevo fermare il cuore di un
tizio a caso col pensiero, allora che cosa avrei fatto ad una grossa bestia
bavosa, incazzosa e puzzolente? Decisi di scoprirlo, puntandomi sulle mani per
avvicinarmi alle fauci, fissando quella gola che stava per diventare il mio
nuovo capello.
</p><p>
La presa sulle braccia si alleggerì, il lupo fu spinto indietro e potei alzarmi.
Continuavo a fissarlo, lui e la sua bocca spalancata. Grattava per terra
tentando di restare fermo, allora provai a puntare in alto e lo sollevai ad un
paio di metri da terra.
</p><p>
Dopo qualche zampata all'aria, decise di smettere di provare, e ridacchiò come
aveva fatto prima.
</p><p>
Gli chiesi che ci trovasse di divertente, rispose &laquo;Non credevo che fossi
multicolore, ragazzo. Forse avevano ragione&raquo;
</p><p>
&laquo;Allora ti basta? Mi dirai chi sei, chi ti manda e che volete tu e i tuoi da
me?&raquo;
</p><p>
Quello rise fragorosamente &laquo;Che credi? Che sia un messaggero? Io sono un
assassino: ho portato la morte a quelli come te per centinaia di anni, e se
credi che infrangerò un voto solo perché non tocco col piede a terra, allora sei
uno stolto e morirai presto&raquo;
</p><p>
Stufo com'ero di sentire quei discorsi, strinsi più forte la morsa con la quale
trattenevo il corpo peloso della bestia, e gli sbraitai contro senza remore
&laquo;Non capisco un cazzo di quello che dici, mostro! Parla chiaro oppure taci e
muori! Adesso, dimmi: chi e cosa sei?&raquo;
</p><p>
Non rispose, ma ci pensò. E io lo seppi.
</p><p>
Si chiamava effettivamente \battesimo. Sesto cucciolo di nove fratelli, unico
rimasto, fiero assassino da qualcosa come quattrocento anni. Questo riuscii a
sentire nella sua testa prima che urlasse &laquo;Dannata profezia! Tu leggi la
mente?!&raquo;
</p><p>
Poi si liberò. Non so bene come, forse fui io a distrarmi, forse fu lui a
pensare qualcosa che mi portò distante dal combattimento, ma persi la presa e
tutta la sua molò cadde a terra.
</p><p>
Era ancora bianco, e come imparai questo significava una carica quasi
istantanea. Ma essere veloci non serve, quando si va contro un muro. Ero
fermamente deciso ad ottenere delle risposte, e mi chiesi allora che potesse
fare il pugno di un uomo la cui mente solleva i lupi.
</p><p>
Beh, quei pugni fermano i lupi. Riversai tutto il mio senso d'inadeguatezza per
l'ignoranza e l'inquietudine che le parole del lupo di fecero provare, le
riversai nel pugno sinistro e sperai con tutto me stesso che arrivasse in faccia
alla belva prima che la faccia della belva arrivasse a me. Ma in effetti non lo
sperai: lo volli. E così andò.
</p><p>
Presi il lupo nella mandibola, pochi centimetri sotto l'occhio sinistro, con un
cazzotto fumante d'odio. Ed era effettivamente fumante. Il lupo deviò seccamente
dalla sua traiettoria, andò lungo e finì un paio di metri a destra, stramazzando
per terra. E tornò del suo colore naturale. Beh, forse non naturale, comunque
tornò nero come era all'inizio.
</p><p>
Fissai il mio pugno, ancora chiuso. Era viola. Era viola scuro, quasi
metallizzato, e fumava. Osservai il lupo, poi il pugno, poi il lupo, poi ancora
il pugno. Cercai di rilassarmi, lasciando andare il rancore per la sua strada, e
riuscii a riaprire la mano. Lentamente, tornò anche quella al suo colorito
naturale.
</p><p>
Feci un passo verso il lupo, tentando di ascoltare che fosse ancora presente.
Respirava, e pensava. Evidentemente, le sue divese, che aveva alzato nel
rendersi conto di cosa avrei potuto scoprire da lui, era calate.
</p><p>
Purtroppo, il bastardo non mi disse nient'altro di utile. Pensò soltanto alla
famiglia, a come sarebbe stato ricordato come il migliore della sua cucciolata,
che la sua vita non sarebbe potuta finire meglio di così. Infine però rise e
pensò che almeno io non avrei potuto ottenere nulla da lui. Rise, e se ne andò.
</p><p>
Nel senso che svanì. Non fece come il pensiero dell'autista del furgone, non si
ammutolì semplicemente: lui effettivamente se ne andò, si allontanò. Senza che
io potessi capire in che direzione. Ma non morì, se ne andò. Ebbi la netta
sensazione di aver perso.
</p><p>
Il corpo però rimase. Mi chiesi se avrei potuto farci qualcosa. Mi chiesi se non
fosse un inganno, una trappola. Mi chiesi che ci facessi io su una montagna, un
quarto d'ora dopo mezzogiorno, in piena estate, con un lupo morto lungo tre
metri.
</p><p>
Urlai forte.
</p><p>
Urlai ancora più forte.
</p><p>
Urlai per la terza volta.
</p><p>
Poi mi sedetti per terra, tenendomi alla larga dalla carcassa che avevo appena
conquistato.
</p><p>
Venne un corvo. E si posò sulla carcassa.
</p><p>
Ed io dissi &laquo;Sciò, quella preda è mia&raquo;
</p><p>
&laquo;E' un dono invece, stolto. Un dono raro&raquo; disse il corvo, fissando con i suoi
occhi verdastri.
</p><p>
Ero forse troppo stanco, forse troppo studito, e rimasi impalato.
</p><p>
&laquo;Ora sei battezzato. Mi incontrerai qui, domani a mezzogiorno&raquo; disse il
corvo.
</p><p>
E se ne volò via.
</p><p>
Ero decisamente troppo scosso per fare qualunque cosa. Rimasi fermo per almeno
un'ora.
</p><p>
Mi si addormentò il sedere.
</p><p>
Decisi di alzarmi.
</p><p>
Mi alzai, ma le gambe non avevano intenzione di funzionare, quindi volai fino a
sollevarmi di un metro o due, per lasciare che il sangue tornasse a circolare.
</p><p>
La carcassa del lupo era un dono? Così aveva detto il corvo.
</p><p>
`Così aveva detto il corvo' mi parve una frase assurda. E lo era. Ma il dubbio
restava. Decisi che avrei toccato il cadavere. Non appena mi fosse tornata la
sensibilità alle gambe.
</p><p>
Non appena poggai la mano sul pelo irsuto della belva, l'intera carcassa svanì,
sparendo nel nulla come se non fosse mai stata lì.
</p><p>
Così mi impalai una seconda volta.
</p><p>
C'era stato veramente? Forse stavo semplicemente collassando, dopo aver evitato
di mangiare per sei mesi, dopo aver letto una biblioteca, forse stavo
semplicemente cedendo.
</p><p>
E che significava essere `multicolore'? Perché l'aveva spaventato tanto? Perché
c'era un lupo parlante?
</p><p>
Agitavo le mani attorno a me, mentre facevo questi discorsi. Poi ne fui stufo,
stufo marcio, e cacciai via, con un gesto, tutti questi pensieri e dissi:
&laquo;Fanculo, ci dormirò sopra, proprio come ho sempre fatto&raquo;.
</p><p>
E mi stesi per terra.
</p><p>
E mi addormentai.
</p><p>
In sogno, rividi \battesimo, il suo arrivo sulla cima. Non era comparso, era
effettivamente arrivato; non ero stato abbastanza rapido per accorgermene, la
prima volta, ma adesso stavo prestando attenzione e lo vidi: se n'era uscito da
una specie di tenda trasparente, come se avesse scostato un drappo di cielo e
fosse arrivato da questa parte.
</p><p>
In sogno, rividi anche me stesso, e vidi che anch'io, proprio come vidi fare a
\battesimo, avevo perso il colore, all'inizio della battaglia. Non solo vedevo
bianchiccio, quindi, ma ero bianco; i capelli, gli occhi, anche una certa
luminosità a circondarmi.
</p><p>
In sogno, vidi anche il secondo cambio di colore: lui sbiancò lentamente,
diventando rapido quanto me e forse di più, io invece, quando volli
togliermelo di dosso, diventai scuro, nero con riflessi violacei. Pessima
immagine, parevo il cugino malvagio di Prince.
</p><p>
In quello stato, ero evidentemente più lento del \battesimo bianco; tuttavia, ero
immensamente più duro e più forte: quando lui partì alla carica per azzannarmi,
al quinto tentativo, arrivai per pelo a centrarlo con il mio pugno, ma quel
pugno gli frantumò il cranio come potreste strappare un filo d'erba.
</p><p>
Evidentemente, il viola metallizzato vince sul bianco evanescente. Forte.
</p><p>
Poi, ancora in sogno, vidi una cosa strana: stavo cercando la mente del mio
avversario e la sentii andarsene; questa volta la vidi con questi occhi: vidi
due di loro, uno morto per terra, uno arzillo e zampettante, che se ne andava
come era arrivato, scostando un pezzo di cielo e sparendoci dietro.
</p><p>
Poi venne il corvo, parlò come prima. Vidi come venne e come se ne andò, e fece
allo stesso modo. Quelle due creature, nere con gli occhi verdi, potevano
andarsene altrove attraversando una tenda. Questo mi infastidì. Seppur sapevo
dove il corvo sarebbe ricomparso, di lì a un giorno, sapere che lui e forse
altri come lui potevano fare questo mi lasciava in netto svantaggio.
</p><p>
Infine, il sogno mi mostrò un'ultima cosa: quando toccai il cadavere di
\battesimo, quello non svanì nel nulla come avrei immaginato, bensì divenne
polvere e mi si impregnò addosso.
</p><p>
Mi svegliai sdraiato su un fianco. E non riuscii a tirarmi in piedi. Mi
strascicai in ginocchio, restando carponi. Quasi come quando percepii i
pensieri, al mercato o in piazza, rischiai di venire sopraffatto dalla quantità
di stimoli sensoriali che mi riempivano la testa.
</p><p>
Questa volta non erano pensieri, erano veri rumori: sentivo lo scricchiolio dei
passi delle formiche, il ronzio delle api, sentivo il battito d'ali dolce e
gentile d'una farfalla, l'incedere pesante di uno scarabeo. E potevo anche
vederli, muoversi e sciamare in ogni direzione, a terra, sulle piante, in aria.
E potevo anche fiutarli, quasi tutti: odori di ogni genere, diversi in ogni
direzione. Beh, non proprio di ogni genere, veramente: era quasi tutti
sgradevoli, in realtà.
</p><p>
Schifato, per istinto, aprii la bocca e mostrai la lingua. Mi sembrò che
qualcosa non andasse. Lingua troppo sottile, troppi denti.
</p><p>
Quello, più l'incapacità di alzarmi in piedi, tutte quelle sensazioni
migliorate. Mi venne un dubbio.
</p><p>
Mossi la coda. Funzionò.
</p><p>
Tentai di esclamare il mio disappunto, ma non mi fu possibile. Galoppai per
tutta la piana, su in cima alla Ramiglia, per almeno un'ora, prima di trovare
una minuscolissima pozzanghera. Nel riflesso vidi una sagoma scura e distorta,
ch'era senza dubbio un lupo, o un cane, o comunque qualcosa di eccezionalmente
somigliante a \battesimo. Forse ero più piccolo.
</p><p>
Fatto sta che ero lupo e non avevo idea di come tornare normale.
</p><p>
E non avrei potuto nemmeno tornarmene a casa, in quelle condizioni. Così provai
un po' di tutto, corsi in giro, marcai il territorio, provai a volare, a
cambiare colore, a piegare gli insetti alla mia volontà, ma tutto fu inutile.
Ero soltanto un lupo normale. Pensante forse, ma per il resto, non avevo più i
miei incredibili sfavillanti poteri.
</p><p>
Forse l'acquisizione ancora era disponibile, ma non avevo libri per provare.
Forse la percezione del pensiero era ancora lì, ma non c'erano teste pensanti
nei paraggi. Strano, pensai. Era un giorno d'estate: possibile che non ci fosse
nessuno, lì attorno?.
</p><p>
Comunque, non ero del tutto privo di capacità: passai l'intera giornata a
spostarmi, a cercare. Non ebbi né fame né sete, né tantomeno mi sentii stanco o
spossato. E correre con quattro zampe non è poi male, sei giustificato nel fare
tutto con la testa, e la coda aiuta con l'equilibrio.
</p><p>
Tuttavia, alla lunga la giornata passò, venne notte ed io ero ancora in forma
canina. Di notte notai che tutte le mie percezioni calavano in modo
sostanzialmente inferiore a quand'ero umano, e potevo ancora vederci, sentirci e
odorarci quasi alla perfezione.
</p><p>
Non che ci fosse bisogno di far nulla, perché come ebbi modo di scoprire, la
notte in montagna non accade niente di interessante attorno ad un lupo: tutti se
ne stanno alla larga, anche più che di giorno. E così, snobbato da ogni creatura
montana, attesi fino al giorno dopo. Mi diedi alla botanica e all'entomologia,
passeggiando con calma osservai e catalogai ogni creature, ogni pianta, ogni
insettino, ogni fiore, persino i funghi. Che palle, erano a migliaia.
</p><p>
L'alba è una cosa magnifica, soprattutto se vista dalla montagna. Nessun'altra
luce a distrarre la vista, soltanto il sole che sorge. E sorse, e salì, e giunse
in alto. Ed io rimasi in attesa, senza sapere che aspettarmi.
</p><p>
D'un tratto, a mezzogiorno, un corvò passò per la tenda del cielo e scese
delicatamente a terra, di fronte a me.
</p><p>
&laquo;Io sono \smeraldino&raquo; si presentò &laquo;Non temere&raquo;
</p><p>
Lo fissai storto, temendo la sua prossima mossa.
</p><p>
&laquo;Non intendo farti del male. Vedo che sai caduto nella trappola. Bene. E'
passato quasi un intero giorno: non sarai più in grado di tornare indietro,
forse non servirà nemmeno ucciderti&raquo;
</p><p>
Poi fece una lunga pausa. Gli girai intorno, digrignando i denti con le orecchie
basse, cercando di intuire qualcosa, di afferrare chi fosse, cosa volesse.
Pareva effettivamente innocuo.
</p><p>
C'erano state cose fastidiose nella mia vita. Molte, molto fastidiose: essere
soprassati in bicicletta, dover lasciare i tuoi giocattoli alla tua cugina più
piccola, essere accusati ingiustamente d'aver fatto qualcosa che poi s'era
rotto, vedere certa gente eletta per il quarto mandato, doversi alzare
sull'autobus per lascia posto ad una vecchina che poi scende alla fermata
successiva, ma essere sfottuti da un corvo poteva stare in cima a quella lista.
</p><p>
&laquo;Sciai ke sciuccede a discturbare il can ke dorme?&raquo; dissi, sbavando e
digrignando i denti.
</p><p>
Mi scagliai verso il pennuto, tentando di azzannarlo, imitando i tentativi di
carica che avevo subito il giorno prima. Non ebbi un gran successo, contro un
avversario volante. Urlai per la vergogna del fallimento, o forse abbaiai.
</p><p>
Quello disse &laquo;Vuoi giocare? Sia, allora. Balliamo&raquo;
</p><p>
E tutto il cielo si rannuvolò all'istante. Soffiò il vento, venne il freddo, e
le nuvole si caricarono, e cominciò a piovere.
</p><p>
Nel fitto della pioggia accaddero tre cose: mi si bagnò tutto il pelo, che non è
una cosa elegante; persi di vista il corvo, anche con la vista canina
migliorata, e non riuscii né a sentirlo né a fiutarlo; l'umido e il freddo e il
buio e le urla del corvo e tutto quel casino fecero precipitare il mio umore, e
m'incazzai. M'incazzai duro e giurai che avrei ammazzato quel bastardo.
</p><p>
Gli sentti dire, da qualche parte &laquo;Attento! Questo è forte: se dovesse
pigliarti, puzzarei anche di più!&raquo;
</p><p>
E calò un fulmine.
</p><p>
Non so come, riuscii a schivarlo. Non di molto, ma non mi prese.
</p><p>
Avrei giurato di sentire uno `tsk` di disappunto, da qualche parte fra gli
alberi. Poi ne piovve un altro.
</p><p>
Questo non cadde molto vicino, per fortuna. O forse il corvo stava giocando con
me. Questa possibilità mi fece infuriare ulteriormente, mentre arrancavo nella
pioggia in una direzione a caso. Volgevo lo sguardo a destra, a sinistra, in
alto e in basso, cercando di scorgere qualche movimento nella pioggia.
</p><p>
Mi pareva impossibile che piovesse tanto, e che fosse così buio. Era ancora
mezzogiorno, eppure stava venendo giù di tutto e quello ancora ridacchiava, in
mezzo ai tuoni e al vento che sbatteva gli alberi come sacchi di sabbia per
pugili.
</p><p>
Poi il bastardo si fece avanti personalmente, pizzicandomi con le sue zampette
sulle spalle. Fastidio. Gracchiava nel buio, senza che potessi vederlo, troppo
distante per individuarlo, anche per balzare e tentare di afferrarlo.
</p><p>
Il grosso nuvolone sopra di noi fu nuovamente carico e minacciò di scagliare una
terza volta. Stavolta mi decisi a combattere e contrattaccare. Perché il fulmine
avrebbe dovuto preferire me che ero a terra, quando aveva alberi e un corvo più
a portata? Venne dunque il fulmine, e mentre fissavo lo sguardo in alto gli
parlai e dissi: &laquo;Fulmine, non mi piglierai&raquo;.
</p><p>
Il fulmine cadde un metro alla mia sinistra.
</p><p>
&laquo;Allora, pennuto! Scperi ancora di scibarti della mia carcascia?&raquo; urlai con la
mia voce canina al vento.
</p><p>
Il corvo mi sentì, torno alla carica per beccarmi ancora e questa volta lo vidi
arrivare. Si avvicinò da sinistra, da dietro, per colpirmi nuovamente alla
