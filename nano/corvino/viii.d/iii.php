<div class="section">
	<h2>
		Al parcheggio
	</h2><p>
		Dannato lampione.
	</p><div class="inside"><p>
		Non male, dato che soltanto un giorno prima doveva
		essersi fulminato; e non male neanche gli operai del
		pronto intervento riparazione lampioni; quanti ce ne
		vorranno di quelli come loro per cambiare una lampadina?
	</p><p>
		Almeno un paio, perché immagino ci voglia qualcuno a
		tenere la scala, o a manovrare il braccio della gru
		prima che l'altro possa raggiungere gli otto o nove
		metri d'altezza del lampione.
	</p></div><p>
		E così finimmo, io e il lampione, al parcheggio dalla forma
		strana, vicino alla rampa.
	</p><p>
		Io e il lampione, il lampione ed io.
	</p>
</div><div class="section">
	<p>
		Io sotto il lampione, il lampione sopra di me. E niente accadde.
	</p><p>
		Ed ero abbastanza sicuro di quello ch'era successo. Salendo la
		rampa, passando le parcheggio, proprio sotto quel lampione,
		m'era mancata la terra sotto i piedi.
	</p><div class="inside"><p>
		Beh, forse non era proprio quel lampione, dopotutto. Forse era
		uno degli altri...
	</p></div><p>
		Allora raggiunsi la rampa, mi girai, tornai sui miei passi e
		tentai di ricordare l'esatto percorso.
	</p><p>
		Non fu difficile.
	</p><p>
		Passai sotto il lampione giusto, percorsi precisamente la stessa
		strada, posai i piedi negli stessi punti. Niente.
	</p><p>
		Provai, riprovai. Avanti, indietro, un po' più a sinistra, un
		po' a destra, più piano, più veloce. Niente.
	</p>
</div><div class="section">
	<p>
		Quando mi resi conto della stanchezza, a forza di camminare, mi
		fermai.
	</p><div class="inside"><p>
		In effetti, che stavo facendo? Stavo veramente percorrendo un
		pezzo di strada, nella speranza che passare sopra un punto in
		particolare m'avesse permesso di staccarmi da terra?
	</p><p>
		Basta con le cazzate! Ne avevo abbastanza.
	</p><p>
		L'aver affrontato la conversazione più pesante di questa giovane
		vita deve avermi bollito il cervello. Devo aver preso un botta
		clamorosa, devo aver avuto troppo sangue al cervello &ndash;
		già, non da altre parti, come ci si potrebbe aspettare, eh? Al
		cervello &ndash; ed ho avuto un'allucinazione, una visione, un
		viaggio in acido, qualcosa del genere.
	</p></div><p>
		Giurai a me stesso che non avrei mai più cercato di volare, così
		come avrei evitato di interrogarmi ulteriormente su come sarebbe
		dovuta andare quella sera fatidica.
	</p>
</div><div class="section">
	<p>
		Me ne tornai a casa, mi infilai nel letto e m'addormentai. E
		dormii, fisso fisso.
	</p><p>
		Quando mi svegliai, era un altro giorno. Un giorno di lezione.
		Di nuovo.
	</p>
</div>
