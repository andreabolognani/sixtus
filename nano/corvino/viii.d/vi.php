	<p>
		E mentre pensavo alla lampadina funzionante mi sentii
		sollevato. Non particolarmente felice, ma rincuorato.
		Gaio. Sollevato.
	</p><p>
		Sollevato è il termine giusto, perché mentre pensai a
		quanto fosse alto il lampione e stimai che fossero
		appunto otto o nove metri, ci arrivai.
	</p><p>
Con più coscenza e meno sonno del giorno prima, stavo galleggiando a svariati
metri d'altezza e vidi da vicino com'è la lampadina di un lampione. Nulla di
particolarmente interessante: è soltanto una lampadina molto grossa, coperta da
un verto piuttosto spesso. Ma niente di ché. Decisi che non valeva la pena di
osservare oltre, anche perché era tecnicamente ancora giorno, nonostante i
lampioni fossero accessi. Saranno state le sette, le sette e mezza di sera, e
non ero affatto certo che nessuno sarebbe passato per quel parcheggio. Per
quanto poco frequentato che fosse, era comunque pieno di automobili parcheggiate
in almeno la metà dei posti a disposizione, e in quell'ora molti sarebbero
potuti tornare a casa dal lavoro (forse era un po' tardi, ma perché rischiare?).
</p><p>
Decisi di scendere. E mi resi conto di non sapere affatto come fare.
</p><p>
Non fu bello.
</p><p>
Per un attimo mi sentii idiota, semplicemente.
</p><p>
Poi immaginai per un attimo la scena: passa un tizio qualunque, trova uno appesa
al lampione, lo vede (mi vede) e urla, chiama la polizia e cose del genere.
Vabbeh, pensai, mal che vada dirò di essermi arrampicato e di non essere più
capace di scendre, come fanno i gatti sugli alberi.
</p><p>
Non molto plausibile, ma meglio che farsi beccare.
</p><p>
Poi immaginai che fosse lei a vedermi. E allora caddi. Caddi a peso morto. Non
fu bello. Nient'affatto.
</p><p>
Dev'essere sembrato divertente, visto da fuori. Ma posso garantire che da dentro
l'unico aspetto positivo fu la brevità della caduta. Perché non caddi molto a
lungo, non abbastanza per girarmi ma neanche abbastanza a lungo perchè l'impatto
fosse fatale.
</p><p>
Caddi secco secco sulla schiena, ma m'accorsi di star urlando soltanto per lo
spavento. Ritenni, sul momento, di essere stato enormemente fortunato per non
aver subito la benché minima frattura, soltanto i graffi sulle mani per essermi
appoggiato in tutta fretta sull'asfalto, il fiato corto per aver gridato, ma per
il resto stavo bene.
</p><p>
Decisi di andarmene, di archiviare quell'episodio e di dormirci sopra. Ma
tristemente non era nemmeno ora di cena, e prima del sonno sarebbero passate
ore. Mi odiai per non aver aspettato la notte, prima di tentare strambi
esperimenti di piegamento delle leggi fisiche.
</p><p>
Tornai a casa, ebbi la mia cena. In silenzio, raccontando di com'ero caduto
passando in un qualche posto senza onore, cercando di distoglere l'attenzione,
cambiando discorso, controinterrogando i miei genitori sulla loro giornata.
</p><p>
Decisi di non uscire quella sera, per evitare altri spiacevoli incidenti e altre
possibili cadute, anche peggiori. Per non sfidare la fortuna.
</p><p>
Però un dubbio mi assillava. Ero salito fino alla lampadina senza fare nulla di
particolare. Non stavo effettivamente facendo nulla; niente mosse, nessuna
parola. Mi stavo chiedendo come fosse la lampadina, ed ero contento che
funzionasse.
</p><p>
Aspettai qualche giorno, per evitare di tornare troppo presto al luogo
incriminato (chissà che qualcuno m'avesse visto) e per farmi passare il dolore
alle mani (qualche graffio me l'ero fatto).
</p><p>
Così, due notti dopo il mio secondo volo tornai al lampione. Questa volta ero
molto più preoccupato che interessato ai possibili risultati, semplicemente
avevo paura di cadere come l'altra volta. Un po' come quando devi saltare sopra
un buco, o da una roccia all'altra. Sai bene che puoi saltare fino a lì, non è
neanche tanto lontano, con un passo ce la fai, ma sai anche che se per caso
dovessi scivolare o cadere ti faresti parecchio male. E non solo, c'è anche il
disonore per aver perso i denti facendo una cosa stupida. Non una grande
prospettiva. In fondo, la paura dell'altezza è una cosa assolutamente naturale.
</p><p>
Come quando ti vuoi tuffare da una scogliera: te la fai addosso, rimugini un
pochetto, valuti l'altezza, la profondità dell'acqua. Dopo un po' ti decidi e
salti oppure ti arrendi. Se riesci a buttarti la prima volta puoi tornare allo
stesso salto tutte le volte che vuoi.
</p><p>
Ma con il mio lampione non fu la stessa cosa. Forse il lampione era stato
sostituito con uno più alto, assieme a tutti quelli del parcheggio. Non avevo
alcuna voglia di tornare lassù. E poi la luce del lampione era andata, di nuovo.
Cominciai a pensare a quanto potente potesse essere il destino, che poteva

fulminare quella lampadina una volta al giorno. Forse era davvero il caso che
qualcuno abbattesse quel parcheggio e rifacesse tutto l'impianto, con un po' di
sale in zucca, stavolta, magari.
</p><p>
Dopo una buona mezzo, finalmente, me ne andai. Non valeva la pena di rischiare i
grattoni sulle mani, la schiena e il culo per vedere da vicino un lampione
spento. Così me ne andai, sconfitto.
</p><p>
Dopo una ventina di passi, mentre compativo la mia codardia, vidi sfarfallare la
luce alle mie spalle. Quando mi voltai a controllare, notai chiaramente che il
lampione era tornato a funzionare. Il destino doveva avercela giù dura con quel
lampione: decisi di tornare a controllare. Un po' più allegro, stavolta, deciso
a scoprire il perché di quel portento.
</p><p>
Ma mentre mi avvicinavo, il lampione si spense nuovamente. Fanculo.
</p><p>
Tornai sui miei passi senza indugio e feci per andarmene.
</p><p>
E il lampione si riaccese.
</p><p>
Allora ci tornai una terza volta.
</p><p>
E ancora una volta si spense.
</p><p>
``Lampione del cazzo'' pensai ``Mi prendi per il culo? Fanculo te e chi t'ha
messo qui''.
</p><p>
L'intero parcheggio divenne buio. Tutti i lampioni. Tutti i cazzo di lampioni.
</p><p>
Mi convinsi che qualcosa non andava e mi allontanai fino al distributore di
benzina, dal quale si poteva intravedere l'area del parcheggio, senza vedere
direttamente le luci. Quando fui là fuori, la luce era tornata ancora una volta.
</p><p>
Mi chiesi seriamente se potessi essere io la causa.
</p><p>
``Bravi lampioni, continuate a funzionare'' dissi.
</p><p>
E funzionarono.
</p><p>
Poi tornai di là, in mezzo al parcheggio. E funzionavano.
</p><p>
``Lampioni del cazzo, spegnetevi''. E funzionavano.
</p><p>
Evidentemente si era trattato di una clamorosa botta di sfiga ed io non
c'entravo nulla.
</p><p>
Un po' deluso pensai ``Teoria del cazzo, che mi salta in mente?'' e il lampione
più vicino si spense.
</p><p>
Rimasi in quel parcheggio per un paio d'ore.
</p><p>
Provai e riprovai, per essere assolutamente certo di poterlo fare a volontà.
</p><p>
E dopo parecchi tentativi, non senza un discreto sforzo di concentrazione, fui
in grado di spegnere un lampione qualsiasi, fissandolo e incazzandomi. Finché
restavo rilassato a sufficienza, invece, tutte le luci restavano accese.
</p><p>
Alla fine feci provai anche qualche coreografia, spegnendo i lampioni in ordine,
o due a due. Due insieme mi risultò più difficile, ma capii che sfruttando la
rabbia derivante dal fallimento potevo effettivamente riuscirci. Mi bastava non
essere sicuro di poterlo fare.
</p><p>
E fu così che imparai a spegnere i lampioni.
</p><p>
Ed ero abbastanza sollevato da sollevarmi, e andai a controllare di nuovo come
fosse la lampadina del lampione da vicino. Poi mi venne l'idea che mi rovinò la
schiena: ``E se spegnessi la luce mentre fluttuo?''
</p><p>
Male, male, male, molto male.
</p><p>
Spegnere una luce significa anche tornare alla gravità, come scoprii
dolorosamente. E' abbastanza accettabile che una mente non ben addestrata non
possa provare due emozioni diverse in uno stesso momento. Non fu affatto facile
imparare una cosa del genere, infatti, e mi ci volle un sacco di tempo. Ma in
quel momento mi fu lampante quanto fosse importante fare una sola cosa alla
volta.
</p><p>
Ero per terra, caduto pesantemente sul culo da una buona altezza. Già allora,
come avrei scoperto poi, ero abbastanza duro e non mi ruppi nulla, ma mi presi
comunque una bella incassata alla schiena, e non mi rialzai prima di una decina
di minuti. E quando mi rialzai fu anche peggio, perché la pressione sulle ossa
del bacino cambiò posizione o direzione; non so esattamente come funzioni, so
che mi si risistemarono le ossa e il procedimento, seppur naturale (almeno
spero) non fu affato indolore.
</p><p>
Quanto è brutto camminare con il male alla schiena spero non dobbiate mai
saperlo. E' brutto. Parecchio. Stai piegato perché non riesci a stare dritto, e
sai che stare piegato non aiuta la schiena; vorresti andare più in fretta,
perché vorresti arrivare prima, ma i movimenti sono più dolorosi quanto più sono
rapidi, quindi vai piano e sai che ci metterai di più, aggravando la tua
situazione e dilatando l'attesa del divano. Oh, lui sì è tuo amico e potrebbe
aiutarti e confortarti, ma è lontano, a casa che ti aspetta.
</p><p>
E io abitavo anche al quinto piano. Niente ascensore, lunga lunga ascesa, venti
rampe diverse con lo scorrimano da un solo lato.
</p><p>
Decisi che non avrei tentato di ritornare a casa direttamente, ma che avrei
cercato un posto dove riposarmi, possibilmente stando comodo.
	</p>
