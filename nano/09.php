o pranzi dove conosci cugini che non vedi da quindici anni, dove trovi persone
anziani che tutti chiamano zii ma che magari non sono neanche parenti; ecco, una
perpetua cena così. Un eterno posto sbagliato.
</p><p>
La storia non cambiò per un pezzo, fino al 13 agosto 2005.
