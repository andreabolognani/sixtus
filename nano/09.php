sentito nominare; semplicemente nulla di quello che avevo letto in vita mi
ispirava veramente. Niente mi pareva interessante.
</p><p>
Forse era l'effetto del troppo tempo a disposizione. Nel tempo che non passai in
biblioteca, nei sei mesi tra novembre e maggio del 2005, me ne andai a spasso.
Arrivai, camminando (o correndo) su tutte le cime montane che si potevano vedere
da casa mia. Una volta esaurite quella quindicina di montagne in vista,
cominciai con quelle nascoste oltre; correndo fino a fuori città, poi volando
direttamente fino ad una cima già conquistata, poi a terra nuovamente, fino alla
meta successiva. Muovendomi così lungo una circonferenza che allargai
progressivamente, passai un altro mese, vedendo tre o quattro sentieri nuovi
ogni pomeriggio, poi anche le notti. Presi infatti ad uscire tutte le sere,
lasciando la finestra aperta, assicurandomi di uscire senza rumore, per poi
fare ritorno prima che i miei si alzassero, verso le 06:30; stando fuori tutta
la notte.
</p><p>
Mi accorsi infatti di non aver più veramente bisogno di dormire, né di mangiare.
Potevo farlo, e sentivo comunque la fame, ma non ero mai stanco. Mai abbastanza
da dovermi fermare. Interruppi la tradizione che mi vedeva vagare a zonzo per la
città, in cerca della prossima fontanella.
</p><p>
E fu così che in un paio di mesi vidi il panorama da tutte le cime entro 150
chilometri da dove abitavo. Bello, in verità. Ma neanche quello era poi granché.
Ero comunque costretto a tornare a casa, per colazione, per pranzo e per cena,
tolte alcune occasioni in cui uscivo con gli amici. Ma in quel periodo tutti i
miei coetanei erano alle prese con la maturità, con le simulazioni d'esame, a
preparare tesine, nessuno aveva molto tempo o voglia per uscire a far due passi.
Nemmeno io, in effetti, avevo voglia di stare al passo di gente così lenta. Ma
meglio quello che stare solo soletto, andando per montagne ai cento all'ora. O
forse no?
</p><p>
La verità è che mi sentivo veramente fuori posto, dovunque.
</p><p>
E' come quando devi andare a mangiare da qualche parente che non vedi mai, che
