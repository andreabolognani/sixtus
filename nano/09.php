<h2>L'estate</h2>
<div class="intro"><p>
Che palle.
</p></div><p>
Che due enormi palle, gonfie fino al punto di scoppiare.
</p><p>
Due giorni dopo aver scoperto di poter leggere a contantto, un giorno dopo aver
scoperto di poter correre come un treno, quattro giorni dopo aver volato ero già
mortalmente stufo di essere `super'.
</p><p>
Essere immensamente veloce nel fare tutto più in fretta di chiunque altro avessi
attorno rese la mia vita estremamente lenta.
</p><p>
Mortalmente lenta.
</p><p>
Fintanto c'ero solo, avevo virtualmente nessun tempo morto per gli spostamenti.
Quando invece non lo ero, tutto era un tempo morto. Stare al passo con le
persone era tanto che stare fermi. Prendere l'autobus, andare in macchina, stare
in coda alle poste; divenne tutto insopportabile.
</p><p>
Non capitò nulla d'interessante per un sacco di tempo.
</p><p>
Passarono il Natale, l'inverno, il mio compleanno, la maturità, buona parte
della vacanze estive, senza nulla degno di nota. Niente era più degno di nota,
tutto era normale.
</p><p>
Avevo anche tentato di distrarmi leggendo la biblioteca. TUTTA la biblioteca,
quella comunale, almeno. Ci andai un pomeriggio dopo la scuola, passando di
scaffale in scaffale, mano destra in tasca, mano sinistra sui libri, un
passettino alla volta. Calcolai una media di otto secondi per volume. In due
mesi scarsi lessi tutti quei centomila volumi pubblici a disposizione. Non una
grande soddisfazione. E fu l'apice di quel periodo.
</p><p>
Imparai una cosa, mentre assilimavo la biblioteca: erano disponibili, in
quantità estremamente limitata, volumi in lingue straniere. Ne lessi di inglesi,
di francesi, di russi, di arabi, di cinesi, e anche svariati altri; capii che
conoscere la lingua non mi era affatto necessario per leggere il libro. E'
naturale, in fondo, non stato affatto leggendo, ma acquisendo l'impronta di
ciascuno volume. Nel ripensare al `Guerra e Pace' di Tolstoj, notai come potevo
rivedere le parti originali in russo e francese assieme alle parti tradotte che
avevo invece letto dalla versione italiana.
</p><p>
E fu così che imparai il russo, il francese, l'inglese, il danese, e molte altre
lingue. A leggerle, per lo meno; i suoni non mi erano affatto chiari, in
effetti. Ahimé, non trassi particolare spinta emotiva da tutta quella
conoscenza. Di fatto, pochissimi di quei libri mi erano piaciuti. Non che ci
