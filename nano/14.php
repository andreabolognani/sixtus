<h2>La rivelazione</h2>
<div class="intro"><p>
Lasciato il funerale, andai dritto ad incontrare il mio prossimo
avversario. La sua identità mi era stata chiara come il sole, dopo aver appreso
tutto quello che i miei due seguaci aveva da dirmi.
</p></div><p>
Ci volle molto tempo infatti, tre giorni interi, perché mi raccontassero tutto.
</p><p>
Cominciarono con un classico ``Allora, cosa vorresti sapere prima?''
</p><p>
Cominciai chiedendo come mai fosse buio nonostante l'ora. Ci volle una lunga,
lunga risposta.
</p><p>

</p></div><div class="section"><p>

</p><p>
Dunque, avevo ragione sulla storia della `tenda celeste'. Non ci trovavamo più
nel mondo vero, eravamo `di là'. Non `l'Aldilà', quello è un altro posto. Ma il `di
là' non è un luogo diverso, è lo stesso.
</p><p>
E' sovrapposto al mondo.
</p><p>
Per questo potevo sentire il vento, percepire i suoni, vedere tutte le
bestioline montane intente nei loro affari.
</p><p>
Mi dissero che da lì avrei anche potuto muovermi liberamente (più liberamente
che nel mondo umano), avrei potuto volare senza essere visto, avrei potuto
leggere la mente senza fissare male la gente (se per caso mi fosse andato di
farlo, adoro fissare male la gente).
</p><p>
Per una ragione non giustificata, il sole non si vede. E' sempre buio `di là',
sia di giorno che di notte; ma non è certo un problema per noi che di notte ci
vediamo.
</p><p>

</p></div><div class="section"><p>

</p><p>
Quando chiesi la ragione dell'impossibilità per la luce solare diretta di
passare `di là', sentii per la prima volta una risposta che poi mi venne a noia,
per il numero di occorrenze che di essa ci furono. La risposta fu `perché così
funziona la magia'.
</p><p>
`Magia'.
</p><p>
Una parola alla quale non avevo pensato. Mi pareva fin troppo ridicolo.
</p><p>
La seconda domanda che posi fu spontanea ``Chi sono io?'' ma non era
sufficiente, quindi continuai prima che potessero rispondermi ``Che sono questi
poteri? E voi, chi siete? Da dove venite? Perché mi chiamate `Maestro'?''
</p><p>
Tempo al tempo, arrivammo alla risposta per tutte queste domande. Nell'ordine.
</p><p>
Chi io fossi non fu chiaro da subito, e i due ebbero da discutere un poco. Mi
dissero che `quelli come loro' (che ancora non sapevo identificare, lo seppi più
avanti) chiamavano quelli come me `\spontanei'.
</p><p>
Uno `\spontaneo' è una persona che nasce tra la gente comune. Ad un certo punto,
dopo l'adolescenza e spesso in rapporto ad un qualche trauma, sviluppa
spontaneamente e senza alcuno studio, un qualche potere magico innato. Questa
spiegazione mi parve estremamente calzante.
</p><p>
Che fossero quei poteri, dunque, era un po' più chiaro: erano effettivamente
magici. Poteri magici innati. Alcuni ce li hanno; molti altri no. Ed io ero uno
dei pochi.
</p><p>
Chi fossero quelle due bestie, Battesimo e \smeraldino, fu lunga e noiosa.
Fondamentalmente, `quelli come loro' e `quelli come me' se le danno di santa
ragione non appena si incontrano. E questo accade da qualche migliaio di anni.
</p><p>
Fin dai tempi più remoti della storia umana, sono esistiti degli `\spontanei'.
Ogniqualvolta uno \spontaneo\ sbatteva il faccione contro uno di `loro', i due
si sfidavano a duello, proprio come era accaduto a noi tre.
</p><p>
Solo che non accadeva mai a tre. Che i miei due seguaci ricordassero, era
successo una sola volta, forse due, che uno \spontaneo\ uccidesse il suo lupo e il
suo corvo. Di solito, gli \spontanei\ non erano mai abbastanza in gamba da
superare il primo incontro; quando poi, ogni dozzina di casi, erano il corvo o
il lupo a rimetterci le penne, saltava fuori l'altro, e il poveraccio era
fottuto.
</p><p>

</p></div><div class="section"><p>

</p><p>
Sul perché mi chiamassero \maestro, non ci fu molto da dire. Non sul nome,
almeno. Pare che fosse consuetudine, da quelle parti, divorare i poteri degli
sconfitti: in pratica, il sopravvissuto poteva riuscire ad ottenere una parte
(anche sostanziale) dei poteri del defunto. Molti corvi e molti lupi, uccisori
gli uni degli altri, e più rararemente, uccisori di `\spontanei', si
guadagnavano questo appellativo come segno di rispetto.
</p><p>
Sul come fossi io il \maestro, ci fu una lunga discussione. Fin da subito, mi fu
chiaro che la cosa era estremamente rara, quasi sconvolgente. Dicevano
addiritttura ch'era molto meglio per lo essere morti, piuttosto che dover
passare il resto della vita a spiegare come potesse esistere uno come me. Beh,
io.
</p><p>
La stranezza era che, a memoria d'uomo (di lupo e di corvo, veramente), ero il
primo \maestro\ umano ad abbattere due avversari. Come era stato accennato
prima, infatti, in non più di una dozzina di casi lo \spontaneo\ era
sopravvisuto ad un duello. E anche in quel caso, era sopravvisuto soltanto per
soccombere al successivo. E in quei casi, i lupi e corvi vincitori divennero
grandi eroi per la propria gente.
</p><p>
Un nonno di Battesimo e il padre di Smeraldino erano eroi di quella portata,
ed entrambi erano stati guide per i rispettivi popoli. Dei re, insomma, mica
cazzi. Re.
</p><p>

</p></div><div class="section"><p>

</p><p>
Mi sfuggì dunque la domanda ``Quindi, mi par di capire, che voi siate entrambi
principi?''
</p><p>
Erano effettivamente principi. Chiesi a ciascuno di parlarmi un poco della
propria gente. \battesimo, in quanto morto da un giorno in più, andò per primo.
</p><p>

</p></div><div class="section"><p>...</p></div><div class="section"><p>

</p><p>
Il nonno di Battesimo aveva nome \eracleo. Era alto quanto un uomo, più
pesante degli orsi più grossi, e il suo territorio era grande quanto la
Germania.
</p><p>
Quando ebbe 103 anni, fiutò uno \spontaneo.
</p><p>
Lo fiutò da migliaia di chilometri di distanza, mentre cacciava attorno a queste
montagne. Lo \spontaneo\ stava in Danimarca, e si chiamava \kasper.
</p><p>

</p></div><div class="section"><p>

</p><p>
\eracleo\ seguì le sue tracce, galoppò un giorno e una notte, giunse al porto di
Copenaghen. Era la sera del 13 settembre 1293. Quando giunse, uscì dal `di là' e
vide affondare due galeoni. Fiutò, nell'acqua, l'odore del sangue.
</p><p>
\kasper, ch'era in grado di alzare onde di sei metri e di governare le navi con
la sola forza della mente aveva saputo evitare le penne avvelenate di un corvo,
gli aveva resistito e l'aveva annegato. Il corvo aveva nome \brillo.
</p><p>
\eracleo\ gli intimò dunque la resa, e la restituizione di ciò che apparteneva a
lui, ossia i poteri del corvo, non degni degli umani. \kasper\ rispose
scagliandogli addosso penne di corvo avvelenate.
</p><p>
Il lupo corse sull'acqua. Lo \spontaneo\ invocò la tempesta e sollevò le onde. Il lupo
le sfruttò per saltare più in alto.
</p><p>

</p></div><div class="section"><p>

</p><p>
Sette minuti dopo, trentotto imbarcazioni erano affondate o stavano calando a
picco. \eracleo\ sbranò il corvo e divenne il settimo lupo abbattitore di un
\maestro umano.
</p><p>
Regnò sulle quindici famiglie di lupi per 308 anni, poi morì.
</p><p>
Dopo di lui, non vi fu più un lupo re.
</p><p>

</p></div><div class="section"><p>...</p></div><div class="section"><p>

</p><p>
Il padre di Smeraldino aveva nome \arfollo.
</p><p>
Il 24 agosto 1994 stava sorvolando Granada. Scorse uno \spontaneo\ e gli calò
sopra. Non fece in tempo.
</p><p>
Lo spontaneo si chiamava \malsatio\ e sputava fiamma, dalla bocca e dagli occhi.
Era lento e goffo, ma riuscì ad incenerire un lupo che aveva nome \rogaron.
</p><p>
\arfollo\ scagliò i suoi fulmini sull'umano, ma quello già bruciava e non ne
risentì, rispondendo con fuochi d'artificio che illuminarono la notte. Il corvo
chiamò il vento e la pioggia, e gelò e spense tutto ciò che lo \spontaneo\ mi scagliò
contro.
</p><p>
Infine, gli congelò il cuore, e divenne il quinto corvo abbattitore di un
\maestro umano. Si spense l'anno scorso, all'età di 178 anni.
</p><p>

</p></div><div class="section"><p>...</p></div><div class="section"><p>

</p><p>
Mi inchinai di fronte alla regalità degli antenati dei miei seguaci. Poi chiesi
che avessero quei poveracci, il danese e lo spagnolo, e chissà quanti altri di
cui ignoravo l'esistenza, per soccombere a lupi e corvi.
</p><p>
Ridacchiarono entrambi.
</p><p>
Poi dissero ``Nessuno spontaneo ha mai mostrato due colori, come invece hai fatto tu, \maestro''.
</p><p>
</p><p>
</p><p>
La storia dei colori non mi era nuova: ricordavo chiaramente di quanto
\battesimo di fosse stupito di me, chiamandomi `multicolore'. Chiesi che
significasse, e che fossero i colori di cui parlavano.
</p><p>

</p></div><div class="section"><p>

</p><p>
Battesimo parlò per primo ``Quando ci scontrammo la prima volta, tu cambiasti,
passando al giallo che indica leggerezza, e riuscisti a scivarmi. Per combattere
nelle stesse condizioni, come si addice ad un duello tra pari, cambiai anch'io.
Forse avrai notato il mio pelo dorato, in quell'occasione''
</p><p>
Non lo interruppi per spiegargli che vidi bianco e non giallo, per evitare di
confondermi.
</p><p>
Lui proseguì ``Ma poi tu feci quello che non avevo previsto, beh, quello che
nessuno avrebbe previsto: tu cambiasti colore, lasciando il giallo e passando
all'indaco che indica potenza. In quello stato, non avrei potuto reggere nemmeno
l'ombra di un tuo colpo, e infatti mi spezzasti.''
</p><p>
Non lo interruppi nemmeno questa volta, e trattenni quel senso di rigetto nel
sentirmi additare come colorato d'indaco. Mi trattenni; a stento, ma lo feci.
</p><p>
Poi parlò Smeraldino ``E con me feci una cosa simile, ma ancora una volta
diversa: quando venni ad incontrarti eri arancione, che indica il mutamento. Non
sapevo che alcuno \spontaneo\ fosse mai stato arancione. Ma immaginai che non
fossi in grado di controllare la tua forma, né tantomeno di cambiare colore.''
</p><p>
Ancora una volta non interruppi. L'arancione non lo vidi nemmeno.
</p><p>
Continuò il corvo ``Ma infine, come non saprei dire, tu riuscisti a cambiare
ancora, non solo piegando il mio fulmine con il celeste che indica controllo, ma
riuscisti in qualche modo a mescolare il celeste con il giallo, riuscendo a
colpirmi prima che potessi scansarti. Il fulmine non sarebbe stato nemmeno
necessario, ero ormai spacciato. Colpa mia, certo, che abbasai la guardia. Ma
nessuno, nelle mie penne, avrebbe potuto prevedere un simile prodigio''
</p><p>
E allora, sentite queste prime spiegazioni, ne chiesi altre.
</p><p>
Per prima cosa, infatti, non sapevo di questi colori; non ero in grado di
vederli, se non per quei riflessi bianchi e neri che mi annebbiavano la vista.
Credetti che fossero attacchi di qualche genere, non che fossero una
manifestazione del mio potere.
</p><p>
Su questo, mi dissero che in effetti i colori erano sette. Ma c'erano anche due
cariche. Una carica chiara, una carica scura.
</p><p>
La carica scura, in qualche modo, viene utilizzata da chi si colora di indaco,
di verde e di rosso. La carica chiara, invece, viene utilizzata da chi usa
l'arancione, il giallo, il blu e il violetto.
</p><p>

</p></div><div class="section"><p>

</p><p>
Dunque, il giallo mi rende leggero, e permette di correre come un treno.
</p><p>
L'arancione mi permette di cambiare forma, come per il mio aspetto di lupo, ma
non solo.
</p><p>
Il rosso mi permette di percepire il pensiero, di acquisire i libri (e non solo)
e di acuire i miei sensi.
</p><p>
Il verde mi permette di riprendermi dal mal di schiena, di evitare di mangiare e
di dormire.
</p><p>
Il blu mi permette di spegnere i lampioni e di piegare i fulmini.
</p><p>
L'indaco mi permette di rafforzarmi e irrobustirmi, di abbattere i lupi e
probabilmente di sfondare i muri.
</p><p>
Il violetto, infine, mi permette di afferrare i lupi alla carica senza toccarli,
di trattenerli e di volare.
</p><p>

</p></div><div class="section"><p>

</p><p>
A quanto pare, il fatto che la scienza affermi che l'indaco non esiste, e che
tra il blu e il violetto non ci sia nulla, evidentemente alla Magia non importa.
</p><p>

</p></div><div class="section"><p>...</p></div><div class="section"><p>

</p><p>
Wow.
</p><p>
Ma c'erano ancora dubbi. Come mi avevano trovato, questi due? Perché volevano
uccidermi? Che sarebbe successo dopo? 
</p><p>
Domandai per prima l'ultima ``Che cosa succederà adesso?''
</p><p>
Non seppero rispondermi subito.
</p><p>
Nessuno lo sapeva: ero semplicemente il primo esploratore in un mondo
sconosciuto. Nessuno dei loro aveva mai visto campare uno come me per più di un
paio di giorni.
</p><p>
Dissero effettivamente `un paio di giorni'.
</p><p>
``Perché un paio di giorni soltanto?'' chiesi ``Saranno passati almeno sei mesi
quando ho volato per la prima volta. Non si è presentato nessuno di prima di voi
due''
</p><p>
Seguì un lungo silenzio.
</p><p>

</p></div><div class="section"><p>

</p><p>
Mi spiegarono di come gli \spontanei\ morissero per inseperienza, come i piccoli
uccellini quando lasciano il nido al primo tentativo: molti semplicemente cadono
a terra e muoiono. Per noi dovrebbe essere la stessa cosa: appena i nostri
poteri si manifestano, qualcuno compare per mangiarci.
</p><p>
A quanto pare, questo accade per la troppa visibilità. I colori lasciano tracce,
possono essere percepiti anche da molto lontano, se qualcuno li sta cercando. Fu
in questo modo che gli antenati dei miei seguaci furono in grado di procurarsi
gli scontri.
</p><p>
Fortunatamente per noi, di lupi e di corvi ce ne sono pochi. Tuttavia, stando a
quanto mi dissero, il riverbero di uno \spontaneo\ è una cosa abbastanza grossa,
illumina l'intera regione, lascia tracce ovunque, lascia persino un odore.
</p><p>
Questo accade ogni volta che lo \spontaneo\ cambia colore, e quando accade quasi
immediatamente un lupo o un corvo se ne accorgono.
</p><p>
Il fatto che a me non fosse capitato era sostanzialmente inspiegabile. Chiesi ai
miei insegnanti se fosse il caso di controllare questo `riverbero', ma entrambi
mi intimarono di non tentare.
</p><p>
Per quanto fosse particolare infatti, la mia trasparenza poteva essere la chiave
per la mia sopravvivenza.
</p><p>
Per quanto avessi lasciato una traccia, essa doveva essere molto presente. In
pratica, mi dissero che la cima della Mariglia era impressa della mia presenza,
grazie a tutto il tempo che ero uso passare lì. Tant'è che entrambi pensavano
che vivessi lì.
</p><p>
Non solo ero multicolore, quindi, ero anche trasparente.
</p><p>

</p></div><div class="section"><p>

</p><p>
E mi immaginai la scena, con il banditore che annuncia ``All'angolo rosso,
Terrorsaur il Terribile, 208 centimetri per 113 chilogrammi, 21 vittorie - il
pubblico esulta, grida, urla; piovono fiori, coriandoli e reggiseni - e
nell'angono blu, Corvino il Multicolore Trasparente, 168 centimetri per 64
chilogrammi, al suo debutto - pionovo fischi, disapprovazione, lattine e
bottiglie''
</p><p>
Ridacchio immaginando la scena. Sì, rido per non piangere.
</p><p>

</p></div><div class="section"><p>

</p><p>
``Significa quindi che devo ritenermi estremamente fortunato ad essere stato
scoperto soltanto ora, anziché mesi fa? O può esserci dell'altro?'' chiesi.
</p><p>
I pericoli erano molti; cercarono di spiegarmi concisamente quanto fossi un
cucciolo in un territorio di caccia conteso da svariati predatori. Fino ad
allora avevo vissuto in una conca, se così si può dire, ma ora le probabilità
che io venissi scoperto erano estremamente più alte, e avrei dovuto tenermi
pronto.
</p><p>
``Pronto per cosa?'' chiesi io, temendo che ci fossero molti altri lupi e corvi,
anche più d'uno alla volta.
</p><p>
Ma fu peggio.
</p><p>

</p></div><div class="section"><p>...</p></div><div class="section"><p>

