\streghe. A centinaia.
</p><p>
Eh già, perché se il mondo `di qua' era un posto abbastanza carino, molto pieno
di gente più o meno buona, `di là' invece era un gran casino.
</p><p>
Battesimo affermò che i lupi fossero non più di 300, nel mondo. Smeraldino
credeva che i corvi fossero mezzo migliaio. Ma c'era molta altra roba.
</p><p>
C'erano i gatti. Estremamente più numerosi di lupi e corvi. Infatti, i lupi e i
corvi `di là' non sono quelli che abbiamo 'di qua', mentre i gatti sono gli
stessi.
</p><p>
Ma non solo, ci sono anche le \streghe. Ovviamente.
</p><p>
E controllano il mondo. Ovviamente.
</p><p>

</p></div><div class="section"><p>

</p><p>
``Non lo avevi chiesto'' risposero alla mia domanda seguente.
</p><p>
Allora lo feci ``Allora, ditemi: chi sono? Quante sono? Che sanno fare? Che
aspetto hanno? Cosa potrebbero volere da me?''
</p><p>
E questo fu il motivo per cui non scesi da quella cima per altri tre giorni. Il
mondo `di là' è complicato.
