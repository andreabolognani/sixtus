<h2>La corsa</h2>
</p><p>
\first{Grazie a quella che poi chiamai `acquisizione', finii ben presto per
saperne un sacco.}
</p><p>
Soltanto in quel pomeriggio conobbi il contenuto di tutto quello che di stampato
avevo in camera. Prima ogni libro scolastico, poi anche il resto.
</p><p>
Imparai a sopportare il dolore. Trattenendo il fiato e sapendo che stava per
arrivare, fui presto in grado di trattenerlo abbastanza da non emettere suoni,
da non tremare, da non avere insomma una reazione troppo anomala.
</p><p>
Decisi di uscire. Dopo cena annunciai che mi sarei fatto un giretto, mi infilai
le scarpe, presi la giaccia e me ne andai a spasso. Prima di uscire m'ero
assicurato che la portafinestra che da sul balcone fosse aperta.
</p><p>
Non avevo un'idea precisa su dove sarei andato, presi una strada a caso e
camminai e camminai, finendo forse per caso, forse inconsciamente, al parco
dov'ero passato con \camelia. Mi parve che facesse molto meno freddo, senza lei
accanto. Non dovrebbe essere il contrario?
</p><p>
Mi sedetti sulla stessa panchina, a pensare ``E se...? Sarebbe potuta andare
diversamente, se avessi reagito meglio?''
</p><p>
Ma forse avevo già reagito nel modo migliore. Ma sarebbe stato veramente peggio,
se avessi reagito peggio? E perché peggio, poi? Come sarebbbe potuta non andare
in meglio, invece? C'erano evidenti possibilità di avere pelle a contatto,
scambio di fluidi, cosacce varie.
</p><p>
Eppure più ci pensavo più ero convinto d'aver fatto bene. E lo feci. Poi conobbi
anche il motivo e tutto mi fu chiaro, ma in quel momento, seduto solo soletto su
quella panchina ero perso come una boa di segnalazione in mezzo al mare. Seppure
vero che la convizione di essere nel giusto mi ancorava al fondo e mi teneva
saldo ad una lunga catena, quella catena era parecchio lungo e il mare era
parecchio burrascoso, lasciandomi queste condizioni molto ampia libertà di
galleggiare a destra e a sinistra, avanti e indietro... Dubbi, dubbi, dubbi.
</p><p>
Dopo un quarto d'ora comincia ad averne le balle piene e il sedere congelato.
Quindi mi lasciai quei dubbi alle spalle, mozzai la catena d'ancoraggio della
boa e me ne andai. Al volo.
</p><p>
Mi portai in alto, sopra gli alberi, oltre la portata dell'illuminazione
pubblica, per evitare di essere visto. Seguendo il sentierino del parco, arrivai
fino al fiume e da lì mi mossi lungo la pista ciclabile, che da lì proseguiva
per svariati chilometri, giù e giù lungo il fiume fin fuori città. Volai fino
all'ultima piazzola, là dove si trovano le ultime panchine prima del prossimo
paese, quasi cinque chilometri dopo.
</p><p>
Scesi e mi sedetti lì, ma mi rialzai quasi immediatamente, perché non avevo un
granché voglia di stare seduto. Presi a camminare lungo la strada, verso sud,
verso il prossimo paesello. Ricordai che in fondo a quel tratto, dove si trovava
l'incrocio che porta al paese, si trovava anche una fontanella; al solo pensiero
mi venne sete. Beh, non proprio sete sete, era più che altro voglia di fare un
po' di strada, stancarsi abbastanza da avere sete nel momento di giungere a
portata d'acqua. Così mi diressi in giù.
</p><p>
E cammina cammina, avevo davvero tanta strada davanti. Anche con il mio passo,
che negli anni era andato migliorando, avrei impiegato quasi mezz'ora. Come
spesso avevo fatto in passato, lasciai che i pensieri vagassero liberamente
nella mia testa.
</p><p>
Pensai agli ultimi giorni trascorsi e mi resi conto di quanto fossi un bambino
felice coperto di giocattoli nuovi. Sveglio com'ero da almeno quindici, avevo
letto tutti i libri che avevo in casa e avrei potuto recitare ogni singola riga
che li componeva; ero arrivato fuori città senza camminare un passo, bensì
volando con la sola volontà di arrivare fin lì, e non avevo neanche sonno. E
potevo spegnere i lampioni col pensiero.
</p><p>
Prima di chiedermi se tutte queste nuove e inaspettate capacità avessero un
origine, un motivo, e più importante, delle conseguenze, mi venne spontaneo
chiedermi se l'elenco fosse completo o se invece ne avessi delle altre, ancora
sconosciute. E siccome ai tempi ero ancora giovane e sbarbatello, scelsi
d'indagnare la seconda parte; che poteri avrei potuto avere? E come li avrei
trovati?
</p><p>
La mia conoscenza letteraria non era particolarmente ricca di individui dotati
di poteri, ma la mia conoscenza dei fumetti invece mi mise a disposizione una
serie notevole di cose che avrei potuto fare.
</p><p>
Camminare sui muri! Peccato, non avevo muri su cui provare.
</p><p>
Vedere attraverso i vestiti: fissai forte forte una manica della giacca, ma non
scomparve.
</p><p>
Volare: già fatto.
</p><p>
Sollevare un'automobile: niente automobili, niente sassi, niente bidoni della
spazzatura; fuori città com'ero non c'era nulla di pesante da sollevare.
</p><p>
Ossa indistruttibili: questo decisi di non verificarlo.
</p><p>
Parlare coi pesci: non c'erano pesci, o almeno sperai che non ci fossero, perché
quel fiume era color terra bruciata. E poi, probabilmente, i pesci non avrebbero
avuto un granché da dirmi. Poi scoprri che mi sbagliavo, su entrambe queste
affermazioni.
</p><p>
Fiato atomico, provo, niente. Ultraraggio, provo, niente. Raggi laser dagli
occhi, provo, niente. Pugni a razzo, provo, niente. Ragnatele dalle mani, provo,
niente. E così anche per l'urlo di Tarzan, la lettura del pensiero, i
supersensi, l'invisibilità, la vista termica, i baffi prensili e l'allungamento
delle braccia.
</p><p>
Decido di smetterla con i tentativi a caso, anche perché comincio a stufarmi.
Tento per l'ultima volta urlando ``Shazam'', ma non accade nulla, non mi
colpisce alcun fulmine.
</p><p>
Allora, vaffanculo, la piantai lì e ripresi a camminare. Ero ancora a metà strada,
probabilmente avrei impiegato ancora dieci minuti, forse quindici. Ma mi sentivo
abbastanza spazientito, così decisi di correre, per un tratto almeno, per
avvicinarmi un pochetto.
</p><p>
Avevo corso in qualche occasione, prima di allora; alle medie, anche al biennio
superiore, per i giochi che le scuole organizzano tra maggio e giugno. In almeno
tre distinte occasione corsi i 1000 metri piani. Esperienza davvero intensa, che
sconsiglio a chiunque non sia seriamente intenzionato ad esprimere tutto se
stesso. La prima volta arrivai penultimo, con un tempo di quasi cinque minuti.
Con gli anni, un allenamento migliore e un po' di volontà in più arrivai ad un
rispettabile record personale di 3'41", nel 2004.
</p><p>
Ci sono due modi per cominciare a correre: puoi partire di corsa da fermo,
oppure puoi partire dando un'accelerata mentre cammini. Spesso scelgo la
seconda, perché spesso sto già camminando quando sto per correre. In quella
fresca notte di novembre, al buio sulla pista ciclabile fuori città, stavo
appunto camminando.
</p><p>
Buttanto avanti il peso, inclinando la schiena, dopo aver insipirato
profondamente, cominciai a correre. Cominciai molto piano, perché non m'ero
stiracchiato prima di cominciare, il battito era basso, i muscoli non erano
sciolti, i polmoni non erano abituati. Di solito davo inizio così ad una lenta
progressione, aumentando la lunghezza delle falcate senza modificare il ritmo,
respirando man mano più a fondo mantenendo la cadenza. Il ritmo è importante per
arrivare a correre a lungo, lasciando che il metabolismo carburi al meglio,
senza spingere.
</p><p>
Se tutto va bene, entro un paio di minuti dovresti smettere di sentire freddo,
dovresti aver trovato il giusto ritmo per il respiro e dovresti sentire nello
stomaco quella sensazione che ti dice che potrai andare avanti così. In pratica
arrivi al punto in cui sei già stanco, ma la fatica rimane costante finché non
ti fermi o non copri una decina di chilometri. Se la sensazione non arriva, è
perché stai morendo, oppure perché non stai spingendo abbastanza forte.
</p><p>
Non stavo spingendo abbastanza forte, non abbastanza da far fatica; allora
spinsi di più, aumentando il passo, fino al punto in cui, solitamente, correvo
per gli allenamenti. Lo stomaco resse, i polmoni pure. Dopo un po', constatando
che ancora avevo gambe e fiato, e volendo spingere ancora, spinsi. E spinsi
parecchio, tenendo il ritmo che normalmente tenevo in gara: un ritmo abbastanza
massacrante, che in tre minuti arrivava a chiedermi quasi tutto quello che
avevo; ma non accadde.
</p><p>
Corsi così per un minuto, forse due; voltandomi verso destra vidi il fiume, e
capii di correre alla velocità della corrente: avrei dovuto essere in bicicletta
per andare così forte. Ma ancora le gambe mi tenevano, senza neanche particolare
fatica, e il fiato pure.
</p><p>
E come al mio primo volo di qualche giorno prima, mi sentti leggero e vidi tutto
bianchiccio. Corsi come in sogno.
</p><p>
Quando mi fermai ero effettivamente stanco, avevo il fiato corto e mi dolevano
appena le gambe. Ed ero perso in mezzo alla campagnia, fuori fuori città lungo
il fiume. Mi guardai attorno, cercando punti di riferimento e tutto quello che
trovai fu una luce lontana su un campanile. Con un lieve sforzo di memoria, mi
convinsi che quello fosse un paesino ad una decina di chilometri a sud.
</p><p>
Forse avevo effettivamente coperto dieci chilometri in un paio di minuti di
corsa, ma non ero troppo sicuro del paesello. Avrei dovuto attraversare tutta la
valle, perché lì il fiume percorreva una larghissima ansa, ed il paese era tutto
dall'altra parte. Non avevo ponti in vista, e per quanto ne sapessi non ce
n'erano per una buona distanza, in entrame le direzioni; passai il fiume al
volo, con un certa attenzione ad eventuali presenze nei dintorni, passando basso
sull'acqua. Giunto dall'altra lato, non vi trovai né una pista ciclabile, né una
strada, ma soltanto campi e campi, una larga spianata di terra, alberi spogli,
pali di cemento, qualche casetta...
</p><p>
Mi rimisi a correre non appena misi piede a terra, contenendomi un pochino,
stavolta. Non da correre tanto forte da veder svanire i confini degli oggetti
come prima, un po' meno. Corsi comunque molto più in fretta di quanto facessi
normalmente, più velocemente di quanto avessi mai fatto in gara, probabilmente
più in fretta di quanto fossi mai andato in bici con la stessa pendenza.
</p><p>
Correre sulla terra non è certo facile come lo è correre sull'asfalto, ma
almeno, essendo quella una notte di novembre, la temperatura era
sufficientemente bassa da rendere il terreno (anche la terra smossa dei campi)
abbastanza dura da non affondarci dentro.
</p><p>
Corsi per un minuto circa, passando di campo in campo. Vista la velocità che
riuscivo a mantenere, ebbi la confidenza di poter saltare i fossi che incontrai
di tanto in tanto, larghi anche quattro o cinque metri, senza alcun problemi.
Provai quindi sia il salto in lungo che il salto in alto. Con un paio di prove
in progressione, vidi che potevo saltare sopra alle vigne, ai meli, alle
casette; e li passavo anche di parecchio.
</p><p>
Lasciai stare questi esperimenti poiché ben presto arrivai a quel paese di cui
avevo riconosciuto il campanile, seguii un tratto di strada fino ad arrivare
alla prima casa del paese e verificai il nome sul cartello. Ero effettivamente a
dodici chilometri da casa, secondo le indicazioni stradali.
</p><p>
Ricordai allora quella scena del film `Superman' in cui il giovane Clark perde
lo scuolabus e corre a scuola accanto alla ferrovia, passando accanto al treno e
seguendolo per un po'. Lungo il fiume, oltre alla pista ciclabile, passano anche
i binari. Allora me ne tornai sui miei passi, saltando fossi, alberi e casette
lungo il percorso, fino a tornare al fiume; sull'argine staccai il salto più
forte che potei, dopo la rincorsa più folle che avessi mai preso (un centinaio
di metri, credo). Passai il fiume. E lì la distanza da argine ad argine era
abbondantemente sopra i quaranta metri. Wow.
</p><p>
Poi mi portai fin sulla ferrovia e attesi che arrivasse il treno. Non che
abitassi al centro del mondo, anzi, casa mia stava in una città persa tra le
montagne, ma comunque capoluogo di provincia, traffico commerciale, centro
turistico, musei a livello mondiale, cose interessanti, laghi, montagne,
castelli, vini.
</p><p>
Non passò alcun treno per mezz'ora. E cominciava ad essere tardi. Non è che
non avessi tempo, ma starsene da soli di notte al freddo dopo aver scoperto di
poter correre come un treno (beh, forse, comunque molto molto in fretta) non
sembrò una prospettiva allettante.
</p><p>
Ma potendo correre come un treno, forse avrei potuto seguirne uno. Presi quindi
a seguire le rotaie, giù e giù di corsa verso sud. Ci volle quasi un'ora intera
perché mi trovassi un treno davanti; a quel punto ero quasi fuori provincia. E
comunque, sì, mi girai e corsi dietro al treno; lo superai anche.
</p><p>
Tornando verso casa, poi, provai per la seconda volta a correre più che potei:
facendo un paio di conti stiami d'aver toccato i 150 chilometri orari. Erano più
delle famose 88 miglia orarie necessarie per viaggiare nel tempo. Ma non avevo
con me un aggeggio pieno di lucine a forma di Y.
</p><p>
Me ne tornai a casa, vagamente stanco, un po' correndo, un po' volando.
</p><p>
Il giorno dopo era sabato. E senza volerlo mi svegliai ancora una volta troppo
presto, verso le quattro di mattina. Sbrigati senza neanche pensare i compiti
assegnati per il lunedì successivo, mi misi pigramente a fare anche tutti gli
altri, tutto ciò di cui avevo certezza. Ovviamente, saltai tutte le parti che
prevedevano lettura. Non aprii nemmeno alcuni libri: annotati sul diario avevo
le pagine con gli esercizi, non ebbi necessità di consultare il testo perché
conoscevo a memoria anche le domande, oltre alle risposte.
</p><p>
Scrissi tutto quello che avevo da scrivere per martedì e mercoledì. Poi,
controllando ciò che avevo segnato per giovedì mi resi conto di due cose: non
ricordavo esattamente quello che avevo scritto, soltanto quello che avevo letto;
mi ripromisi quindi di fissare in mente le pagine dopo averle scritte. L'altra
cosa che mi venne in mente fu che, potendo correre come le automobili in
autostrada, forse avrei potuto anche scrivere a quella velocità; pessima idea.
Nè le biro, né la carta sono fatte per sopportare quel genere di attriti:
spezzai la biro e strappai una pagina.
</p><p>
Altra biro, altra pagina, diedi un'occhiata all'originale e poi lo trascrissi,
quasi alla perfezione (la mano non era molto abituata a scrivere a razzo) e poi
completai quello che avevo da completare.
</p><p>
Ed erano soltanto le sei di mattina. Essendo sabato, probabilmente nessun altro
si sarebbe alzato prima di un paio d'ore.
</p><p>
E fu così che capii quanto essere superiore possa essere noioso.
</p><p>
