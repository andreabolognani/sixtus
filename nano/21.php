<h2>L'oracolo</h2>
<div class="intro"><p>
La parola chiave, per decifrare tutto, era `leggenda'.
</p></div><p>
Tutto quello che aveva a che fare con la `leggenda' in qualche modo, per noi
comuni mortali, era effettivamente realtà per tutti coloro che abitavano `di
là'.
</p><p>
Tutto quello che aveva a che fare con la `leggenda' in qualche modo, per gli
esseri magici `di là', era effettivamente storia. Era soltanto troppo vecchia e
troppo poco nota.
</p><p>
Più tardi scoprii come le prime \streghe\ malvagie avessero lavorato duramente
per ottenere il mondo come lo era allora.
</p><p>
In quel momento, tutto quello che i miei tre alleati morti sapevano dirmi fu che
una singola leggenda appariva chiara per tutti e tre: l'esistenza dell'oracolo
come voce e guida per tutti quelli che lo volessero.
</p><p>

</p></div><div class="section"><p>

</p><p>
``E quindi voi dite che io dovrei andare a cercare questo leggendario oracolo,
che magari non esiste nemmeno, per chiedergli come morire il più in fretta
possibile, senza soffrire?'' chiesi.
</p><p>
Quei tre annuirono sinceramente, con gli occhi sgrananti, pieni di fiducia.
</p><p>
``Ma andate un po' affanculo! Stronzi!''
</p><p>

</p></div><div class="section"><p>

</p><p>
Mi girai nel letto per dar loro le spalle. Che cazzo avevano in mente, questi,
venire a svegliarmi in tre per annunciarmi una tale cazzata? Questi raccontavano
storielle mentre io me ne dormivo in pace nel mio candido lettino, sperando che
il momento della morte arrivasse in fretta....
</p><p>
Oh, stavo veramente male, quel giorno.
</p><p>

</p></div><div class="section"><p>

</p><p>
Quando fui completamente sveglio, mi spiegarono meglio la faccenda.
</p><p>
Avevano ragionato a lungo, tutta la notte, ed avevo trovato delle indicazioni.
Vecchie storie dei lupi, dei corvi, delle \streghe\ narrano molte cose. Molte
cose fantasiose, altre, molto poche, narrano cose vere.
</p><p>
Mettendo insieme i pezzi, emerse qualcosa d'interessante. Non era mai stato
fatto prima, questo confronto, perché evidentemente o nessuno aveva mai
acquisito un lupo, un corvo e una \strega, oppure perché tutti coloro che
sapevano avevano la bocca chiusa.
</p><p>
Scoprimmo che alle \streghe\ era proibito assimilare sia un lupo che un corvo.
Era anzi meglio non assimilare nessuno. Dalla parte di lupi e corvi, il problema
non sussisteva, ma era comunque proibito avere a che fare con le \streghe.
</p><p>
Incominciai a temere di star scoperchiando il vaso di Pandora. E così in effeffi
stavo facendo.
</p><p>
Le leggende non dovrebbero venire incrociate.
</p><p>

</p></div><div class="section"><p>

</p><p>
Ragionammo assieme e dopo un po' vidi emergere chiaramente i contorni di un
complotto: esistevano un totale di nove aree proibite, nel mondo. Zone in cui
nessuno si sarebbe dovuto inoltrare, per motivi non meglio specificati.
</p><p>
Non in modo sensato, almeno.
</p><p>
Una di queste locazioni, guarda caso, sarebbe la periferia occidentale di
Pozzuoli.
</p><p>

</p></div><div class="section"><p>

</p><p>
Era quasi troppo classico per crederci.
</p><p>
Avevo letto parecchie teorie sulla locazione vicino al Vesuvio dell'ingresso
agl'Inferi, là dove Odisseo avrebbe parlato con l'oracolo, là dove Enea avrebbe
parlato con il padre.
</p><p>
La curiosità soprassò la paura.
</p><p>

</p></div><div class="section"><p>...</p></div><div class="section"><p>

</p><p>
\first{Il Vesuvio era davvero un bel pezzo di montagna. E là vicino abitava
effettivamente un oracolo. E quello che appresi lì sotto cambiò il destino del
mondo.}
</p><p>
Pur giungendo da Nord, passai in volo sopra l'intero Golfo di Napoli. Bello.
Frebbrile, nelle sue infinite attività.
</p><p>
Mi dispiacque un pochino non avere molto tempo per fermarmi ad osservare la
città. Ma la mia vita era in pericolo, e l'ombra di mille \streghe\ mi convinse
a proseguire nella mia ricerca.
</p><p>
Scesi dunque di quota e mi avvicinani a quella pozza odorosa ch'è il Lago
d'Averno. E' vero, non ci sono uccelli là attorno. E finché rimasi nei dintorni
ebbi modo di pentirmi d'aver l'olfatto di un lupo, perché rese tutto ancor più
insopportabile.
</p><p>
Esplorai tutto il lago, feci il giro un paio di volte. Sempre `di qua',
ovviamente. Seppur vero che il vero ingresso sarebbe stato probabilmente
occultato dalla magia e invisibile, quella restava una zona proibita.
</p><p>
Proibita anche alle \streghe, che non è poco. Immaginammo, io e i miei
\acquisiti, che potesse esserci un qualche genere di sorveglianza.
</p><p>
A quanto pare, no.
</p><p>

</p></div><div class="section"><p>

</p><p>
Ero ancora quasi completamente nuovo in quel mondo, e non ero ancora pienamente
in grado di afferrare le sue dinamiche.
</p><p>
Trovai poco credibile che dichiarare un luogo proibito fosse sufficiente ad
impedire che la gente ci entrasse. Ma erano parole di \strega, ed evidentemente
intimorivano abbastanza.
</p><p>
Beh, non me, almeno. Quindi, in teoria, avevo ragione. Così credevo.
</p><p>
Mi sbagliavo.
</p><p>

</p></div><div class="section"><p>

</p><p>
Al secondo giro completo, senza alcun indizio, senza una traccia, senza aver
trovato un buco che fosse uno, fui sul punto di arrendermi.
</p><p>
``Qui ci sono soltanto puzza e immondizia, cari miei. Che faccio?'' chiesi ad
alta voce.
</p><p>
Seguì un lungo silenzio. I dintorni del lago erano effettivamente spettrali, in
qualche modo. Rimasi ad ammirare quel tetro spettacolo.
</p><p>

</p></div><div class="section"><p>

</p><p>
Poi mi ricordai di una cosa: avevo letto che Omero sosteneva che in questi
luoghi, ai tempi del mito, vivessero i Cimmeri.
</p><p>
<<Là dei Cimmeri è il popolo e la città / di nebbia e nube avvolti>>
</p><p>
Un po' qua, un po' là, ai Cimmeri era spesso attribuita la condanna a vivere in
grotte sotterranee. O subacque, forse.
</p><p>
Mi tuffai nel lago.
</p><p>

</p></div><div class="section"><p>

</p><p>
Fu una pessima idea.
</p><p>
Era come nuotare nei liquami.
</p><p>

</p></div><div class="section"><p>

</p><p>
Ma fu anche un'ottima idea.
</p><p>
Perché trovai un buco.
</p><p>

</p></div><div class="section"><p>

</p><p>
Trattenendo il fiato, più per non sentire l'odore che per evitare di affogare,
mi avvicinai al buco per dare un'occhiata.
</p><p>
Era un buco.
</p><p>
Non ero un grande esperto, l'acqua era scura e melmose e il fiato era poco.
Tornai in superficie, tossendo.
</p><p>
``Bene, ho trovato un buco'' dissi, secco.
</p><p>

</p></div><div class="section"><p>

</p><p>
Incerto sul da farsi, chiesi aiuto.
</p><p>
Persino da morti, Battesimo e Smeraldino non pareva avere alcuna intenzione
di avvicinarsi all'acqua. Stavano sulla riva ad aspettarmi.
</p><p>
Feci loro un segno, ma fecero finta di non vedermi. Accanto a loro stava anche
\camelia, in attesa.
</p><p>
``C'è forse qualcosa che potreste fare, per aiutarmi? Nulla nulla?'' insinuai.
</p><p>
Mi tuffai una seconda volta, riesaminai il buco. Nulla di nuovo, tornai su.
</p><p>
Non nascosi la mia delusione, fissando i miei inutili subordinati.
</p><p>
Infine, Camelia parlò ``Che ne diresti di una forma più adatta?''
</p><p>
E così dicendo fece dei gesti con la mano e mi fece perdere l'equilibrio. Finii
con la testa sott'acqua.
</p><p>
Sgranai gli occhi e m'accorsi di vederci meglio di prima. E di respirare. Ero un
qualche pesce. Ero uno `squalius lucumonis', un cavedano etrusco.
</p><p>
``Non credevo che potessi farlo''
</p><p>
``Non posso. Tu puoi''
</p><p>
``Ah. E non potevi scegliere un pesce più grande?''
</p><p>
Il cavedano etrusco, tipico dell'Ombrone, non è più lungo di 20 centimetri. Non
ebbi risposta.
</p><p>

</p></div><div class="section"><p>

</p><p>
Nuotai lungo il tunnel. Era un passaggio naturale, vulcanico probabilmente.
</p><p>
Era bello lungo, mi ci vollero una decina di minuti. O forse era corto e mi ci
volle molto tempo perché era lungo una spanna.
</p><p>
Comunque, giunsi ad una grotta. All'ingresso sotterraneo di una grotta.
</p><p>
Vedevo chiaramente il pelo dell'acqua. Mi misi la testa fuori e cominciai a
soffocare, perché ero ancora pesce.
</p><p>
``Attento alla forma che scegli, \maestro'' m'apostrofò la \strega.
</p><p>
Irritato, passai all'arancione e tornai del mio aspetto normale. Per normale
intendo non solo il mio aspetto umano, ma anche l'abbigliamento completamente
nero che avevo utilizzato durante il duello nel bosco.
</p><p>
Credetti di dovermi mostrare abbastanza elegante, se ci fosse veramente stato un
oracolo.
</p><p>

</p></div><div class="section"><p>

</p><p>
Mi addentrai nella grotta.
</p><p>

</p></div><div class="section"><p>...</p></div><div class="section"><p>

</p><p>
\first{La grotta non era adornata, era completamente naturale. E non mandava
alcuna luce.}
</p><p>
Passai `di là' per un attimo appena, per verificare che non vi fossero illusioni
potenti. Andai avanti lungo il percorso naturale che mi si presentava davanti,
più o meno scendendo verso il basso.
</p><p>

</p></div><div class="section"><p>

</p><p>
Infine, arrivai in fondo alla grotta. Se prima era una sorta di galleria
discendente, ora si allargava in una gigantesca volta.
</p><p>
``Salute, \maestro\ \corvino. Benvenuto'' disse una voce.
</p><p>
Ne cercai la fonte, e vidi una grigia sagoma in piedi sopra un grosso sasso.
</p><p>
Era un vecchio, vestito alla greca.
</p><p>

</p></div><div class="section"><p>

</p><p>
\character{Tiresia}.
</p><p>

</p></div><div class="section"><p>

</p><p>
``E' vera quella storia dei serpenti?''
</p><p>
``Tsk. Chiedono tutti la stessa cosa'' rispose.
</p><p>
``Tutti chi?''
</p><p>
``Odisseo, Edipo, Creonte, Dante'' elencò quello, nostalgico.
</p><p>

</p></div><div class="section"><p>

</p><p>
``Come hai distinto il maschio dalla femmina? E con che tagliasti loro la
testa?
</p><p>
``Per la prima, hai osservato le differenze del tema del colore? Hai contato le
squame retroclocali? Hai premuto la cloaca per far fuoriscire i genitali?
</p><p>
``Per la seconda, hai mozzato la testa? O li hai tagliati in due per lungo,
dividendo la mandibola dalla mascella?''
</p><p>

</p></div><div class="section"><p>

</p><p>
Mi osservò basito. Poi disse ``Non credevo che avresti veramente chiesto anche
questo. Dovevo sentirlo.''
</p><p>
Poi si avvicinò, fece per mettermi una mano sulla spalla, ma poi si fermò, mi
puntò gli occhi addosso e disse:
</p><p>
``Ho tranciato di netto con il mio pugnale il serpente che dava, e la seconda
volta il serpente che prendeva''
</p><p>
Già. Non doveva essere stato poi così difficile, viste le circostanze. Ma lui
proseguì senza lasciarmi il tempo di controbattere ``Va, ora. Lui ti aspetta''
</p><p>
E fece un segno con la mano.
</p><p>

</p></div><div class="section"><p>

</p><p>
Chi mi stava aspettando? Non era forse lui l'oracolo? Chi altri?
</p><p>
Lo fissai, sperduto.
</p><p>
``Non credere'' disse ``che questo vecchio fantasma possa esserti utile. Lo
potevo essere ai tempi del mito, ma sono battuto e imprigionato''
</p><p>
Solo allora notai come fosse semitrasparente, come i lembi dei suoi vestiti
svolazzassero senza vento, come avesse catene ai polsi e alle caviglie.
</p><p>
Mi indicò un altro grosso sasso, accanto al suo. Sopra quel sasso c'era un
piccolo oggetto, uno scrigno.
</p><p>
``L'\oracolo\ è là dentro e ti aspetta. Aprilo'' disse \character{Tiresia}.
</p><p>
Quando mi voltai lui non c'era più. Era svanito. Immaginai che fosse tornato a
scontare la sua condanna.
</p><p>
Lo scrigno era scarsamente decorato e decisamente consunto. Avrebbe potuto avere
tutti i millenni di storia del mondo alle spalle, per quanto pareva vecchio.
</p><p>
Mi feci scrupolo a toccarlo, perché temevo che potesse andare in pezzi o
polverizzarsi, come le mummie quando si apre il loro sarcofago.
</p><p>

</p></div><div class="section"><p>

</p><p>
``Che cos'è, questo?'' chiesi.
</p><p>
I miei tre \acquisiti\ comparvero al mio fianco e fissarono me, poi lo scrigno,
poi me.
</p><p>
\smeraldino, non potendo resistere alla sua natura di nero rapace, mi invitò
``Dai, apri! Apri! Aprilo!''
</p><p>
Ma Camelia mi fermò ``Aspetta. C'è scritto qualcosa''
</p><p>
Io non vidi nulla. Ma già allora avevo la sensazione che Camelia non avesse
mai torto. Chiesi per sicurezza:
</p><p>
``Dove? Io non vedo nulla''
</p><p>
``Vai `di là'. Tutto apparirà più chiaro'' disse lei.
</p><p>
``E' pericoloso''
</p><p>
``Sì. Molte cose lo sono. Ma questo è per te soltanto, devi vederlo''
</p><p>
Sapevo che nel momento esatto in cui fossi passato `di là' avrei avuto una
visione più chiara della faccenda, ma sapevo anche che quell'area era proibita.
Proibita dal circolo interno. Se il luogo fosse stato sorvegliato, sarebbe
scattato un qualche allarme, e la reazione mi avrebbe cancellato dalla faccia
della Terra.
</p><p>
Ma mi fidai della mia \strega.
</p><p>
Passai `di là'.
</p><p>

</p></div><div class="section"><p>

</p><p>
Sul forziere, a chiare lettere, era inciso un messaggio. Diceva `Al
multicolore'.
</p><p>
``Sono io!'' dissi.
</p><p>
``Toccalo'' mi consigliò \camelia.
</p><p>
Toccai l'incisione. Quella baluginò, i colori e i segni si rimenscolarono e il
messaggio cambiò in `Il multicolore mostrerà i sette colori dell'Arcobaleno'.
</p><p>
``E' una prova'' affermò lei.
</p><p>
``Immagino che voglia che io assuma tutti e sette i colori. Ora lo faccio''
</p><p>
Passai in successione il giallo, l'arancione, il rosso, il violetto, il verde,
il blu, l'indaco.
</p><p>
Non accadde nulla.
</p><p>

</p></div><div class="section"><p>

</p><p>
Nulla di nulla.
</p><p>

</p></div><div class="section"><p>

</p><p>
``Ne ho forse scordato qualcuno? Non mi pare...'' balbettai.
</p><p>
Seguì un silenzio imbarazzante. Mi voltai in cerca del fantasma, in cerca
d'aiuto, in cerca d'indizi. C'eravamo soltanto io e i miei tre compagni.
</p><p>
Uhm...
</p><p>
``C'è qualche altra possibile interpretazione di `multicolore', per caso?''
</p><p>
Fu un'ottima domanda.
</p><p>
Ci vidi giusto: `multicolore' può riferirsi non solo a colui che può assumere
colori diversi (quasi tutte le \streghe\ hanno almeno tre colori, come
\camelia), ma anche a coloro che sono in grado di assumere più colori
contemporaneamente.
</p><p>
``L'hai mai fatto, \camelia?'' chiesi.
</p><p>
``Non proprio. Forse'' riflettè ``per tenere alta la guardia mentre attacco. Ma
sai, io non sono una grande esperta di lotta. E poi la nozione di colori non è
propriamente corretta''
</p><p>
Lunga storia in breve, esiste una vasta e complessa teoria streghesca, per cui i
colori non sarebbero la giusta interpretazione da dare alle vie della magia.
Tentò di spiegarmelo molte volte, ma l'unico risultato era farmi ricordare di
quel dannato professore di filosofia. Possano morire nel fuoco lui e questa
streghesca teoria, che tra l'altro è sbagliata.
</p><p>
Provai.
</p><p>
Misi due colori assieme.
</p><p>
In fondo, m'ero dimostrato in grado di colpire molto forte e molto in fretta
diventando in parte violetto, in parte giallo.
</p><p>
Ora bastava che pensassi di dover correre molto in fretta con il giallo, di
dover colpire duro e pesante con l'indaco, di leggere la mente con il rosso, di
tener sollevato da terra il mio avversario con il violetto, di cambiare forma
con l'arancione, di guarire mentre attaccavo con il verde, e di invocare il
fulmine sulla sua testa con il blu.
</p><p>
Una morte terribile, per il mio ipotetico avversario. E uno sforzo terribile
per la mia testolina.
</p><p>
Inutile dire che non funzionò: passai per i vari colori, molto più in fretta di
prima; ma uno alla volta.
</p><p>
Chiesi la collaborazione dei miei subalterni e cominciai con l'alzarmi in volo.
Finché non fosse sceso, sarei rimasto giallo almeno un po'. Poi presi un sasso e
lo sollevai; finché il sasso avesse volato con me, ci sarebbe stato il violetto.
</p><p>
Poi pensai a quanto fossi stanco e volli un'aspirina. Intervenne il verde e
rimase. Poi cambiai vestiti, utilizzando l'arancione. Ma l'effetto svanì presto.
Decisi di concentrarmi su altro, e sperai di fare in fretta. Più mal di testa mi
aiutò a desiderare più verde, ma non aiutò molto per il resto.
</p><p>
Tentai di plasmare la forma del sasso, e questo attivò il blu. Mantenere il
sasso molle mi aiutò a focalizzare anche questo colore.
</p><p>
Chiesi allora agli \acquisiti\ di immaginare cose a caso, e tentai indovinare il
loro pensiero; con questo sfoderai il rosso. Indovinai cosa pensavano, ma
l'effetto non durò.
</p><p>
Cominciavo ad essere stanco. Pensai che forse una canzone sarebbe stata più
duratura e chiesi loro di pensare al testo e di cantare nella propria mente.
Seguire le parole che scorrevano rese più duraturo l'effetto del rosso.
</p><p>
A quel punto, sul punto di svenire per lo sforzo, puntai il sasso, cambiai i
miei vestiti con un paio di pantaloni attillati, per metà rosso fuoco, per metà
verde, un paio di stivali, niente maglietta ed una maschera da lottatore sul
volto. E questo fu l'arancione. Fortificai la mano sinistra con tutto ciò che mi
restava e questo fu l'indaco.
</p><p>
Sferrai un pungo diretto verso il sasso volante e malleabile, che andò in pezzi.
In bolle, per la verità. Si spalmò per mezza caverna.
</p><p>
Caddi a terra stremato e persi tutti i colori, tranne il verde. Il verde mi
serviva ancora e mi sarebbe servito per il quarto d'ora successivo. Imparai che
tenere più d'un colore alla volta può essere estremamente faticoso.
</p><p>

</p></div><div class="section"><p>

</p><p>
Dopo essermi riposato e ripreso, tornai a controllare lo scrigno. Se fosse stato
intatto, probabilmente mi sarei arreso, me ne sarei uscito e mi sarei lasciato
ammazzare dalla prima \strega\ che passasse di lì.
</p><p>
Ma era aperto. Bene.
</p><p>
Dentro, ironia della sorte, c'era un secondo forziere. Mpf.
</p><p>

</p></div><div class="section"><p>

</p><p>
Anche il secondo forziere pareva vecchio come il mondo, ma era indubbiamente
meglio conservato.
</p><p>
Ed anche questo aveva una scritta incisa su di esso. Questa però diceva `Al
trasparente'.
</p><p>
Spensi i colori. Tutti.
</p><p>
E non accadde nulla.
</p><p>
Nulla di nulla.
</p><p>

</p></div><div class="section"><p>

</p><p>
Mi ricordai della procedura corretta e toccai il testo. Quello cambiò e divenne
``Il trasparente non mostrerà alcun colore''. Tenni spenti tutti i miei colori.
</p><p>
E non accadde nulla.
</p><p>

</p></div><div class="section"><p>

</p><p>
Mi sentii come preso in giro. Mostrare i miei poteri soltanto per doverli
nascondere. Agli occhi di un forziere.
</p><p>
Pensai che, in qualche modo, stessi facendo qualcosa che poteva turbare il
forziere. Nel senso che stessi mostrando qualche colore anche senza volerlo. Mi
guardai attorno per accertare di essere solo ed effettivamente mi ricordai di
avere con me tre intrusi.
</p><p>
Tanto per provare, chiesi loro di andarsene fino a quando non li avessi
richiamati. Ubbidirono senza discutere, come si addice a buoni servitori.
</p><p>
Fissai il forziere, gli feci notare come fossimo veramente soli, lui ed io, e
spensi tutti i colori. Sentii un `click'.
</p><p>
Per prima cosa, richiami i miei \acquisiti, perché volevo che assistessero. Mi
avvicinai, aprii il forziere con entrambe le mani e vi trovai qualcosa avvolto
