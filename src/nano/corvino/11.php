Una di queste fu il cane.
</p><p>
E non era un cane. Era troppo grosso, troppo scuro, troppo cattivo per essere un
cane: doveva essere un lupo, uno che aveva mangiato almeno un orso.
</p><p>
Nel sogno, ero su una panchina, con un libro in mano. Lungo un fiume. Leggevo il
Conte di Montecristo. Il secondo volume. D'un tratto, mi passa davanti una
ragazza. Cammina svelta svelta sull'altro marciapiede. Se ne va.
</p><p>
Proseguo nella lettura, sono accanto ad Edmond, sulla barca mentre le guardie lo
portano al castello d'If. Vedo Mercedes salutarlo da lontano, sulla rupe.
</p><p>
Questa scena è diversa nel libro; ed è anche nella prima metà, non nella
seconda. Poi la ragazza di prima mi passa davanti, di nuovo. Stavolta è da
questo lato. Mi alzo e la seguo. Continuo a leggere. Edmond si finge morto,
viene gettato in mare. Seguo la ragazza attraversando un ponte. O forse un
cavalcavia. Edmond nuota verso la salvezza, incontra una barca che lo recupera,
si finge vittima di naufragio.
</p><p>
Corro dietro alla ragazza, perché cammina come il vento. Svolta su per una
rampa, la seguo. Suona un campanello, entra in casa. Non posso più seguirla.
Vedo una casa circondata dagli alberi. Ho sete.
</p><p>
Mi riparo dal sole sotto un albero. Vedo due grosse luci verdi. Sono gli occhi
d'un cane. Non è un cane. E' un lupo? E' grande.
</p><p>
Nel sogno, lo fissai e lui fissò me. A lungo.
